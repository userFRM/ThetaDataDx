#!/usr/bin/env bash
# regen_byte_identical.sh — verify that the build-time generators produce
# byte-identical output across two runs of the same tree.
#
# The generator pipeline lives under `crates/thetadatadx/build_support/*`
# and feeds two sink sets:
#
#   1. `target/<profile>/build/thetadatadx-*/out/*_generated.rs`
#      (consumed by the crate's own source via `include!`).
#   2. Checked-in SDK + tools surfaces (`sdks/**/*_generated*`, the per-
#      language tick/fpss/streaming wrapper files, and the CLI/MCP
#      utilities) rewritten by `cargo run --bin generate_sdk_surfaces
#      --features config-file`.
#
# The test captures a SHA-256 snapshot of both sinks, force-reruns the
# build script (by touching build.rs) AND the sdk_surfaces binary, and
# diffs the snapshots. Any non-empty diff means the generators now
# depend on something unstable (HashMap iteration order, unsorted
# input, environment, etc.) — a drift bug that will bite every PR.
#
# HOW TO RUN:
#   bash crates/thetadatadx/tests/regen_byte_identical.sh
#
# Exit code 0 = byte-identical. Non-zero = drift detected; the diff is
# printed on stderr with file paths + before/after hashes.
#
# Run from the REPO ROOT — the script detects this itself and aborts if
# Cargo.toml is not on the caller's PWD.

set -euo pipefail

if [ ! -f "Cargo.toml" ] || ! grep -q 'workspace' Cargo.toml 2>/dev/null; then
    echo "error: run from the repo root (Cargo.toml with [workspace] must exist)" >&2
    exit 2
fi

TMPDIR="$(mktemp -d)"
trap 'rm -rf "$TMPDIR"' EXIT

BEFORE="$TMPDIR/before.sha256"
AFTER="$TMPDIR/after.sha256"

capture() {
    local out="$1"
    : > "$out"
    # OUT_DIR artifacts (from build.rs). Cargo creates one output directory
    # per fingerprint — the hash dir varies across rebuilds, so we strip
    # it and dedupe by basename + content.
    find target -path "*/build/thetadatadx-*/out/*" -type f \
        \( -name "*_generated.rs" -o -name "beta_endpoints.rs" \) 2>/dev/null | \
      while read -r f; do
        printf '%s  out/%s\n' "$(sha256sum "$f" | awk '{print $1}')" "$(basename "$f")"
      done | sort -u >> "$out"

    # Checked-in SDK + tools surfaces discovered by header-marker scan +
    # by file-name pattern. Keeps the matcher robust if the generator
    # adds another file in the future.
    find sdks tools -type f \
        \( -name "*_generated*" -o -name "*.inc" -o -name "streaming_methods.rs" \
           -o -name "utility_functions.rs" -o -name "utilities.rs" \
           -o -name "fpss_methods.go" -o -name "utilities.go" \
           -o -name "fpss_event_classes.rs" -o -name "buffered_event.rs" \
           -o -name "tick_classes.rs" -o -name "tick_arrow.rs" \
           -o -name "historical_methods.rs" -o -name "historical.go" \
           -o -name "validate.go" -o -name "tick_converters.go" -o -name "tick_structs.go" \
           -o -name "fpss_event_structs.go" -o -name "endpoint_options.go" \) 2>/dev/null | \
      while read -r f; do
        if [ -f "$f" ] && head -3 "$f" | grep -qiE "@generated|DO NOT EDIT|Code generated|generated by build|generated by generate_sdk"; then
            sha256sum "$f"
        fi
      done | sort >> "$out"
}

echo "==> Baseline: force-regen and snapshot"
touch crates/thetadatadx/build.rs
cargo build -p thetadatadx --quiet
cargo run -p thetadatadx --bin generate_sdk_surfaces --features config-file --quiet >/dev/null
capture "$BEFORE"
before_lines=$(wc -l < "$BEFORE")
echo "    captured $before_lines file hashes"

echo "==> Regenerate and re-snapshot"
touch crates/thetadatadx/build.rs
cargo build -p thetadatadx --quiet
cargo run -p thetadatadx --bin generate_sdk_surfaces --features config-file --quiet >/dev/null
capture "$AFTER"

if diff -u "$BEFORE" "$AFTER"; then
    echo "OK: generators are byte-identical across $before_lines files"
    exit 0
else
    echo "FAIL: generator output drifted between two runs" >&2
    echo "      the above diff shows changed SHA256 hashes." >&2
    exit 1
fi
