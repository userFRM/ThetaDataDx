//! Go SDK emitter.
//!
//! Produces three outputs:
//! * `sdks/go/endpoint_options.go` — `With<Xxx>` option-builder scaffolding.
//! * `sdks/go/historical.go` — one method per endpoint on `*Client`.
//! * `sdks/go/endpoint_with_options.h.inc` — cgo extern decls for the
//!   `_with_options` FFI entry points.

use std::fmt::Write as _;

use super::super::helpers::{
    ffi_free_fn, ffi_header_return_type, go_c_var_name, go_converter_name, go_method_arg_decl,
    go_result_type, is_streaming_endpoint, method_params, sdk_method_arg_name, to_camel_case,
    to_go_exported_name,
};
use super::super::model::{GeneratedEndpoint, GeneratedParam};

pub(super) fn render_go_options(params: &[GeneratedParam]) -> String {
    let mut out = String::new();
    out.push_str("// Code generated by build.rs from endpoint_surface.toml; DO NOT EDIT.\n\n");
    out.push_str("package thetadatadx\n\n");
    out.push_str("/*\n#include \"ffi_bridge.h\"\n*/\nimport \"C\"\n\n");
    out.push_str("import (\n");
    out.push_str("\t\"time\"\n");
    out.push_str("\t\"unsafe\"\n");
    out.push_str(")\n\n");
    out.push_str("// EndpointRequestOptions contains the shared optional request fields projected from endpoint_surface.toml.\n");
    out.push_str("// TimeoutMs is the cross-cutting per-call deadline (W3); see WithTimeoutMs.\n");
    out.push_str("type EndpointRequestOptions struct {\n");
    for param in params {
        let field_name = to_go_exported_name(&param.name);
        let field_type = match param.param_type.as_str() {
            "Int" => "*int32",
            "Float" => "*float64",
            "Bool" => "*bool",
            _ => "*string",
        };
        writeln!(out, "\t// {}", param.description).unwrap();
        writeln!(out, "\t{} {}", field_name, field_type).unwrap();
    }
    out.push_str("\t// TimeoutMs is the per-call deadline in milliseconds. When set, on\n");
    out.push_str("\t// expiry the in-flight gRPC call is cancelled and the returned error\n");
    out.push_str("\t// carries \"Request deadline exceeded\". The Client handle stays usable\n");
    out.push_str("\t// for subsequent calls. See WithTimeoutMs / WithDeadline.\n");
    out.push_str("\tTimeoutMs *uint64\n");
    out.push_str("}\n\n");

    out.push_str("// EndpointOption applies one optional request field to an endpoint request.\n");
    out.push_str("type EndpointOption func(*EndpointRequestOptions)\n\n");
    out.push_str(
        "func collectEndpointRequestOptions(opts []EndpointOption) *EndpointRequestOptions {\n",
    );
    out.push_str("\tif len(opts) == 0 {\n\t\treturn nil\n\t}\n");
    out.push_str("\toptions := &EndpointRequestOptions{}\n");
    out.push_str(
        "\tfor _, opt := range opts {\n\t\tif opt != nil {\n\t\t\topt(options)\n\t\t}\n\t}\n",
    );
    out.push_str("\treturn options\n}\n\n");

    for param in params {
        let helper_name = format!("With{}", to_go_exported_name(&param.name));
        let field_name = to_go_exported_name(&param.name);
        let arg_type = match param.param_type.as_str() {
            "Int" => "int32",
            "Float" => "float64",
            "Bool" => "bool",
            _ => "string",
        };
        writeln!(out, "// {} sets {}.", helper_name, param.name).unwrap();
        writeln!(
            out,
            "func {}(value {}) EndpointOption {{",
            helper_name, arg_type
        )
        .unwrap();
        out.push_str("\treturn func(options *EndpointRequestOptions) {\n");
        out.push_str("\t\tvalueCopy := value\n");
        writeln!(out, "\t\toptions.{} = &valueCopy", field_name).unwrap();
        out.push_str("\t}\n");
        out.push_str("}\n\n");
    }

    // Cross-cutting per-call deadline (W3). The Go-idiomatic name is
    // WithTimeout (time.Duration) plus WithTimeoutMs (uint64) so callers can
    // pick whichever flavor matches their existing code.
    out.push_str(include_str!("templates/go/with_timeout_ms.go.tmpl"));
    out.push_str(include_str!("templates/go/with_deadline.go.tmpl"));

    out.push_str("func endpointRequestOptionsToC(opts *EndpointRequestOptions) (*C.TdxEndpointRequestOptions, func()) {\n");
    out.push_str("\tif opts == nil {\n\t\treturn nil, func() {}\n\t}\n\n");
    out.push_str("\tcOpts := &C.TdxEndpointRequestOptions{}\n\n");
    out.push_str("\tvar allocations []unsafe.Pointer\n");
    out.push_str("\tfree := func() {\n");
    out.push_str("\t\tfor _, allocation := range allocations {\n");
    out.push_str("\t\t\tC.free(allocation)\n");
    out.push_str("\t\t}\n");
    out.push_str("\t}\n\n");
    for param in params {
        let field_name = to_go_exported_name(&param.name);
        let template = match param.param_type.as_str() {
            "Int" => include_str!("templates/go/int_case.go.tmpl"),
            "Float" => include_str!("templates/go/float_case.go.tmpl"),
            "Bool" => include_str!("templates/go/bool_case.go.tmpl"),
            _ => include_str!("templates/go/string_case.go.tmpl"),
        };
        out.push_str(
            &template
                .replace("__FIELD__", &field_name)
                .replace("__PARAM__", &param.name),
        );
    }
    // Cross-cutting per-call deadline.
    out.push_str("\tif opts.TimeoutMs != nil {\n");
    out.push_str("\t\tcOpts.timeout_ms = C.uint64_t(*opts.TimeoutMs)\n");
    out.push_str("\t\tcOpts.has_timeout_ms = 1\n");
    out.push_str("\t}\n");
    out.push_str("\n\treturn cOpts, free\n");
    out.push_str("}\n");

    out
}

pub(super) fn render_go_endpoint_with_options_decls(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str("/* @generated DO NOT EDIT \u{2014} regenerated by build.rs from endpoint_surface.toml */\n");
    // All non-streaming endpoints now have a `_with_options` form (W3) so
    // every Go method can pass at least the cross-cutting timeout_ms.
    for endpoint in endpoints
        .iter()
        .filter(|endpoint| !is_streaming_endpoint(endpoint))
    {
        let ffi_name = format!("tdx_{}_with_options", endpoint.name);
        let return_type = ffi_header_return_type(&endpoint.return_type);
        let params = method_params(endpoint);
        write!(
            out,
            "extern {} {}(const TdxClient* client",
            return_type, ffi_name
        )
        .unwrap();
        for param in &params {
            if param.param_type == "Symbols" {
                write!(out, ", const char* const* symbols, size_t symbols_len").unwrap();
            } else {
                write!(out, ", const char* {}", param.name).unwrap();
            }
        }
        out.push_str(", const TdxEndpointRequestOptions* options);\n");
    }
    out
}

pub(super) fn render_go_historical(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str("// Code generated by build.rs from endpoint_surface.toml; DO NOT EDIT.\n\n");
    out.push_str("package thetadatadx\n\n");
    out.push_str("/*\n#include \"ffi_bridge.h\"\n*/\nimport \"C\"\n\n");
    out.push_str("import (\n");
    out.push_str("\t\"fmt\"\n");
    out.push_str("\t\"runtime\"\n");
    out.push_str("\t\"unsafe\"\n");
    out.push_str(")\n\n");
    // cgo-thread-local correctness (W3 round-3): the FFI stores the last
    // error in a Rust `thread_local!`. Go's runtime can migrate a
    // goroutine to a different OS thread between successive cgo calls, so
    // reading the error slot after a call on a different thread returns
    // stale/empty data. Every generated wrapper pins the goroutine to
    // one OS thread for the duration of the clear/call/check sequence
    // via runtime.LockOSThread. See
    // [docs/dev/w3-async-cancellation-design.md].

    for endpoint in endpoints
        .iter()
        .filter(|endpoint| !is_streaming_endpoint(endpoint))
    {
        out.push_str(&render_go_endpoint_method(endpoint));
        out.push('\n');
    }

    out
}

fn render_go_endpoint_method(endpoint: &GeneratedEndpoint) -> String {
    let method_name = to_go_exported_name(&endpoint.name);
    let method_params = method_params(endpoint);
    let mut out = String::new();

    let mut signature_parts = method_params
        .iter()
        .map(|param| go_method_arg_decl(param))
        .collect::<Vec<_>>();
    // Every endpoint accepts opts now (W3): even if no builder params are
    // declared, callers can pass WithTimeoutMs / WithDeadline.
    signature_parts.push("opts ...EndpointOption".into());
    writeln!(
        out,
        "func (c *Client) {}({}) ({}, error) {{",
        method_name,
        signature_parts.join(", "),
        go_result_type(&endpoint.return_type)
    )
    .unwrap();

    // Pin the goroutine to a single OS thread for the whole FFI sequence
    // (clear error slot -> cgo call -> read error slot). The error slot
    // is a Rust thread_local; reading it on a different OS thread from
    // the one that set it would return empty (stale) data. See W3 round-3.
    out.push_str("\truntime.LockOSThread()\n");
    out.push_str("\tdefer runtime.UnlockOSThread()\n");

    let has_symbols = method_params
        .iter()
        .any(|param| param.param_type == "Symbols");
    if has_symbols {
        out.push_str("\tcSymbols, cSymbolsLen := symbolsToCArray(symbols)\n");
        out.push_str("\tdefer freeSymbolArray(cSymbols, cSymbolsLen)\n");
    }

    for param in method_params
        .iter()
        .filter(|param| param.param_type != "Symbols")
    {
        let var_name = go_c_var_name(param);
        let arg_name = to_camel_case(&sdk_method_arg_name(param));
        writeln!(out, "\t{} := C.CString({})", var_name, arg_name).unwrap();
        writeln!(out, "\tdefer C.free(unsafe.Pointer({}))", var_name).unwrap();
    }

    out.push_str(
        "\tcOpts, freeOpts := endpointRequestOptionsToC(collectEndpointRequestOptions(opts))\n",
    );
    out.push_str("\tdefer freeOpts()\n");

    // Clear any stale FFI error before the call so a successful empty
    // result and a failure (timeout, server error) sentinel can be told
    // apart by checking the error slot afterwards. W3 round-2 fix —
    // see ffi/src/lib.rs::tdx_clear_error.
    out.push_str("\tC.tdx_clear_error()\n");

    write!(
        out,
        "\tarr := C.tdx_{}_with_options(c.handle",
        endpoint.name
    )
    .unwrap();
    for param in &method_params {
        if param.param_type == "Symbols" {
            out.push_str(", cSymbols, cSymbolsLen");
        } else {
            write!(out, ", {}", go_c_var_name(param)).unwrap();
        }
    }
    out.push_str(", cOpts");
    out.push_str(")\n");

    if endpoint.return_type == "StringList" {
        // stringArrayToGo consults `tdx_last_error` to disambiguate
        // success-empty from failure-with-empty-sentinel.
        out.push_str("\treturn stringArrayToGo(arr)\n");
        out.push_str("}\n");
        return out;
    }

    // Tick endpoints have the same shape: empty array means either no rows
    // or a failure (e.g. timeout). Consult the error slot directly.
    writeln!(
        out,
        "\tif e := lastErrorRaw(); e != \"\" {{\n\t\t{}(arr)\n\t\treturn nil, fmt.Errorf(\"thetadatadx: %s\", e)\n\t}}",
        ffi_free_fn(&endpoint.return_type)
    )
    .unwrap();
    writeln!(
        out,
        "\tresult := {}(arr)",
        go_converter_name(&endpoint.return_type)
    )
    .unwrap();
    writeln!(out, "\t{}(arr)", ffi_free_fn(&endpoint.return_type)).unwrap();
    out.push_str("\treturn result, nil\n");
    out.push_str("}\n");
    out
}
