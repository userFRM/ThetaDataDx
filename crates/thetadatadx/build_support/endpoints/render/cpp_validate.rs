//! Live parameter-mode matrix validator generator for the C++ SDK.
//!
//! Emits `sdks/cpp/examples/validate.cpp`: one `cell(...)` lambda per
//! (endpoint, mode), behind a `std::packaged_task` + detached `std::thread`
//! timeout shim and `_Exit` teardown when any cell timed out.

use std::fmt::Write as _;

use super::super::helpers::{
    cpp_arg_literal, cpp_builder_setter, is_streaming_endpoint, method_params,
};
use super::super::model::GeneratedEndpoint;
use super::super::modes::test_modes_for;

/// Generate the C++ SDK validator (one row per (endpoint, mode) pair).
pub(super) fn render_cpp_validate(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT \u{2014} regenerated by generate_sdk_surfaces from endpoint_surface.toml\n",
    );
    out.push_str("// Live parameter-mode matrix validator for the C++ SDK. Every cell is\n");
    out.push_str("// attempted against production; the server is the ground truth for what\n");
    out.push_str("// the account can access. Cells whose documented min_tier exceeds the\n");
    out.push_str("// live account tier come back as a permission error and are classified\n");
    out.push_str("// SKIP: tier-permission. Real configuration bugs surface as FAIL. Cells\n");
    out.push_str("// that don't finish within 60 seconds classify as FAIL with \"timeout\n");
    out.push_str("// after 60s\". The worker thread is detached and keeps running the SDK\n");
    out.push_str("// call; to avoid a use-after-free race between that leaked thread and\n");
    out.push_str("// the Client/Config/Credentials destructors on main's unwind, the\n");
    out.push_str("// validator exits via std::_Exit whenever any timeout fired -- skipping\n");
    out.push_str("// destructors is exactly what we want when a leaked thread might still\n");
    out.push_str("// touch FFI state. When no timeouts fired the normal return path is\n");
    out.push_str("// taken so RAII runs as usual. See issues #287, #290.\n");
    out.push_str("#include <algorithm>\n");
    out.push_str("#include <cctype>\n");
    out.push_str("#include <chrono>\n");
    out.push_str("#include <cstdlib>\n");
    out.push_str("#include <fstream>\n");
    out.push_str("#include <future>\n");
    out.push_str("#include <iomanip>\n");
    out.push_str("#include <iostream>\n");
    out.push_str("#include <sstream>\n");
    out.push_str("#include <string>\n");
    out.push_str("#include <sys/stat.h>\n");
    out.push_str("#include <thread>\n");
    out.push_str("#include <utility>\n");
    out.push_str("#include <vector>\n");
    out.push_str("#include \"thetadx.hpp\"\n\n");
    out.push_str("namespace {\n\n");
    out.push_str("constexpr auto kPerCellTimeout = std::chrono::seconds(60);\n\n");
    out.push_str("std::string lower(std::string value) {\n");
    out.push_str(
        "    std::transform(value.begin(), value.end(), value.begin(), [](unsigned char c) {\n",
    );
    out.push_str("        return static_cast<char>(std::tolower(c));\n");
    out.push_str("    });\n");
    out.push_str("    return value;\n");
    out.push_str("}\n\n");
    out.push_str("struct CellRecord {\n");
    out.push_str("    std::string endpoint;\n");
    out.push_str("    std::string mode;\n");
    out.push_str("    std::string status;\n");
    out.push_str("    int row_count = 0;\n");
    out.push_str("    long long duration_ms = 0;\n");
    out.push_str("    std::string detail;\n");
    out.push_str("};\n\n");
    out.push_str("std::string json_escape(const std::string& in) {\n");
    out.push_str("    std::string out;\n");
    out.push_str("    out.reserve(in.size() + 2);\n");
    out.push_str("    for (char c : in) {\n");
    out.push_str("        switch (c) {\n");
    out.push_str("            case '\\\\': out += \"\\\\\\\\\"; break;\n");
    out.push_str("            case '\"':  out += \"\\\\\\\"\"; break;\n");
    out.push_str("            case '\\n': out += \"\\\\n\";  break;\n");
    out.push_str("            case '\\r': out += \"\\\\r\";  break;\n");
    out.push_str("            case '\\t': out += \"\\\\t\";  break;\n");
    out.push_str("            default:\n");
    out.push_str("                if (static_cast<unsigned char>(c) < 0x20) {\n");
    out.push_str("                    char buf[8];\n");
    out.push_str("                    std::snprintf(buf, sizeof(buf), \"\\\\u%04x\", c);\n");
    out.push_str("                    out += buf;\n");
    out.push_str("                } else {\n");
    out.push_str("                    out += c;\n");
    out.push_str("                }\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("    return out;\n");
    out.push_str("}\n\n");
    out.push_str("} // namespace\n\n");
    out.push_str("int main(int argc, char** argv) {\n");
    out.push_str("    const std::string creds_path = argc > 1 ? argv[1] : \"creds.txt\";\n");
    out.push_str("    int pass = 0;\n");
    out.push_str("    int skip = 0;\n");
    out.push_str("    int fail = 0;\n");
    out.push_str("    // Track whether any cell timed out. Leaked worker threads racing with\n");
    out.push_str("    // RAII Close() on client/config/creds would be UB, so we _Exit at end\n");
    out.push_str("    // if this flag is set instead of returning.\n");
    out.push_str("    bool any_timeout = false;\n");
    out.push_str("    std::vector<CellRecord> records;\n\n");
    out.push_str("    try {\n");
    out.push_str("        auto creds = tdx::Credentials::from_file(creds_path);\n");
    out.push_str("        auto config = tdx::Config::production();\n");
    out.push_str("        auto client = tdx::Client::connect(creds, config);\n\n");
    out.push_str(
        "        auto cell = [&](const char* endpoint, const char* mode, const char* declared_min_tier, auto&& call) {\n",
    );
    out.push_str("            const std::string label = std::string(endpoint) + \"::\" + mode;\n");
    out.push_str(
        "            CellRecord rec{endpoint, mode, std::string{}, 0, 0, std::string{}};\n",
    );
    out.push_str("            // If a prior cell timed out, its worker thread is still running\n");
    out.push_str(
        "            // an FFI call against `client`. The FFI is not thread-safe on the\n",
    );
    out.push_str("            // same handle (ffi/src/lib.rs:21), so we must NOT issue another\n");
    out.push_str("            // call on `client` now. Record the cell as SKIP:\n");
    out.push_str("            // aborted-after-timeout and continue so the artifact keeps the\n");
    out.push_str("            // full cell list (the agreement check can still pair cells\n");
    out.push_str("            // across SDKs).\n");
    out.push_str("            if (any_timeout) {\n");
    out.push_str(
        "                std::cout << \"  \" << std::left << std::setw(60) << label << \" SKIP: aborted-after-timeout\" << std::endl;\n",
    );
    out.push_str("                ++skip;\n");
    out.push_str("                rec.status = \"SKIP\";\n");
    out.push_str("                rec.detail = \"aborted-after-timeout\";\n");
    out.push_str("                records.push_back(rec);\n");
    out.push_str("                return;\n");
    out.push_str("            }\n");
    out.push_str("            const auto t0 = std::chrono::steady_clock::now();\n");
    out.push_str("            try {\n");
    out.push_str("                // std::packaged_task + detached std::thread so a timed-out\n");
    out.push_str("                // future doesn't block in its destructor (which std::async's\n");
    out.push_str("                // future does). Worker thread leaks until the SDK call\n");
    out.push_str("                // returns; we flag any_timeout so main exits via _Exit and\n");
    out.push_str("                // avoids destroying the Client handle while the leaked\n");
    out.push_str("                // thread may still be reading from it (UAF), and so that\n");
    out.push_str("                // subsequent cells don't race against the leaked worker.\n");
    out.push_str("                using Result = decltype(call());\n");
    out.push_str(
        "                std::packaged_task<Result()> task(std::forward<decltype(call)>(call));\n",
    );
    out.push_str("                auto future = task.get_future();\n");
    out.push_str("                std::thread(std::move(task)).detach();\n");
    out.push_str(
        "                if (future.wait_for(kPerCellTimeout) == std::future_status::timeout) {\n",
    );
    out.push_str(
        "                    std::cout << \"  \" << std::left << std::setw(60) << label << \" FAIL  timeout after 60s\" << std::endl;\n",
    );
    out.push_str("                    ++fail;\n");
    out.push_str("                    any_timeout = true;\n");
    out.push_str("                    rec.status = \"FAIL\";\n");
    out.push_str("                    rec.detail = \"timeout after 60s\";\n");
    out.push_str(
        "                    rec.duration_ms = std::chrono::duration_cast<std::chrono::milliseconds>(std::chrono::steady_clock::now() - t0).count();\n",
    );
    out.push_str("                    records.push_back(rec);\n");
    out.push_str("                    return;\n");
    out.push_str("                }\n");
    out.push_str("                auto value = future.get();\n");
    out.push_str("                rec.row_count = static_cast<int>(value.size());\n");
    out.push_str(
        "                std::cout << \"  \" << std::left << std::setw(60) << label << \" PASS\" << std::endl;\n",
    );
    out.push_str("                ++pass;\n");
    out.push_str("                rec.status = \"PASS\";\n");
    out.push_str("            } catch (const std::exception& e) {\n");
    out.push_str("                const std::string msg = lower(e.what());\n");
    out.push_str(
        "                if (msg.find(\"permission\") != std::string::npos || msg.find(\"subscription\") != std::string::npos) {\n",
    );
    out.push_str(
        "                    std::cout << \"  \" << std::left << std::setw(60) << label << \" SKIP: tier-permission (declared min_tier=\" << declared_min_tier << \")\" << std::endl;\n",
    );
    out.push_str("                    ++skip;\n");
    out.push_str("                    rec.status = \"SKIP\";\n");
    out.push_str("                    rec.detail = \"tier-permission\";\n");
    out.push_str(
        "                } else if (msg.find(\"no data found\") != std::string::npos) {\n",
    );
    out.push_str(
        "                    std::cout << \"  \" << std::left << std::setw(60) << label << \" PASS  (no data)\" << std::endl;\n",
    );
    out.push_str("                    ++pass;\n");
    out.push_str("                    rec.status = \"PASS\";\n");
    out.push_str("                    rec.detail = \"no data\";\n");
    out.push_str("                } else {\n");
    out.push_str(
        "                    std::cout << \"  \" << std::left << std::setw(60) << label << \" FAIL  \" << e.what() << std::endl;\n",
    );
    out.push_str("                    ++fail;\n");
    out.push_str("                    rec.status = \"FAIL\";\n");
    out.push_str("                    std::string d = e.what();\n");
    out.push_str("                    if (d.size() > 200) { d = d.substr(0, 200); }\n");
    out.push_str("                    rec.detail = std::move(d);\n");
    out.push_str("                }\n");
    out.push_str("            }\n");
    out.push_str(
        "            rec.duration_ms = std::chrono::duration_cast<std::chrono::milliseconds>(std::chrono::steady_clock::now() - t0).count();\n",
    );
    out.push_str("            records.push_back(rec);\n");
    out.push_str("        };\n\n");

    for endpoint in endpoints
        .iter()
        .filter(|endpoint| !is_streaming_endpoint(endpoint))
    {
        let mp = method_params(endpoint);
        for mode in test_modes_for(endpoint) {
            let mut args_parts: Vec<String> = mp
                .iter()
                .zip(mode.args.iter())
                .map(|(param, value)| cpp_arg_literal(param, value))
                .collect();
            if !mode.builder_overrides.is_empty() {
                let setters: String = mode
                    .builder_overrides
                    .iter()
                    .filter_map(|(name, value)| cpp_builder_setter(endpoint, name, value))
                    .collect();
                if !setters.is_empty() {
                    args_parts.push(format!("tdx::EndpointRequestOptions{{}}{setters}"));
                }
            }
            let args = args_parts.join(", ");
            writeln!(
                out,
                "        cell({:?}, {:?}, {:?}, [&] {{ return client.{}({}); }});",
                endpoint.name, mode.name, mode.min_tier, endpoint.name, args
            )
            .unwrap();
        }
    }

    out.push_str("    } catch (const std::exception& e) {\n");
    out.push_str(
        "        std::cerr << \"validator bootstrap failure: \" << e.what() << std::endl;\n",
    );
    out.push_str("        return 1;\n");
    out.push_str("    }\n\n");
    out.push_str(
        "    std::cout << \"\\nC++: \" << pass << \" PASS, \" << skip << \" SKIP, \" << fail << \" FAIL\" << std::endl;\n",
    );
    out.push_str(
        "    std::cout << \"COUNTS:\" << pass << \":\" << skip << \":\" << fail << std::endl;\n",
    );
    // Emit the per-cell JSON artifact for the cross-language agreement check.
    // Write relative to the working directory; the validate_release.sh wrapper
    // cd's into the repo root so artifacts land in the standard location.
    out.push_str("    try {\n");
    out.push_str("        const char* artifact_dir = \"artifacts\";\n");
    out.push_str("        mkdir(artifact_dir, 0755);\n");
    out.push_str("        std::ofstream artifact(\"artifacts/validator_cpp.json\");\n");
    out.push_str(
        "        artifact << \"{\\n  \\\"lang\\\": \\\"cpp\\\",\\n  \\\"records\\\": [\\n\";\n",
    );
    out.push_str("        for (size_t i = 0; i < records.size(); ++i) {\n");
    out.push_str("            const auto& r = records[i];\n");
    out.push_str(
        "            artifact << \"    {\\\"endpoint\\\": \\\"\" << json_escape(r.endpoint) << \"\\\", \";\n",
    );
    out.push_str(
        "            artifact << \"\\\"mode\\\": \\\"\" << json_escape(r.mode) << \"\\\", \";\n",
    );
    out.push_str(
        "            artifact << \"\\\"status\\\": \\\"\" << json_escape(r.status) << \"\\\", \";\n",
    );
    out.push_str("            artifact << \"\\\"row_count\\\": \" << r.row_count << \", \";\n");
    out.push_str("            artifact << \"\\\"duration_ms\\\": \" << r.duration_ms << \", \";\n");
    out.push_str(
        "            artifact << \"\\\"detail\\\": \\\"\" << json_escape(r.detail) << \"\\\"}\";\n",
    );
    out.push_str("            if (i + 1 < records.size()) { artifact << \",\"; }\n");
    out.push_str("            artifact << \"\\n\";\n");
    out.push_str("        }\n");
    out.push_str("        artifact << \"  ]\\n}\\n\";\n");
    out.push_str("        artifact.close();\n");
    out.push_str("        std::cout << \"artifact: artifacts/validator_cpp.json\" << std::endl;\n");
    out.push_str("    } catch (const std::exception& e) {\n");
    out.push_str("        std::cerr << \"artifact write failure: \" << e.what() << std::endl;\n");
    out.push_str("    }\n");
    out.push_str("    std::cout.flush();\n");
    out.push_str("    std::cerr.flush();\n");
    out.push_str("    if (any_timeout) {\n");
    out.push_str("        // Skip destructors -- leaked worker thread may still be reading\n");
    out.push_str("        // Client/Config/Credentials FFI handles. _Exit terminates the\n");
    out.push_str("        // process without unwinding.\n");
    out.push_str("        std::_Exit(fail > 0 ? 1 : 0);\n");
    out.push_str("    }\n");
    out.push_str("    return fail > 0 ? 1 : 0;\n");
    out.push_str("}\n");
    out
}
