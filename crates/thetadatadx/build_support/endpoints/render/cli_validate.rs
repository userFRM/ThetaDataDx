//! Live parameter-mode matrix validator generator for the CLI surface.
//!
//! Emits `scripts/validate_cli.py`: one subprocess-driven row per
//! (endpoint, mode) cell. CLI-specific caveat: builder-override cells are
//! skipped because clap positional args don't support targeted optional
//! injection.

use std::fmt::Write as _;

use super::super::helpers::{cli_command_tokens_for_mode, is_streaming_endpoint};
use super::super::model::GeneratedEndpoint;
use super::super::modes::test_modes_for;

/// Generate the CLI validator (one row per (endpoint, mode) pair).
pub(super) fn render_cli_validate(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str("#!/usr/bin/env python3\n");
    out.push_str(
        "# @generated DO NOT EDIT \u{2014} regenerated by generate_sdk_surfaces from endpoint_surface.toml\n",
    );
    out.push_str("\"\"\"Live parameter-mode matrix validator for the CLI surface.\n\n");
    out.push_str("Each row is one (endpoint, mode) cell. Modes cover concrete fixtures plus\n");
    out.push_str("wildcard / ISO-date / legacy-zero variants for option ContractSpec\n");
    out.push_str("endpoints. Every cell is attempted against production; the server is the\n");
    out.push_str("ground truth for what the account can access. Cells whose documented\n");
    out.push_str("`min_tier` exceeds the live account tier come back as a permission error\n");
    out.push_str("and are classified `SKIP: tier-permission`. Real configuration bugs\n");
    out.push_str("(invalid arguments, wire-format errors) surface as `FAIL`. See issue\n");
    out.push_str("#287.\n\n");
    out.push_str("Each cell is bounded by a 60-second subprocess timeout. A stuck cell\n");
    out.push_str("classifies as FAIL with `timeout after 60s` rather than stalling CI\n");
    out.push_str("indefinitely -- a human should investigate whether the fixture is too\n");
    out.push_str("broad or the server is misbehaving. See issue #290.\n\"\"\"\n");
    out.push_str("from __future__ import annotations\n\n");
    out.push_str("import json\n");
    out.push_str("import os\n");
    out.push_str("import pathlib\n");
    out.push_str("import subprocess\n");
    out.push_str("import sys\n");
    out.push_str("import time\n\n");
    out.push_str("PER_CELL_TIMEOUT_SECS = 60\n\n");
    out.push_str("REPO = pathlib.Path(__file__).resolve().parents[1]\n");
    out.push_str(
        "TDX = REPO / \"target\" / \"release\" / (\"tdx.exe\" if os.name == \"nt\" else \"tdx\")\n",
    );
    out.push_str("ARTIFACT_PATH = REPO / \"artifacts\" / \"validator_cli.json\"\n\n");
    out.push_str("# (endpoint, mode_name, declared_min_tier, [argv...])\n");
    out.push_str("# `declared_min_tier` is informational only (printed on tier-permission\n");
    out.push_str("# skips so you can see which modes the server refused).\n");
    out.push_str("CELLS = [\n");
    for endpoint in endpoints
        .iter()
        .filter(|endpoint| !is_streaming_endpoint(endpoint))
    {
        // CLI skips modes with builder overrides. The CLI's positional-arg
        // model means isolating a single optional would require passing
        // empty strings for the preceding optionals, which `insert_raw`
        // rejects. Rather than emit fake cells that look like they exercise
        // the optional but in fact send the bare concrete args (which was
        // the prior state Codex flagged), we drop those cells entirely.
        // The cross-language agreement script already handles cells missing
        // from one SDK as an info-level note. Follow-up: convert the CLI to
        // flag-style optionals (#290).
        for mode in test_modes_for(endpoint)
            .into_iter()
            .filter(|mode| mode.builder_overrides.is_empty())
        {
            let tokens = cli_command_tokens_for_mode(endpoint, &mode)
                .into_iter()
                .map(|token| format!("{token:?}"))
                .collect::<Vec<_>>()
                .join(", ");
            writeln!(
                out,
                "    ({:?}, {:?}, {:?}, [{}]),",
                endpoint.name, mode.name, mode.min_tier, tokens
            )
            .unwrap();
        }
    }
    out.push_str("]\n\n");
    out.push_str("if not TDX.exists():\n");
    out.push_str("    raise SystemExit(f\"missing CLI binary: {TDX}\")\n\n");
    out.push_str("creds = sys.argv[1]\n");
    out.push_str("pass_count = skip_count = fail_count = 0\n");
    out.push_str("records: list[dict] = []  # one record per cell for the agreement check\n\n");
    out.push_str("def _json_row_count(output: str) -> int:\n");
    out.push_str("    \"\"\"Count top-level rows from the CLI's --format json response.\n\n");
    out.push_str("    The CLI emits pretty-printed JSON (multi-line), so we parse the\n");
    out.push_str("    whole stdout rather than just the last line. For list responses\n");
    out.push_str("    returns len(list). For dict responses that look columnar (values\n");
    out.push_str("    are lists), returns len of any column. For dict responses that\n");
    out.push_str("    are a single object (e.g. calendar_open_today), returns 1.\n");
    out.push_str("    Non-JSON output returns 0.\n");
    out.push_str("    \"\"\"\n");
    out.push_str("    stripped = output.strip()\n");
    out.push_str("    if not stripped:\n");
    out.push_str("        return 0\n");
    out.push_str("    # The CLI may prefix with a warning / progress line before the JSON.\n");
    out.push_str("    # Walk backwards through possible JSON start tokens to find the\n");
    out.push_str("    # first that parses.\n");
    out.push_str("    for start_token in (\"[\", \"{\"):\n");
    out.push_str("        idx = stripped.find(start_token)\n");
    out.push_str("        if idx == -1:\n");
    out.push_str("            continue\n");
    out.push_str("        try:\n");
    out.push_str("            parsed = json.loads(stripped[idx:])\n");
    out.push_str("        except json.JSONDecodeError:\n");
    out.push_str("            continue\n");
    out.push_str("        if isinstance(parsed, list):\n");
    out.push_str("            return len(parsed)\n");
    out.push_str("        if isinstance(parsed, dict):\n");
    out.push_str("            # Columnar dict: treat any list-valued column as the row\n");
    out.push_str("            # vector. Keeps agreement with the Python SDK.\n");
    out.push_str("            for v in parsed.values():\n");
    out.push_str("                if isinstance(v, list):\n");
    out.push_str("                    return len(v)\n");
    out.push_str("            return 1\n");
    out.push_str("    return 0\n\n");
    out.push_str("for endpoint, mode, min_tier, argv in CELLS:\n");
    out.push_str("    label = f\"{endpoint}::{mode}\"\n");
    out.push_str("    t0 = time.monotonic()\n");
    out.push_str("    try:\n");
    out.push_str("        proc = subprocess.run(\n");
    out.push_str("            [str(TDX), \"--creds\", creds, *argv, \"--format\", \"json\"],\n");
    out.push_str("            cwd=REPO,\n");
    out.push_str("            stdout=subprocess.PIPE,\n");
    out.push_str("            stderr=subprocess.STDOUT,\n");
    out.push_str("            check=False,\n");
    out.push_str("            timeout=PER_CELL_TIMEOUT_SECS,\n");
    out.push_str("        )\n");
    out.push_str("    except subprocess.TimeoutExpired:\n");
    out.push_str("        duration_ms = int((time.monotonic() - t0) * 1000)\n");
    out.push_str(
        "        print(f\"  {label:60s} FAIL  timeout after {PER_CELL_TIMEOUT_SECS}s\")\n",
    );
    out.push_str("        fail_count += 1\n");
    out.push_str("        records.append({\n");
    out.push_str("            \"endpoint\": endpoint, \"mode\": mode, \"status\": \"FAIL\",\n");
    out.push_str(
        "            \"row_count\": 0, \"duration_ms\": duration_ms, \"detail\": \"timeout after 60s\",\n",
    );
    out.push_str("        })\n");
    out.push_str("        continue\n");
    out.push_str("    duration_ms = int((time.monotonic() - t0) * 1000)\n");
    out.push_str("    output = (proc.stdout or b\"\").decode(\"utf-8\", errors=\"replace\")\n");
    out.push_str("    msg = output.lower()\n");
    out.push_str("    row_count = 0\n");
    out.push_str("    status: str\n");
    out.push_str("    detail: str = \"\"\n");
    out.push_str("    if proc.returncode == 0:\n");
    out.push_str("        row_count = _json_row_count(output)\n");
    out.push_str("        status = \"PASS\"\n");
    out.push_str("        print(f\"  {label:60s} PASS\")\n");
    out.push_str("        pass_count += 1\n");
    out.push_str("    elif \"permission\" in msg or \"subscription\" in msg:\n");
    out.push_str("        status = \"SKIP\"\n");
    out.push_str("        detail = \"tier-permission\"\n");
    out.push_str(
        "        print(f\"  {label:60s} SKIP: tier-permission (declared min_tier={min_tier})\")\n",
    );
    out.push_str("        skip_count += 1\n");
    out.push_str("    elif \"no data found\" in msg:\n");
    out.push_str("        status = \"PASS\"\n");
    out.push_str("        detail = \"no data\"\n");
    out.push_str("        print(f\"  {label:60s} PASS  (no data)\")\n");
    out.push_str("        pass_count += 1\n");
    out.push_str("    else:\n");
    out.push_str("        status = \"FAIL\"\n");
    out.push_str("        detail = output.strip()[:200]\n");
    out.push_str("        print(f\"  {label:60s} FAIL  {output.strip()}\")\n");
    out.push_str("        fail_count += 1\n");
    out.push_str("    records.append({\n");
    out.push_str("        \"endpoint\": endpoint, \"mode\": mode, \"status\": status,\n");
    out.push_str(
        "        \"row_count\": row_count, \"duration_ms\": duration_ms, \"detail\": detail,\n",
    );
    out.push_str("    })\n");
    out.push('\n');
    out.push_str("print(f\"\\nCLI: {pass_count} PASS, {skip_count} SKIP, {fail_count} FAIL\")\n");
    out.push_str("print(f\"COUNTS:{pass_count}:{skip_count}:{fail_count}\")\n");
    out.push_str("ARTIFACT_PATH.parent.mkdir(parents=True, exist_ok=True)\n");
    out.push_str("ARTIFACT_PATH.write_text(json.dumps({\n");
    out.push_str("    \"lang\": \"cli\",\n");
    out.push_str("    \"records\": records,\n");
    out.push_str("}, indent=2, sort_keys=True))\n");
    out.push_str("print(f\"artifact: {ARTIFACT_PATH}\")\n");
    out.push_str("sys.exit(1 if fail_count > 0 else 0)\n");
    out
}
