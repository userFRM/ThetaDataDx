//! Emit `sdks/go/fpss_event_structs.go` — Go-idiomatic FPSS event types,
//! kind enum, control constants, and `FpssEvent` wrapper.

use std::fmt::Write as _;

use super::common::{go_scalar, snake_to_go_pascal};
use super::layout::{c_struct_offsets, c_struct_size, fpss_event_offsets, fpss_event_size};
use super::schema::{sorted_data_events, Schema};

pub(super) fn render_go_fpss_ffi_sizes(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str("// Code generated by build.rs from fpss_event_schema.toml. DO NOT EDIT.\n\n");
    out.push_str("package thetadatadx\n\n");
    out.push_str("const (\n");
    for (event_name, def) in sorted_data_events(schema) {
        writeln!(
            out,
            "\tCTdxFpss{event_name}ExpectedSize uintptr = {}",
            c_struct_size(&def.columns)
        )
        .unwrap();
    }
    out.push_str("\tCTdxFpssControlExpectedSize uintptr = 16\n");
    out.push_str("\tCTdxFpssRawDataExpectedSize uintptr = 24\n");
    writeln!(
        out,
        "\tCTdxFpssEventExpectedSize uintptr = {}",
        fpss_event_size(schema)
    )
    .unwrap();
    out.push_str(")\n");
    out
}

pub(super) fn render_go_fpss_offset_checks(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str("// Code generated by build.rs from fpss_event_schema.toml. DO NOT EDIT.\n\n");
    out.push_str("package thetadatadx\n\n");
    out.push_str("/*\n#include \"ffi_bridge.h\"\n*/\nimport \"C\"\n\n");
    out.push_str("import \"unsafe\"\n\n");
    out.push_str("var fpssOffsetChecks = []struct {\n");
    out.push_str("\tname string\n");
    out.push_str("\tgot  uintptr\n");
    out.push_str("\twant uintptr\n");
    out.push_str("}{\n");
    for (event_name, def) in sorted_data_events(schema) {
        for (field_name, offset) in c_struct_offsets(&def.columns) {
            writeln!(
                out,
                "\t{{\"C.TdxFpss{event_name}.{field_name}\", unsafe.Offsetof(C.TdxFpss{event_name}{{}}.{field_name}), {offset}}},"
            )
            .unwrap();
        }
    }
    for (field_name, offset) in [("kind", 0usize), ("id", 4usize), ("detail", 8usize)] {
        writeln!(
            out,
            "\t{{\"C.TdxFpssControl.{field_name}\", unsafe.Offsetof(C.TdxFpssControl{{}}.{field_name}), {offset}}},"
        )
        .unwrap();
    }
    for (field_name, offset) in [
        ("code", 0usize),
        ("payload", 8usize),
        ("payload_len", 16usize),
    ] {
        writeln!(
            out,
            "\t{{\"C.TdxFpssRawData.{field_name}\", unsafe.Offsetof(C.TdxFpssRawData{{}}.{field_name}), {offset}}},"
        )
        .unwrap();
    }
    for (field_name, offset) in fpss_event_offsets(schema) {
        writeln!(
            out,
            "\t{{\"C.TdxFpssEvent.{field_name}\", unsafe.Offsetof(C.TdxFpssEvent{{}}.{field_name}), {offset}}},"
        )
        .unwrap();
    }
    out.push_str("}\n");
    out
}

/// Go Contract struct — optional fields use `*int32` / `*bool` pointers
/// so nil represents `None` (Java-style). `Root` is always present as a
/// string (empty when not yet resolved).
fn render_contract_go() -> &'static str {
    "// Contract identifies a subscribed instrument. Root is always present;\n\
// option fields (ExpDate, IsCall, Strike) are non-nil only for options.\n\
// The same Contract value is attached to every FPSS data event the SDK\n\
// emits for the matching contract_id.\n\
type Contract struct {\n\
\tRoot    string\n\
\tSecType int32\n\
\tExpDate *int32\n\
\tIsCall  *bool\n\
\tStrike  *int32\n\
}\n\n"
}

/// Lives next to `fpss.go` in the `thetadatadx` package; replaces the
/// hand-written block that used to live there.
///
/// Breaking rename: `FpssOpenInterest` (not `FpssOpenInterestData`) and
/// `FpssControl` (not `FpssControlData`). The old suffix was a legacy of
/// the first Go prototype and has no active consumers per user direction.
pub(super) fn render_go_fpss_event_structs(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str("// Code generated by build.rs from fpss_event_schema.toml. DO NOT EDIT.\n");
    out.push_str("// Go-idiomatic mirror of the Rust FFI FPSS event types. Lives in\n");
    out.push_str("// the `thetadatadx` package alongside the hand-written client glue.\n\n");
    out.push_str("package thetadatadx\n\n");

    out.push_str(include_str!("templates/go_structs/kind_enum.go.tmpl"));
    out.push_str(include_str!(
        "templates/go_structs/control_kind_consts.go.tmpl"
    ));
    out.push_str(render_contract_go());

    for (event_name, def) in sorted_data_events(schema) {
        let doc_text = if def.doc.is_empty() {
            format!("Fpss{event_name} is a real-time {event_name} event from FPSS.")
        } else {
            def.doc.clone()
        };
        for line in doc_text.lines() {
            writeln!(out, "// {line}").unwrap();
        }
        writeln!(out, "type Fpss{event_name} struct {{").unwrap();
        // Pre-compute Go field names so we can align them in columns.
        let go_fields: Vec<(String, &'static str)> = def
            .columns
            .iter()
            .map(|c| {
                (
                    snake_to_go_pascal(&c.name),
                    go_scalar(&c.r#type, event_name, &c.name),
                )
            })
            .collect();
        let name_width = go_fields.iter().map(|(n, _)| n.len()).max().unwrap_or(0);
        for (name, ty) in &go_fields {
            writeln!(out, "\t{name:<name_width$} {ty}").unwrap();
        }
        out.push_str("}\n\n");
    }

    out.push_str(include_str!(
        "templates/go_structs/control_and_raw_data.go.tmpl"
    ));

    out.push_str("// FpssEvent is a tagged streaming event from FPSS.\n");
    out.push_str("// Check Kind to determine which field is non-nil.\n");
    out.push_str("type FpssEvent struct {\n");
    out.push_str("\tKind FpssEventKind\n");
    for (event_name, _) in sorted_data_events(schema) {
        // Field name matches the kind: `Quote`, `Trade`, `OpenInterest`,
        // `Ohlcvc`. Consumers do `event.Quote` / `event.Ohlcvc`.
        writeln!(out, "\t{event_name} *Fpss{event_name}").unwrap();
    }
    out.push_str("\tControl *FpssControl\n");
    out.push_str("\tRawData *FpssRawData\n");
    out.push_str("}\n");

    out
}
