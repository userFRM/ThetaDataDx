//! Emit `sdks/python/src/fpss_event_classes.rs` — typed `#[pyclass]`
//! structs per Data variant + `buffered_event_to_typed` dispatcher.

use std::fmt::Write as _;

use super::common::{is_contract, python_rust_field_type, snake_case};
use super::schema::{sorted_event_names, ColumnDef, EventDef, Schema};

/// Emit the `Contract` pyclass + helper constructor. Every data event
/// carries a `Py<Contract>` field so Python code can read
/// `event.contract.root`, `event.contract.strike`, etc. through the
/// normal pyo3 getter machinery.
fn render_contract_pyclass() -> &'static str {
    "/// FPSS contract identifier. Surfaced on every decoded FPSS data\n\
/// event as `event.contract`. Matches the shape of the Rust\n\
/// `thetadatadx::fpss::protocol::Contract` struct one-for-one.\n\
#[must_use]\n\
#[pyclass(module = \"thetadatadx\", frozen, skip_from_py_object)]\n\
#[derive(Clone)]\n\
pub(crate) struct Contract {\n\
    #[pyo3(get)] pub root: String,\n\
    #[pyo3(get)] pub sec_type: i32,\n\
    #[pyo3(get)] pub exp_date: Option<i32>,\n\
    #[pyo3(get)] pub is_call: Option<bool>,\n\
    #[pyo3(get)] pub strike: Option<i32>,\n\
}\n\
#[pymethods]\n\
impl Contract {\n\
    fn __repr__(&self) -> String {\n\
        format!(\n\
            \"Contract(root={:?}, sec_type={}, exp_date={:?}, is_call={:?}, strike={:?})\",\n\
            self.root, self.sec_type, self.exp_date, self.is_call, self.strike\n\
        )\n\
    }\n\
}\n\
impl Contract {\n\
    /// Build from the core `thetadatadx::fpss::protocol::Contract` value\n\
    /// carried by each `BufferedEvent::*` Data arm. The `sec_type` enum\n\
    /// is cast to its `#[repr(i32)]` discriminant; Python consumers get\n\
    /// a plain int and can compare against `tdbe.types.enums.SecType`\n\
    /// exports if they need a symbolic reading.\n\
    pub(crate) fn from_core(c: &fpss::protocol::Contract) -> Self {\n\
        Self {\n\
            root: c.root.clone(),\n\
            sec_type: c.sec_type as i32,\n\
            exp_date: c.exp_date,\n\
            is_call: c.is_call,\n\
            strike: c.strike,\n\
        }\n\
    }\n\
}\n\n"
}

pub(super) fn render_python_fpss_event_classes(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT — regenerated by build.rs from fpss_event_schema.toml\n",
    );
    out.push_str("// Typed #[pyclass] structs for every `FpssData` variant + the dispatch\n");
    out.push_str("// helper that converts a `BufferedEvent` into one of them. The single\n");
    out.push_str("// source of truth is `crates/thetadatadx/fpss_event_schema.toml`.\n\n");

    let names = sorted_event_names(schema);

    // Contract pyclass — every data variant's `contract` field returns
    // `Py<Contract>` on read.
    out.push_str(render_contract_pyclass());

    // Structs.
    for event_name in &names {
        let def = &schema.events[*event_name];
        out.push_str(&render_python_event_class_struct(event_name, def));
        out.push('\n');
    }

    // Dispatcher: BufferedEvent -> typed pyclass for EVERY variant.
    // No PyDict fallback — every `[events.*]` entry in the schema has a
    // typed `#[pyclass]` representation, so the match is exhaustive and
    // the dict path is obliterated from the public Python surface.
    out.push_str("pub(crate) fn buffered_event_to_typed(\n");
    out.push_str("    py: Python<'_>,\n");
    out.push_str("    event: &BufferedEvent,\n");
    out.push_str(") -> PyResult<Py<PyAny>> {\n");
    out.push_str("    match event {\n");
    for event_name in &names {
        let def = &schema.events[*event_name];
        out.push_str(&render_python_buffered_match_arm(event_name, def));
    }
    out.push_str("    }\n");
    out.push_str("}\n\n");

    // Module registration helper.
    out.push_str(
        "pub(crate) fn register_fpss_event_classes(m: &Bound<'_, PyModule>) -> PyResult<()> {\n",
    );
    out.push_str("    m.add_class::<Contract>()?;\n");
    for event_name in &names {
        writeln!(out, "    m.add_class::<{}>()?;", event_name).unwrap();
    }
    out.push_str("    Ok(())\n");
    out.push_str("}\n");

    out
}

fn render_python_event_class_struct(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    let doc_text = if def.doc.is_empty() {
        format!("FPSS {event_name} event.")
    } else {
        def.doc.clone()
    };
    for line in doc_text.lines() {
        writeln!(out, "/// {line}").unwrap();
    }
    // `#[must_use]` — events surfaced from the FPSS stream; silently dropping
    // decoded events is a bug. `skip_from_py_object` opts out of the
    // deprecated auto-derived `FromPyObject` impl for pyclasses
    // (pyo3 0.28+); these event structs flow FROM Rust to Python only.
    //
    // No `#[derive(Clone)]` on data-variant event classes: the `contract`
    // field is `Py<Contract>`, which does not implement `Clone` (cloning
    // a Python reference needs a GIL token via `clone_ref(py)`). These
    // events flow one-way from the Rust callback to the Python consumer
    // and are never cloned on the Rust side, so the derive would be dead
    // code even if it compiled.
    out.push_str("#[must_use]\n");
    out.push_str("#[pyclass(module = \"thetadatadx\", frozen, skip_from_py_object)]\n");
    writeln!(out, "pub(crate) struct {event_name} {{").unwrap();
    for column in &def.columns {
        let ty = python_rust_field_type(&column.r#type, event_name, &column.name);
        writeln!(out, "    #[pyo3(get)] pub {}: {},", column.name, ty).unwrap();
    }
    out.push_str("}\n");
    writeln!(out, "#[pymethods]").unwrap();
    writeln!(out, "impl {event_name} {{").unwrap();
    // `__repr__` surfaces live per-instance field values (mirroring the
    // tick-class renderer in `ticks/python_classes.rs::render_python_tick_class_struct`)
    // so pdb, Jupyter, pytest short-repr, and print()-based debugging
    // show actionable diagnostic data instead of an opaque
    // `"Ohlcvc(...)"` placeholder. Cap at six columns to stay one-line
    // readable; `Vec<u8>` payload columns (only on `RawData`) and
    // `received_at_ns` are skipped because the first is unbounded and
    // the second is a timestamp that dwarfs the other fields visually.
    let repr_fields = fpss_event_repr_fields(&def.columns, 6);
    if repr_fields.is_empty() {
        // Defensive fallback — every schema event has columns, but keep
        // the fallback so a future zero-column variant still compiles.
        writeln!(
            out,
            "    fn __repr__(&self) -> String {{ \"{event_name}()\".to_string() }}"
        )
        .unwrap();
    } else {
        let mut fmt_string = String::new();
        for (idx, field) in repr_fields.iter().enumerate() {
            if idx > 0 {
                fmt_string.push_str(", ");
            }
            fmt_string.push_str(field.name);
            fmt_string.push('=');
            // `{:?}` quotes strings and surfaces `None` / `Some(..)` on
            // Option fields (`Simple.detail`, `Simple.id`); `{}` for
            // numerics keeps the output tight.
            fmt_string.push_str(if field.use_debug { "{:?}" } else { "{}" });
        }
        let args = repr_fields
            .iter()
            .map(|f| format!("self.{}", f.name))
            .collect::<Vec<_>>()
            .join(", ");
        writeln!(out, "    fn __repr__(&self) -> String {{").unwrap();
        writeln!(
            out,
            "        format!(\"{event_name}({fmt_string})\", {args})"
        )
        .unwrap();
        out.push_str("    }\n");
    }
    out.push('\n');
    // Use the same lowercase/snake_case wire tag the existing dict-based
    // `next_event` emits ("quote", "trade", "open_interest", "ohlcvc") so
    // `next_event_typed` is a true drop-in — consumer `event.kind` checks
    // don't need to branch on which API they called.
    let kind_tag = snake_case(event_name);
    writeln!(out, "    #[getter]").unwrap();
    writeln!(
        out,
        "    fn kind(&self) -> &'static str {{ \"{kind_tag}\" }}"
    )
    .unwrap();
    out.push_str("}\n");
    out
}

struct FpssReprField<'a> {
    name: &'a str,
    use_debug: bool,
}

/// Pick the first handful of FPSS event columns for `__repr__`
/// rendering. Mirrors the tick-class heuristic
/// (`ticks/python_classes.rs::repr_fields_for_tick`) but skips diagnostic noise:
///
/// * `Vec<u8>` payload columns (unbounded byte blobs — would balloon
///   the repr past a single screen and tank any REPL that auto-repr's
///   incoming events).
/// * `received_at_ns` — a nanosecond epoch timestamp that dwarfs every
///   other field visually and carries no per-event routing signal for
///   debugger consumption. Consumers that need it still read the
///   attribute directly.
fn fpss_event_repr_fields<'a>(
    columns: &'a [ColumnDef],
    max_fields: usize,
) -> Vec<FpssReprField<'a>> {
    let mut fields: Vec<FpssReprField<'a>> = Vec::new();
    for column in columns {
        if fields.len() >= max_fields {
            break;
        }
        // Skip noise fields (see doc comment above). `Vec<u8>` and
        // `received_at_ns` are skipped per module docs; `Contract`
        // because its own `__repr__` is nested and printing the full
        // struct inside the event's one-line repr quickly exceeds the
        // 6-field width budget.
        if column.r#type == "Vec<u8>"
            || column.name == "received_at_ns"
            || column.r#type == "Contract"
        {
            continue;
        }
        // `Option<..>` and `String` types render better with `{:?}`
        // (quoted strings, explicit `None` / `Some(..)`).
        let use_debug = matches!(
            column.r#type.as_str(),
            "String" | "Option<String>" | "Option<i32>"
        );
        fields.push(FpssReprField {
            name: &column.name,
            use_debug,
        });
    }
    fields
}

fn render_python_buffered_match_arm(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    // Pattern binds each schema field to a same-named local.
    writeln!(out, "        BufferedEvent::{event_name} {{").unwrap();
    for column in &def.columns {
        writeln!(out, "            {},", column.name).unwrap();
    }
    // No `..` rest pattern: `BufferedEvent` itself is generator-emitted
    // from this same schema, so the variant arms are exhaustive by
    // construction. Any drift is a compile error, not silent data loss.
    //
    // If the variant carries a Contract field, stage it first so we can
    // hand the `Py<Contract>` handle into the constructed pyclass.
    let has_contract = def.columns.iter().any(|c| is_contract(&c.r#type));
    if has_contract {
        out.push_str(
            "        } => {\n            let contract_py = Py::new(py, Contract::from_core(contract))?;\n            Py::new(\n                py,\n",
        );
    } else {
        out.push_str("        } => Py::new(\n            py,\n");
    }
    let indent = if has_contract {
        "                "
    } else {
        "            "
    };
    writeln!(out, "{indent}{event_name} {{").unwrap();
    for column in &def.columns {
        let rhs = match column.r#type.as_str() {
            // Owned non-`Copy` types + `Option<String>` clone through
            // the deref of the pattern binding. `Option<i32>` implements
            // `Copy`, so explicit `.clone()` is flagged by clippy —
            // deref like a plain primitive instead.
            "String" | "Vec<u8>" | "Option<String>" => {
                format!("{field}.clone()", field = column.name)
            }
            // Contract was staged above into `contract_py`; hand over the
            // handle. `clone_ref(py)` because pyo3's `Py<T>` doesn't
            // auto-clone without a Python token.
            "Contract" => "contract_py.clone_ref(py)".to_string(),
            // `Copy` primitives (including `Option<i32>`) — explicit
            // deref of the reference binding.
            _ => format!("*{field}", field = column.name),
        };
        writeln!(out, "{indent}    {field}: {rhs},", field = column.name).unwrap();
    }
    if has_contract {
        out.push_str(
            "                },\n            )\n            .map(|p| p.into_any())\n        }\n",
        );
    } else {
        out.push_str("            },\n        )\n        .map(|p| p.into_any()),\n");
    }
    out
}
