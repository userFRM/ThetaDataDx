fn render_ts_tick_types(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str("// @generated DO NOT EDIT — regenerated by build.rs from tick_schema.toml\n");
    out.push_str("// TypeScript interfaces for columnar tick data returned by the native addon.\n");
    out.push_str("// Each property is an array — one entry per tick row.\n\n");
    for type_name in sorted_type_names(schema) {
        let def = &schema.types[type_name];
        out.push_str(&render_ts_tick_type_interface(type_name, def));
        out.push('\n');
    }
    out
}

fn render_ts_tick_type_interface(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let is_quote_tick = type_name == "QuoteTick";

    // Strip " -- N fields[...]." from the doc — the TS interface includes
    // contract-id columns not counted in the TOML doc string.
    let first_line = def.doc.lines().next().unwrap_or("");
    let doc = if let Some(pos) = first_line.find(" -- ") {
        let after_dash = &first_line[pos + 4..];
        if after_dash.starts_with(|c: char| c.is_ascii_digit()) {
            let rest = after_dash
                .find(". ")
                .map(|i| &after_dash[i + 2..])
                .unwrap_or("");
            if rest.is_empty() {
                format!("{}.", &first_line[..pos])
            } else {
                format!("{}. {rest}", &first_line[..pos])
            }
        } else {
            first_line.to_string()
        }
    } else {
        first_line.to_string()
    };
    writeln!(out, "/** {doc} */").unwrap();
    writeln!(out, "export interface {type_name}Columnar {{").unwrap();
    for column in &def.columns {
        let ts_type = match column.r#type.as_str() {
            "i32" | "i64" | "eod_num" | "eod_date" => "number[]",
            "f64" | "price" | "eod_price" => "number[]",
            "String" => "string[]",
            other => panic!("unsupported column type '{other}' in TS interface for {type_name}"),
        };
        writeln!(out, "  {}: {};", column.name, ts_type).unwrap();
    }
    if is_quote_tick {
        out.push_str("  midpoint: number[];\n");
    }
    if def.contract_id {
        out.push_str("  expiration: number[];\n");
        out.push_str("  strike: number[];\n");
        out.push_str("  right: string[];\n");
    }
    out.push_str("}\n");
    out
}

fn render_go_tick_converters(schema: &Schema) -> String {
    let mut out = String::new();
    // Go spec header: `^// Code generated .* DO NOT EDIT\.$` — see
    // https://golang.org/s/generatedcode. Required for go vet / gofmt /
    // staticcheck to recognize this as machine-generated. Non-Go
    // emitters keep the uniform `@generated` form.
    out.push_str("// Code generated by build.rs from tick_schema.toml; DO NOT EDIT.\n\n");
    out.push_str("package thetadatadx\n\n");
    out.push_str("/*\n#include \"ffi_bridge.h\"\n*/\nimport \"C\"\n\n");
    out.push_str("import \"unsafe\"\n\n");
    for type_name in sorted_type_names(schema) {
        let def = &schema.types[type_name];
        out.push_str(&render_go_tick_converter(type_name, def));
        out.push('\n');
    }
    out
}

