/// Map a tick_schema.toml column type to the Go public struct field type.
fn go_public_field_type(type_name: &str, field: &str, kind: &str) -> &'static str {
    // CalendarDay.is_open is a bool in the public API
    if type_name == "CalendarDay" && field == "is_open" {
        return "bool";
    }
    // OptionContract.right is i32 in the schema but exposed as string via RightStr()
    if type_name == "OptionContract" && field == "right" {
        return "string";
    }
    match kind {
        "i32" | "eod_num" | "eod_date" => "int",
        "i64" | "eod_num64" => "int64",
        "f64" | "price" | "eod_price" => "float64",
        "String" => "string",
        _ => panic!("unsupported Go public type for {type_name}.{field}:{kind}"),
    }
}

/// Map a tick_schema.toml column to the JSON tag for the Go public struct.
fn go_json_tag(field: &str) -> String {
    field.to_string()
}

fn render_go_tick_structs(schema: &Schema) -> String {
    let mut out = String::new();
    // Go spec header (see render_go_tick_converters for the rationale).
    out.push_str("// Code generated by build.rs from tick_schema.toml; DO NOT EDIT.\n");
    out.push_str("//\n");
    out.push_str("// Public Go tick types, RightStr helper, and Greeks struct.\n");
    out.push_str("// C-mirror (FFI) structs live in tick_ffi_mirrors.go (hand-written).\n\n");
    out.push_str("package thetadatadx\n\n");

    // Emit public Go structs for each tick type
    for type_name in sorted_type_names(schema) {
        let def = &schema.types[type_name];
        out.push_str(&render_go_public_struct(type_name, def));
        out.push('\n');
    }

    // RightStr helper
    out.push_str("// RightStr converts the raw right code to a string.\n");
    out.push_str("// 67='C' (Call), 80='P' (Put), 0=\"\" (not set).\n");
    out.push_str("func RightStr(code int32) string {\n");
    out.push_str("\tswitch code {\n");
    out.push_str("\tcase 67:\n");
    out.push_str("\t\treturn \"C\"\n");
    out.push_str("\tcase 80:\n");
    out.push_str("\t\treturn \"P\"\n");
    out.push_str("\tdefault:\n");
    out.push_str("\t\treturn \"\"\n");
    out.push_str("\t}\n");
    out.push_str("}\n\n");

    // Greeks struct (offline utility, not a tick type — matches tdbe::Greeks)
    out.push_str("// Greeks holds the 22 Black-Scholes greeks + IV from all_greeks().\n");
    out.push_str("type Greeks struct {\n");
    for field in &[
        ("Value", "value"),
        ("Delta", "delta"),
        ("Gamma", "gamma"),
        ("Theta", "theta"),
        ("Vega", "vega"),
        ("Rho", "rho"),
        ("IV", "iv"),
        ("IVError", "iv_error"),
        ("Vanna", "vanna"),
        ("Charm", "charm"),
        ("Vomma", "vomma"),
        ("Veta", "veta"),
        ("Speed", "speed"),
        ("Zomma", "zomma"),
        ("Color", "color"),
        ("Ultima", "ultima"),
        ("D1", "d1"),
        ("D2", "d2"),
        ("DualDelta", "dual_delta"),
        ("DualGamma", "dual_gamma"),
        ("Epsilon", "epsilon"),
        ("Lambda", "lambda"),
    ] {
        writeln!(out, "\t{:9} float64 `json:\"{}\"`", field.0, field.1).unwrap();
    }
    out.push_str("}\n");

    out
}

fn render_go_public_struct(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let go_type = match type_name {
        "IvTick" => "IVTick",
        other => other,
    };
    writeln!(
        out,
        "// {} — {}",
        go_type,
        def.doc.lines().next().unwrap_or("")
    )
    .unwrap();
    writeln!(out, "type {go_type} struct {{").unwrap();

    for column in &def.columns {
        let public_field = go_public_field_name(type_name, &column.field);
        let go_type_str = go_public_field_type(type_name, &column.field, &column.r#type);
        let json_tag = go_json_tag(&column.name);
        writeln!(
            out,
            "\t{:14} {:<8} `json:\"{json_tag}\"`",
            public_field, go_type_str
        )
        .unwrap();
    }
    // QuoteTick gets a Midpoint field
    if type_name == "QuoteTick" {
        out.push_str("\tMidpoint       float64  `json:\"midpoint\"`\n");
    }
    // contract_id fields
    if def.contract_id {
        out.push_str("\tExpiration     int32    `json:\"expiration,omitempty\"`\n");
        out.push_str("\tStrike         float64  `json:\"strike,omitempty\"`\n");
        out.push_str("\tRight          string   `json:\"right,omitempty\"`\n");
    }
    out.push_str("}\n");
    out
}
