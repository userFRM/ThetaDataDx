    out.push_str("// Typed pyclass structs per tick type + list converters.\n");
    out.push_str("// Matches the typed-struct approach used by Rust core, TypeScript, Go,\n");
    out.push_str("// and C++ FFI. Primary return shape for historical endpoint methods.\n\n");

