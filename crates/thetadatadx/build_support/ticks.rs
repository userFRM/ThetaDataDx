fn render_ts_tick_class_struct(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let is_quote_tick = type_name == "QuoteTick";
    // Strip " -- N fields[...]." from doc so the TS class reflects true field
    // count once contract-id columns + midpoint are appended.
    let first_line = def.doc.lines().next().unwrap_or("");
    let doc = if let Some(pos) = first_line.find(" -- ") {
        let after_dash = &first_line[pos + 4..];
        if after_dash.starts_with(|c: char| c.is_ascii_digit()) {
            let rest = after_dash
                .find(". ")
                .map(|i| &after_dash[i + 2..])
                .unwrap_or("");
            if rest.is_empty() {
                format!("{}.", &first_line[..pos])
            } else {
                format!("{}. {rest}", &first_line[..pos])
            }
        } else {
            first_line.to_string()
        }
    } else {
        first_line.to_string()
    };
    writeln!(out, "/// {doc}").unwrap();
    out.push_str("#[napi(object)]\n");
    out.push_str("#[derive(Clone)]\n");
    writeln!(out, "pub struct {type_name} {{").unwrap();
    for column in &def.columns {
        let rust_type = ts_class_rust_type(&column.r#type, type_name, &column.field);
        writeln!(out, "    pub {}: {},", column.field, rust_type).unwrap();
    }
    if is_quote_tick {
        out.push_str("    pub midpoint: f64,\n");
    }
    if def.contract_id {
        out.push_str("    pub expiration: i32,\n");
        out.push_str("    pub strike: f64,\n");
        out.push_str("    pub right: String,\n");
    }
    out.push_str("}\n");
    out
}

fn render_ts_tick_class_factory(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let fn_name = ts_tick_class_factory_name(type_name);
    writeln!(
        out,
        "fn {fn_name}(ticks: &[tick::{type_name}]) -> Vec<{type_name}> {{"
    )
    .unwrap();
    out.push_str("    ticks\n");
    out.push_str("        .iter()\n");
    out.push_str("        .map(|t| {\n");
    writeln!(out, "            {type_name} {{").unwrap();
    for column in &def.columns {
        // String fields must be cloned; primitives are Copy.
        let expr = match column.r#type.as_str() {
            "String" => format!("t.{field}.clone()", field = column.field),
            _ => format!("t.{field}", field = column.field),
        };
        writeln!(out, "                {}: {expr},", column.field).unwrap();
    }
    if type_name == "QuoteTick" {
        out.push_str("                midpoint: t.midpoint,\n");
    }
    if def.contract_id {
        out.push_str("                expiration: t.expiration,\n");
        out.push_str("                strike: t.strike,\n");
        // right is stored as the raw ASCII code (67='C', 80='P') on the
        // Rust tick; project it to a JS-friendly string using the same
        // is_call/is_put helpers the columnar emitter already relies on.
        out.push_str(
            "                right: if t.is_call() { \"C\".to_string() } else if t.is_put() { \"P\".to_string() } else { String::new() },\n",
        );
    }
    out.push_str("            }\n");
    out.push_str("        })\n");
    out.push_str("        .collect()\n");
    out.push_str("}\n");
    out
}

