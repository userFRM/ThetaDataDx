fn render_python_tick_class_struct(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let class = pyclass_name(type_name);
    let is_quote_tick = type_name == "QuoteTick";
    let doc_text = if def.doc.is_empty() {
        format!("Typed {type_name} record.")
    } else {
        def.doc.clone()
    };
    for line in doc_text.lines() {
        writeln!(out, "/// {line}").unwrap();
    }
    out.push_str("#[pyclass(module = \"thetadatadx\", frozen)]\n");
    out.push_str("#[derive(Clone)]\n");
    writeln!(out, "pub(crate) struct {class} {{").unwrap();
    for column in &def.columns {
        // Special case: `OptionContract.right` is stored on the wire as
        // `i32` (ASCII 67/80), but every other tick surfaces `right` as
        // a Python `"C" | "P" | ""` string (via the contract_id injection
        // below). Emit it as String here too so the two paths match and
        // consumer code doesn't get an integer on one endpoint and a
        // string on the others.
        let rust_type = if type_name == "OptionContract" && column.field == "right" {
            "String"
        } else {
            pyclass_field_type(column.r#type.as_str(), type_name)
        };
        writeln!(out, "    #[pyo3(get)] pub {}: {},", column.field, rust_type).unwrap();
    }
    if is_quote_tick {
        out.push_str("    #[pyo3(get)] pub midpoint: f64,\n");
    }
    if def.contract_id {
        out.push_str("    #[pyo3(get)] pub expiration: i32,\n");
        out.push_str("    #[pyo3(get)] pub strike: f64,\n");
        out.push_str("    #[pyo3(get)] pub right: String,\n");
    }
    out.push_str("}\n");

    // __repr__ for discoverability.
    writeln!(out, "#[pymethods]").unwrap();
    writeln!(out, "impl {class} {{").unwrap();
    writeln!(
        out,
        "    fn __repr__(&self) -> String {{ format!(\"{class}(...)\") }}"
    )
    .unwrap();
    out.push_str("}\n");
    out
}

fn pyclass_field_type(column_type: &str, type_name: &str) -> &'static str {
    match column_type {
        "i32" | "eod_num" | "eod_date" => "i32",
        "i64" => "i64",
        "f64" | "price" | "eod_price" => "f64",
        "String" => "String",
        other => panic!("unsupported column type '{other}' in pyclass for {type_name}"),
    }
}

fn render_python_tick_class_list_fn(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let class = pyclass_name(type_name);
    let fn_name = python_list_fn_name(type_name);
    let is_quote_tick = type_name == "QuoteTick";

    writeln!(
        out,
        "pub(crate) fn {fn_name}(py: Python<'_>, ticks: &[tick::{type_name}]) -> PyResult<Py<PyAny>> {{"
    )
    .unwrap();
    out.push_str("    let list = pyo3::types::PyList::empty(py);\n");
    out.push_str("    for t in ticks {\n");
    writeln!(out, "        let obj = {class} {{").unwrap();
    for column in &def.columns {
        // Special case: `OptionContract.right` emits as a
        // `"C" | "P" | ""` string (see struct emitter above). Other
        // ticks get the String `right` from the contract_id injection
        // below; this keeps the standalone OptionContract endpoint in
        // sync so consumers never see a raw `67` / `80` / `0` on one
        // endpoint and a character on every other.
        if type_name == "OptionContract" && column.field == "right" {
            out.push_str(
                "            right: (if t.is_call() { \"C\" } else if t.is_put() { \"P\" } else { \"\" }).to_string(),\n",
            );
            continue;
        }
        let rust_type = pyclass_field_type(column.r#type.as_str(), type_name);
        if rust_type == "String" {
            writeln!(
                out,
                "            {}: t.{}.clone(),",
                column.field, column.field
            )
            .unwrap();
        } else {
            writeln!(out, "            {}: t.{},", column.field, column.field).unwrap();
        }
    }
    if is_quote_tick {
        out.push_str("            midpoint: t.midpoint,\n");
    }
    if def.contract_id {
        out.push_str("            expiration: t.expiration,\n");
        out.push_str("            strike: t.strike,\n");
        out.push_str(
            "            right: (if t.is_call() { \"C\" } else if t.is_put() { \"P\" } else { \"\" }).to_string(),\n",
        );
    }
    out.push_str("        };\n");
    out.push_str("        list.append(Py::new(py, obj)?)?;\n");
    out.push_str("    }\n");
    out.push_str("    Ok(list.into_any().unbind())\n");
    out.push_str("}\n");
    out
}

