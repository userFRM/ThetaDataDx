//! Emit `sdks/python/src/tick_classes.rs` — typed `#[pyclass]` structs,
//! `<Tick>List` wrapper pyclasses, and list converters.
//!
//! Matches the typed-struct approach used by Rust core, TypeScript, Go,
//! and C++ FFI. Primary return shape for all historical endpoint methods:
//! every generated endpoint returns `Py<<TickName>List>`, a typed list
//! wrapper whose terminal methods chain straight into Arrow / pandas /
//! polars / plain `list` without any free-function round-trip.

use std::fmt::Write as _;

use super::schema::{Schema, TickTypeDef};
use super::{pyclass_name, sorted_type_names};

pub(super) fn render_python_tick_classes(
    schema: &Schema,
    snapshot_return_types: &std::collections::HashSet<String>,
) -> String {
    let mut out = String::new();
    out.push_str("// @generated DO NOT EDIT — regenerated by build.rs from tick_schema.toml\n");
    out.push_str(
        "// Typed pyclass structs per tick type + `<Tick>List` wrappers + list converters.\n",
    );
    out.push_str("// Matches the typed-struct approach used by Rust core, TypeScript, Go,\n");
    out.push_str("// and C++ FFI. Primary return shape for historical endpoint methods.\n");
    out.push_str("//\n");
    out.push_str("// Every historical endpoint returns `Py<<TickName>List>`; terminal\n");
    out.push_str("// methods (`to_list`, `to_arrow`, `to_pandas`, `to_polars`) live on\n");
    out.push_str("// the wrapper so chained calls like\n");
    out.push_str("//     tdx.stock_history_eod(...).to_polars()\n");
    out.push_str("// avoid a free-function round-trip.\n\n");

    // Imports kept here because `tick_classes.rs` is `include!`-ed into
    // `lib.rs` BEFORE `tick_arrow.rs`; the `use` statements in the
    // sibling file don't yet apply at this splice point. Duplicates are
    // a no-op in Rust at module scope.
    out.push_str("use arrow::array::{ArrayRef, StringArray};\n");
    out.push_str("use arrow::datatypes::{DataType, Field, Schema};\n");
    out.push_str("use arrow::record_batch::RecordBatch;\n\n");

    let type_names = sorted_type_names(schema);

    // Structs + pymethods for the leaf tick pyclasses.
    for type_name in &type_names {
        let def = &schema.types[*type_name];
        out.push_str(&render_python_tick_class_struct(type_name, def));
        out.push('\n');
    }

    // `<Tick>List` + `<Tick>ListIter` pyclasses. Emitted per tick type so
    // every historical endpoint that returns `EodTicks` (etc.) lands on a
    // concrete typed wrapper.
    for type_name in &type_names {
        let def = &schema.types[*type_name];
        out.push_str(&render_python_tick_list_struct(type_name, def));
        out.push('\n');
    }

    // Converter helpers: take an owned `Vec<tick::T>` and produce
    // `Py<<TickName>List>`. The decoder-owned Vec is moved straight into
    // the wrapper, so no extra allocation / no double-buffering RSS spike.
    for type_name in &type_names {
        let def = &schema.types[*type_name];
        out.push_str(&render_python_tick_class_list_fn(type_name, def));
        out.push('\n');
    }

    // Snapshot-endpoint fast-path converter: take an owned `Vec<tick::T>`
    // and produce a plain `Py<PyList>` of typed pyclass instances. Skips
    // the `<TickName>List` wrapper allocation — snapshots return <=10
    // small rows and callers never chain `.to_polars()` on a 1-row
    // calendar-day result, so the wrapper cost is pure overhead on the
    // latency-sensitive path. Only the snapshot / calendar generator
    // branch in `endpoints/render/python.rs` emits calls to these.
    //
    // Emission is gated on `snapshot_return_types` (derived from
    // `endpoint_surface.toml` by `endpoints::snapshot_return_types`) so
    // tick types that are not returned by any snapshot endpoint — e.g.
    // `EodTick`, `TradeQuoteTick`, `InterestRateTick`, `OptionContract`
    // at the time of writing — do not land as dead-code functions in the
    // generated file. Adding a snapshot endpoint of a new tick type to
    // the TOML automatically opts its converter into emission on the
    // next generator run.
    for type_name in &type_names {
        // `python_list_fn_name` yields the `*_to_pyclass_list` name;
        // the matching collection return-type is the prefix (e.g.
        // `OhlcTick` -> `OhlcTicks`). Map using `tick_collection_name`
        // below so the filter key matches `endpoint_surface.toml::returns`
        // verbatim.
        let collection_name = tick_collection_name(type_name);
        if !snapshot_return_types.contains(collection_name) {
            continue;
        }
        let def = &schema.types[*type_name];
        out.push_str(&render_python_tick_class_vec_to_pylist_fn(type_name, def));
        out.push('\n');
    }

    // `StringList` wrapper + converter for every endpoint whose public
    // return shape is `Vec<String>` (symbols / dates / expirations /
    // strikes). Emitted unconditionally — not schema-driven, but kept
    // in the same generator output so one `include!("tick_classes.rs")`
    // seam covers every list-returning endpoint.
    out.push_str(&render_python_string_list());

    // Registration helper.
    out.push_str(
        "pub(crate) fn register_tick_classes(m: &Bound<'_, PyModule>) -> PyResult<()> {\n",
    );
    for type_name in &type_names {
        writeln!(out, "    m.add_class::<{}>()?;", pyclass_name(type_name)).unwrap();
        writeln!(
            out,
            "    m.add_class::<{}List>()?;",
            pyclass_name(type_name)
        )
        .unwrap();
        writeln!(
            out,
            "    m.add_class::<{}ListIter>()?;",
            pyclass_name(type_name)
        )
        .unwrap();
    }
    out.push_str("    m.add_class::<StringList>()?;\n");
    out.push_str("    m.add_class::<StringListIter>()?;\n");
    out.push_str("    Ok(())\n}\n\n");

    // Note: the `columns_for_qualname` dispatcher that used to live
    // here has been subsumed by `arrow_schema_for_qualname` in
    // `tick_arrow.rs` -- the Arrow schema carries the column names
    // AND types in one typed surface, so the bare name list became
    // redundant.
    out
}

/// Render the `StringList` + `StringListIter` pyclasses. Wraps
/// `Vec<String>` returned by list endpoints (`stock_list_symbols`,
/// `option_list_expirations`, `stock_list_dates`, ...) with the same
/// Python list protocol + chainable terminals as the tick list wrappers.
/// The `column_name` captured at construction drives the DataFrame
/// column name when `.to_arrow()` / `.to_pandas()` / `.to_polars()` is
/// called, so `stock_list_symbols().to_polars()` lands with a column
/// called `symbol` (not `_0` or similar).
fn render_python_string_list() -> String {
    let mut out = String::new();
    out.push_str("// ─────────────── String list wrapper ───────────────\n");
    out.push_str("//\n");
    out.push_str("// Emitted unconditionally (not schema-driven). Shared by every\n");
    out.push_str("// endpoint whose public return is `Vec<String>`.\n\n");

    // StringList pyclass.
    out.push_str("/// Typed list of strings returned by list endpoints\n");
    out.push_str("/// (`stock_list_symbols`, `option_list_expirations`, ...).\n");
    out.push_str("/// Wraps `Vec<String>` and exposes the Python list protocol plus\n");
    out.push_str("/// the chainable terminals (`to_list`, `to_arrow`, `to_pandas`,\n");
    out.push_str("/// `to_polars`). The `column_name` captured at construction drives\n");
    out.push_str("/// the DataFrame column name produced by the Arrow terminals, so\n");
    out.push_str("/// `stock_list_symbols().to_polars()` lands with a typed `symbol`\n");
    out.push_str("/// column rather than a numeric positional index.\n");
    out.push_str("#[must_use]\n");
    out.push_str("#[pyclass(module = \"thetadatadx\", frozen, skip_from_py_object)]\n");
    out.push_str("pub(crate) struct StringList {\n");
    out.push_str("    inner: Vec<String>,\n");
    out.push_str("    column_name: String,\n");
    out.push_str("}\n\n");

    out.push_str("impl StringList {\n");
    out.push_str("    pub(crate) fn new(inner: Vec<String>, column_name: &str) -> Self {\n");
    out.push_str("        Self {\n");
    out.push_str("            inner,\n");
    out.push_str("            column_name: column_name.to_string(),\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str("#[pymethods]\n");
    out.push_str("impl StringList {\n");
    out.push_str("    fn __len__(&self) -> usize {\n");
    out.push_str("        self.inner.len()\n");
    out.push_str("    }\n\n");
    out.push_str("    fn __bool__(&self) -> bool {\n");
    out.push_str("        !self.inner.is_empty()\n");
    out.push_str("    }\n\n");
    out.push_str("    fn __repr__(&self) -> String {\n");
    out.push_str(
        "        format!(\"StringList({} rows, column={:?})\", self.inner.len(), self.column_name)\n",
    );
    out.push_str("    }\n\n");
    out.push_str("    fn __getitem__(&self, idx: isize) -> PyResult<String> {\n");
    out.push_str("        let len = self.inner.len() as isize;\n");
    out.push_str("        let resolved = if idx < 0 { idx + len } else { idx };\n");
    out.push_str("        if resolved < 0 || resolved >= len {\n");
    out.push_str("            return Err(pyo3::exceptions::PyIndexError::new_err(format!(\n");
    out.push_str(
        "                \"StringList index {} out of range (len={})\", idx, self.inner.len()\n",
    );
    out.push_str("            )));\n");
    out.push_str("        }\n");
    out.push_str("        Ok(self.inner[resolved as usize].clone())\n");
    out.push_str("    }\n\n");
    out.push_str(
        "    fn __iter__(slf: PyRef<'_, Self>, py: Python<'_>) -> PyResult<Py<StringListIter>> {\n",
    );
    out.push_str("        Py::new(py, StringListIter { inner: slf.inner.clone(), cursor: 0 })\n");
    out.push_str("    }\n\n");
    out.push_str("    /// Return the wrapped values as a plain Python `list[str]`.\n");
    out.push_str("    fn to_list(&self, py: Python<'_>) -> PyResult<Py<PyAny>> {\n");
    out.push_str("        let list = pyo3::types::PyList::empty(py);\n");
    out.push_str("        for s in &self.inner {\n");
    out.push_str("            list.append(s.as_str())?;\n");
    out.push_str("        }\n");
    out.push_str("        Ok(list.into_any().unbind())\n");
    out.push_str("    }\n\n");
    out.push_str(
        "    /// Return a `pyarrow.Table` with a single `column_name`-named string column.\n",
    );
    out.push_str("    fn to_arrow(&self, py: Python<'_>) -> PyResult<Py<PyAny>> {\n");
    out.push_str("        string_list_slice_to_arrow_table(py, &self.inner, &self.column_name)\n");
    out.push_str("    }\n\n");
    out.push_str("    /// Return a `pandas.DataFrame` with a single `column_name` column.\n");
    out.push_str("    fn to_pandas(&self, py: Python<'_>) -> PyResult<Py<PyAny>> {\n");
    out.push_str(
        "        let table = string_list_slice_to_arrow_table(py, &self.inner, &self.column_name)?;\n",
    );
    out.push_str("        pyarrow_table_to_pandas(py, table)\n");
    out.push_str("    }\n\n");
    out.push_str("    /// Return a `polars.DataFrame` with a single `column_name` column.\n");
    out.push_str("    fn to_polars(&self, py: Python<'_>) -> PyResult<Py<PyAny>> {\n");
    out.push_str(
        "        let table = string_list_slice_to_arrow_table(py, &self.inner, &self.column_name)?;\n",
    );
    out.push_str("        pyarrow_table_to_polars(py, table)\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    // StringListIter pyclass.
    out.push_str("/// Companion iterator for [`StringList`]. Yields owned `String`s\n");
    out.push_str("/// until the wrapped `Vec` is exhausted. One allocation per call to\n");
    out.push_str("/// `__next__` (clone of the inner entry); the underlying Vec is\n");
    out.push_str("/// cloned once at construction so mutating it mid-iteration is\n");
    out.push_str("/// a non-issue.\n");
    out.push_str("#[pyclass(module = \"thetadatadx\", skip_from_py_object)]\n");
    out.push_str("pub(crate) struct StringListIter {\n");
    out.push_str("    inner: Vec<String>,\n");
    out.push_str("    cursor: usize,\n");
    out.push_str("}\n\n");

    out.push_str("#[pymethods]\n");
    out.push_str("impl StringListIter {\n");
    out.push_str("    fn __iter__(slf: PyRef<'_, Self>) -> PyRef<'_, Self> {\n");
    out.push_str("        slf\n");
    out.push_str("    }\n\n");
    out.push_str("    fn __next__(&mut self) -> Option<String> {\n");
    out.push_str("        if self.cursor >= self.inner.len() {\n");
    out.push_str("            return None;\n");
    out.push_str("        }\n");
    out.push_str("        let s = self.inner[self.cursor].clone();\n");
    out.push_str("        self.cursor += 1;\n");
    out.push_str("        Some(s)\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    // Constructor helper for endpoint generators.
    out.push_str("pub(crate) fn strings_to_string_list(\n");
    out.push_str("    py: Python<'_>,\n");
    out.push_str("    values: Vec<String>,\n");
    out.push_str("    column_name: &str,\n");
    out.push_str(") -> PyResult<Py<StringList>> {\n");
    out.push_str("    Py::new(py, StringList::new(values, column_name))\n");
    out.push_str("}\n\n");

    // Slice -> Arrow table helper. Lives here so `StringList.to_arrow`
    // stays zero-copy at the pyarrow boundary (the single column is
    // materialized as an Arrow `StringArray`, then the RecordBatch goes
    // through the same `record_batch_to_pyarrow_table` the tick types use).
    out.push_str("fn string_list_slice_to_arrow_table(\n");
    out.push_str("    py: Python<'_>,\n");
    out.push_str("    values: &[String],\n");
    out.push_str("    column_name: &str,\n");
    out.push_str(") -> PyResult<Py<PyAny>> {\n");
    out.push_str("    let schema = Arc::new(Schema::new(vec![Field::new(\n");
    out.push_str("        column_name,\n");
    out.push_str("        DataType::Utf8,\n");
    out.push_str("        false,\n");
    out.push_str("    )]));\n");
    out.push_str(
        "    let column: ArrayRef = Arc::new(StringArray::from_iter_values(values.iter().map(String::as_str)));\n",
    );
    out.push_str("    let batch = RecordBatch::try_new(schema, vec![column])\n");
    out.push_str(
        "        .map_err(|e| pyo3::exceptions::PyRuntimeError::new_err(e.to_string()))?;\n",
    );
    out.push_str("    record_batch_to_pyarrow_table(py, batch)\n");
    out.push_str("}\n\n");

    out
}

fn python_list_fn_name(type_name: &str) -> &'static str {
    match type_name {
        "EodTick" => "eod_ticks_to_pyclass_list",
        "OhlcTick" => "ohlc_ticks_to_pyclass_list",
        "TradeTick" => "trade_ticks_to_pyclass_list",
        "QuoteTick" => "quote_ticks_to_pyclass_list",
        "TradeQuoteTick" => "trade_quote_ticks_to_pyclass_list",
        "OpenInterestTick" => "open_interest_ticks_to_pyclass_list",
        "MarketValueTick" => "market_value_ticks_to_pyclass_list",
        "GreeksTick" => "greeks_ticks_to_pyclass_list",
        "IvTick" => "iv_ticks_to_pyclass_list",
        "PriceTick" => "price_ticks_to_pyclass_list",
        "CalendarDay" => "calendar_days_to_pyclass_list",
        "InterestRateTick" => "interest_rate_ticks_to_pyclass_list",
        "OptionContract" => "option_contracts_to_pyclass_list",
        other => panic!("unsupported Python list fn: {other}"),
    }
}

