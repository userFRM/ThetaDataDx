//! Emit `sdks/python/src/tick_classes.rs` — typed `#[pyclass]` structs
//! and list converters.
//!
//! Matches the typed-struct approach used by Rust core, TypeScript, Go,
//! and C++ FFI. Primary return shape for all historical endpoint methods.

use std::fmt::Write as _;

use super::schema::{Schema, TickTypeDef};
use super::{pyclass_name, sorted_type_names};

pub(super) fn render_python_tick_classes(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str("// @generated DO NOT EDIT — regenerated by build.rs from tick_schema.toml\n");
    out.push_str("// Typed pyclass structs per tick type + list converters.\n");
    out.push_str("// Matches the typed-struct approach used by Rust core, TypeScript, Go,\n");
    out.push_str("// and C++ FFI. Primary return shape for historical endpoint methods.\n\n");

    let type_names = sorted_type_names(schema);

    // Structs + pymethods.
    for type_name in &type_names {
        let def = &schema.types[*type_name];
        out.push_str(&render_python_tick_class_struct(type_name, def));
        out.push('\n');
    }

    // List converters.
    for type_name in &type_names {
        let def = &schema.types[*type_name];
        out.push_str(&render_python_tick_class_list_fn(type_name, def));
        out.push('\n');
    }

    // Registration helper.
    out.push_str(
        "pub(crate) fn register_tick_classes(m: &Bound<'_, PyModule>) -> PyResult<()> {\n",
    );
    for type_name in &type_names {
        writeln!(out, "    m.add_class::<{}>()?;", pyclass_name(type_name)).unwrap();
    }
    out.push_str("    Ok(())\n}\n\n");

    // Note: the `columns_for_qualname` dispatcher that used to live
    // here has been subsumed by `arrow_schema_for_qualname` in
    // `tick_arrow.rs` -- the Arrow schema carries the column names
    // AND types in one typed surface, so the bare name list became
    // redundant.
    out
}

fn python_list_fn_name(type_name: &str) -> &'static str {
    match type_name {
        "EodTick" => "eod_ticks_to_pyclass_list",
        "OhlcTick" => "ohlc_ticks_to_pyclass_list",
        "TradeTick" => "trade_ticks_to_pyclass_list",
        "QuoteTick" => "quote_ticks_to_pyclass_list",
        "TradeQuoteTick" => "trade_quote_ticks_to_pyclass_list",
        "OpenInterestTick" => "open_interest_ticks_to_pyclass_list",
        "MarketValueTick" => "market_value_ticks_to_pyclass_list",
        "GreeksTick" => "greeks_ticks_to_pyclass_list",
        "IvTick" => "iv_ticks_to_pyclass_list",
        "PriceTick" => "price_ticks_to_pyclass_list",
        "CalendarDay" => "calendar_days_to_pyclass_list",
        "InterestRateTick" => "interest_rate_ticks_to_pyclass_list",
        "OptionContract" => "option_contracts_to_pyclass_list",
        other => panic!("unsupported Python list fn: {other}"),
    }
}

/// Strip " -- N fields[...]." from the first doc line so the rendered
/// comment reflects the actual field set once `contract_id` and
/// `QuoteTick` midpoint get appended to `def.columns`. Words stay stable
/// even when a schema edit adds/removes a column, so we drop the numeric
/// count instead of recomputing it per surface.
pub(super) fn strip_field_count_from_doc(doc: &str) -> String {
    let first_line = doc.lines().next().unwrap_or("");
    if let Some(pos) = first_line.find(" -- ") {
        let after_dash = &first_line[pos + 4..];
        if after_dash.starts_with(|c: char| c.is_ascii_digit()) {
            let rest = after_dash
                .find(". ")
                .map(|i| &after_dash[i + 2..])
                .unwrap_or("");
            if rest.is_empty() {
                return format!("{}.", &first_line[..pos]);
            }
            return format!("{}. {rest}", &first_line[..pos]);
        }
    }
    first_line.to_string()
}

/// Pick the first few fields for `__repr__` rendering. Caps at
/// `max_fields` to keep output one-line readable in REPL / debugger
/// transcripts. Contract-id fields are appended after the schema
/// columns on the emitted struct, so we mirror that order here — the
/// first few real columns come first (e.g. `ms_of_day`, `open`,
/// `high`, `low`), never the trailing `expiration`/`strike`/`right`
/// triple which is identical across a result set and carries no
/// per-row diagnostic signal.
fn repr_fields_for_tick<'a>(
    type_name: &str,
    def: &'a TickTypeDef,
    max_fields: usize,
) -> Vec<ReprField<'a>> {
    let mut fields: Vec<ReprField<'a>> = Vec::new();
    for column in &def.columns {
        if fields.len() >= max_fields {
            break;
        }
        // `OptionContract.right` is surfaced as a String (see
        // `render_python_tick_class_struct`), so prefer `{:?}` so the
        // quotes are visible — consistent with other String fields
        // like `QuoteTick` never exposing `right` directly.
        let is_string = (type_name == "OptionContract" && column.field == "right")
            || matches!(column.r#type.as_str(), "String");
        fields.push(ReprField {
            name: &column.field,
            is_string,
        });
    }
    fields
}

struct ReprField<'a> {
    name: &'a str,
    is_string: bool,
}

fn render_python_tick_class_struct(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let class = pyclass_name(type_name);
    let is_quote_tick = type_name == "QuoteTick";
    let doc_text = if def.doc.is_empty() {
        format!("Typed {type_name} record.")
    } else {
        let stripped = strip_field_count_from_doc(&def.doc);
        // Preserve any trailing doc lines (e.g. `OptionContract`'s "Cannot
        // be `Copy` ..." note) by splicing the stripped first line back
        // onto the rest of the original doc. Lines after the first rarely
        // carry field-count language.
        let mut combined = stripped;
        for line in def.doc.lines().skip(1) {
            combined.push('\n');
            combined.push_str(line);
        }
        combined
    };
    for line in doc_text.lines() {
        writeln!(out, "/// {line}").unwrap();
    }
    // `#[must_use]` — these ticks come from expensive network calls; dropping
    // the result silently is a bug. `skip_from_py_object` opts out of the
    // auto-derived `FromPyObject` impl (deprecated for `Clone` pyclasses in
    // pyo3 0.28+); these classes are returned FROM Rust to Python but never
    // consumed AS arguments INTO Rust, so `FromPyObject` is dead weight.
    out.push_str("#[must_use]\n");
    out.push_str("#[pyclass(module = \"thetadatadx\", frozen, skip_from_py_object)]\n");
    out.push_str("#[derive(Clone)]\n");
    writeln!(out, "pub(crate) struct {class} {{").unwrap();
    for column in &def.columns {
        // Special case: `OptionContract.right` is stored on the wire as
        // `i32` (ASCII 67/80), but every other tick surfaces `right` as
        // a Python `"C" | "P" | ""` string (via the contract_id injection
        // below). Emit it as String here too so the two paths match and
        // consumer code doesn't get an integer on one endpoint and a
        // string on the others.
        let rust_type = if type_name == "OptionContract" && column.field == "right" {
            "String"
        } else {
            pyclass_field_type(column.r#type.as_str(), type_name)
        };
        writeln!(out, "    #[pyo3(get)] pub {}: {},", column.field, rust_type).unwrap();
    }
    if is_quote_tick {
        out.push_str("    #[pyo3(get)] pub midpoint: f64,\n");
    }
    if def.contract_id {
        out.push_str("    #[pyo3(get)] pub expiration: i32,\n");
        out.push_str("    #[pyo3(get)] pub strike: f64,\n");
        out.push_str("    #[pyo3(get)] pub right: String,\n");
    }
    out.push_str("}\n");

    // `__repr__` shows the first six schema fields with their live
    // values so REPLs and debuggers (pdb, IDE watch windows, pytest
    // short-repr) surface per-instance data instead of a useless
    // `"<EodTick(...)>"`. Six is a readable cap; `contract_id` columns
    // (`expiration`/`strike`/`right`) are identical across a result
    // set and carry no per-row signal, so we skip them.
    //
    // `#[new]` constructor accepts every field as a kwarg defaulting
    // to zero / empty-string. Tick classes are primarily returned
    // from the network endpoints so users rarely need to build them
    // manually; the one exception is test fixtures -- the Arrow
    // DataFrame adapter test suite constructs known lists in Python
    // and round-trips them through `to_arrow` / `to_dataframe` /
    // `to_polars` to verify value preservation. Without this ctor
    // there is no way to drive that test.
    writeln!(out, "#[pymethods]").unwrap();
    writeln!(out, "impl {class} {{").unwrap();
    out.push_str(&render_python_tick_class_new(type_name, def));
    let repr_fields = repr_fields_for_tick(type_name, def, 6);
    if repr_fields.is_empty() {
        // Unlikely (every tick has columns), but keep the fallback so
        // a future schema type without columns still compiles.
        writeln!(
            out,
            "    fn __repr__(&self) -> String {{ \"{class}()\".to_string() }}"
        )
        .unwrap();
    } else {
        let mut fmt_string = String::new();
        for (idx, field) in repr_fields.iter().enumerate() {
            if idx > 0 {
                fmt_string.push_str(", ");
            }
            fmt_string.push_str(field.name);
            fmt_string.push('=');
            // `{:?}` quotes strings (`right="C"`), `{}` for numerics —
            // matches the shape an engineer expects in a debugger line.
            fmt_string.push_str(if field.is_string { "{:?}" } else { "{}" });
        }
        let args = repr_fields
            .iter()
            .map(|f| format!("self.{}", f.name))
            .collect::<Vec<_>>()
            .join(", ");
        writeln!(out, "    fn __repr__(&self) -> String {{").unwrap();
        writeln!(out, "        format!(\"{class}({fmt_string})\", {args})").unwrap();
        out.push_str("    }\n");
    }
    out.push_str("}\n");
    out
}

fn pyclass_field_type(column_type: &str, type_name: &str) -> &'static str {
    match column_type {
        "i32" | "eod_num" | "eod_date" => "i32",
        "i64" | "eod_num64" => "i64",
        "f64" | "price" | "eod_price" => "f64",
        "String" => "String",
        other => panic!("unsupported column type '{other}' in pyclass for {type_name}"),
    }
}

/// Emit a `#[new]` kwarg constructor so Python test fixtures can
/// build tick instances. Every field accepts a default (0 / 0.0 /
/// empty-string) so callers can pass just the fields they care
/// about: `EodTick(ms_of_day=1, volume=1_000)`.
fn render_python_tick_class_new(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    struct CtorField<'a> {
        name: &'a str,
        rust_type: &'a str,
        default: &'static str,
    }
    let mut fields: Vec<CtorField> = Vec::new();
    for column in &def.columns {
        // Same OptionContract.right special case as the struct emitter.
        let rust_type = if type_name == "OptionContract" && column.field == "right" {
            "String"
        } else {
            pyclass_field_type(column.r#type.as_str(), type_name)
        };
        let default = match rust_type {
            "i32" => "0i32",
            "i64" => "0i64",
            "f64" => "0.0f64",
            "String" => "String::new()",
            other => panic!("unhandled pyclass ctor default for {other}"),
        };
        fields.push(CtorField {
            name: column.field.as_str(),
            rust_type,
            default,
        });
    }
    if type_name == "QuoteTick" {
        fields.push(CtorField {
            name: "midpoint",
            rust_type: "f64",
            default: "0.0f64",
        });
    }
    if def.contract_id {
        fields.push(CtorField {
            name: "expiration",
            rust_type: "i32",
            default: "0i32",
        });
        fields.push(CtorField {
            name: "strike",
            rust_type: "f64",
            default: "0.0f64",
        });
        fields.push(CtorField {
            name: "right",
            rust_type: "String",
            default: "String::new()",
        });
    }

    // `#[pyo3(signature = (*, a=..., b=...))]` forces keyword-only args so
    // the argument order in tests doesn't hard-code the schema field order.
    out.push_str("    #[new]\n");
    out.push_str("    #[pyo3(signature = (*,");
    for (idx, f) in fields.iter().enumerate() {
        if idx > 0 {
            out.push(',');
        }
        // pyo3 accepts literal Rust expressions for kwarg defaults.
        out.push_str(&format!(" {} = {}", f.name, f.default));
    }
    out.push_str("))]\n");
    // `too_many_arguments` is suppressed at the crate build command
    // (`cargo clippy ... -A clippy::too_many_arguments` in the
    // validation checklist) rather than per-function -- the project
    // policy forbids per-item `#[allow(...)]` additions.
    out.push_str("    fn new(");
    for (idx, f) in fields.iter().enumerate() {
        if idx > 0 {
            out.push_str(", ");
        }
        out.push_str(&format!("{}: {}", f.name, f.rust_type));
    }
    out.push_str(") -> Self {\n");
    writeln!(out, "        Self {{").unwrap();
    for f in &fields {
        writeln!(out, "            {n},", n = f.name).unwrap();
    }
    out.push_str("        }\n");
    out.push_str("    }\n");
    out
}

fn render_python_tick_class_list_fn(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let class = pyclass_name(type_name);
    let fn_name = python_list_fn_name(type_name);
    let is_quote_tick = type_name == "QuoteTick";

    writeln!(
        out,
        "pub(crate) fn {fn_name}(py: Python<'_>, ticks: &[tick::{type_name}]) -> PyResult<Py<PyAny>> {{"
    )
    .unwrap();
    out.push_str("    let list = pyo3::types::PyList::empty(py);\n");
    out.push_str("    for t in ticks {\n");
    writeln!(out, "        let obj = {class} {{").unwrap();
    for column in &def.columns {
        // Special case: `OptionContract.right` emits as a
        // `"C" | "P" | ""` string (see struct emitter above). Other
        // ticks get the String `right` from the contract_id injection
        // below; this keeps the standalone OptionContract endpoint in
        // sync so consumers never see a raw `67` / `80` / `0` on one
        // endpoint and a character on every other.
        if type_name == "OptionContract" && column.field == "right" {
            out.push_str(
                "            right: (if t.is_call() { \"C\" } else if t.is_put() { \"P\" } else { \"\" }).to_string(),\n",
            );
            continue;
        }
        let rust_type = pyclass_field_type(column.r#type.as_str(), type_name);
        if rust_type == "String" {
            writeln!(
                out,
                "            {}: t.{}.clone(),",
                column.field, column.field
            )
            .unwrap();
        } else {
            writeln!(out, "            {}: t.{},", column.field, column.field).unwrap();
        }
    }
    if is_quote_tick {
        out.push_str("            midpoint: t.midpoint,\n");
    }
    if def.contract_id {
        out.push_str("            expiration: t.expiration,\n");
        out.push_str("            strike: t.strike,\n");
        out.push_str(
            "            right: (if t.is_call() { \"C\" } else if t.is_put() { \"P\" } else { \"\" }).to_string(),\n",
        );
    }
    out.push_str("        };\n");
    out.push_str("        list.append(Py::new(py, obj)?)?;\n");
    out.push_str("    }\n");
    out.push_str("    Ok(list.into_any().unbind())\n");
    out.push_str("}\n");
    out
}
