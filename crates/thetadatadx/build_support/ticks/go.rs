pub(super) fn render_go_tick_converters(schema: &Schema) -> String {
    let mut out = String::new();
    // Go spec header: `^// Code generated .* DO NOT EDIT\.$` — see
    // https://golang.org/s/generatedcode. Required for go vet / gofmt /
    // staticcheck to recognize this as machine-generated. Non-Go
    // emitters keep the uniform `@generated` form.
    out.push_str("// Code generated by build.rs from tick_schema.toml; DO NOT EDIT.\n\n");
    out.push_str("package thetadatadx\n\n");
    out.push_str("/*\n#include \"ffi_bridge.h\"\n*/\nimport \"C\"\n\n");
    out.push_str("import \"unsafe\"\n\n");
    for type_name in sorted_type_names(schema) {
        let def = &schema.types[type_name];
        out.push_str(&render_go_tick_converter(type_name, def));
        out.push('\n');
    }
    out
}

fn render_go_tick_converter(type_name: &str, def: &TickTypeDef) -> String {
    let mut out = String::new();
    let (func_name, arr_type, ffi_type, public_type) = match type_name {
        "EodTick" => ("convertEodTicks", "C.TdxTickArray", "cEodTick", "EodTick"),
        "OhlcTick" => (
            "convertOhlcTicks",
            "C.TdxTickArray",
            "cOhlcTick",
            "OhlcTick",
        ),
        "TradeTick" => (
            "convertTradeTicks",
            "C.TdxTickArray",
            "cTradeTick",
            "TradeTick",
        ),
        "QuoteTick" => (
            "convertQuoteTicks",
            "C.TdxTickArray",
            "cQuoteTick",
            "QuoteTick",
        ),
        "TradeQuoteTick" => (
            "convertTradeQuoteTicks",
            "C.TdxTickArray",
            "cTradeQuoteTick",
            "TradeQuoteTick",
        ),
        "OpenInterestTick" => (
            "convertOpenInterestTicks",
            "C.TdxTickArray",
            "cOpenInterestTick",
            "OpenInterestTick",
        ),
        "MarketValueTick" => (
            "convertMarketValueTicks",
            "C.TdxTickArray",
            "cMarketValueTick",
            "MarketValueTick",
        ),
        "GreeksTick" => (
            "convertGreeksTicks",
            "C.TdxTickArray",
            "cGreeksTick",
            "GreeksTick",
        ),
        "IvTick" => ("convertIvTicks", "C.TdxTickArray", "cIvTick", "IVTick"),
        "PriceTick" => (
            "convertPriceTicks",
            "C.TdxTickArray",
            "cPriceTick",
            "PriceTick",
        ),
        "CalendarDay" => (
            "convertCalendarDays",
            "C.TdxTickArray",
            "cCalendarDay",
            "CalendarDay",
        ),
        "InterestRateTick" => (
            "convertInterestRateTicks",
            "C.TdxTickArray",
            "cInterestRateTick",
            "InterestRateTick",
        ),
        "OptionContract" => (
            "convertOptionContracts",
            "C.TdxOptionContractArray",
            "cOptionContract",
            "OptionContract",
        ),
        other => panic!("unsupported Go tick converter type: {other}"),
    };

    writeln!(out, "func {func_name}(arr {arr_type}) []{public_type} {{").unwrap();
    out.push_str("    if arr.data == nil || arr.len == 0 {\n");
    out.push_str("        return nil\n");
    out.push_str("    }\n");
    out.push_str("    n := int(arr.len)\n");
    writeln!(out, "    src := unsafe.Slice((*{ffi_type})(arr.data), n)").unwrap();
    writeln!(out, "    result := make([]{public_type}, n)").unwrap();
    out.push_str("    for i, t := range src {\n");
    if type_name == "OptionContract" {
        out.push_str("        symbol := \"\"\n");
        out.push_str("        if t.Symbol != 0 {\n");
        out.push_str("            symbol = C.GoString((*C.char)(unsafe.Pointer(t.Symbol)))\n");
        out.push_str("        }\n");
    }
    writeln!(out, "        result[i] = {public_type}{{").unwrap();
    for column in &def.columns {
        let public_field = go_public_field_name(type_name, &column.field);
        let source_expr = go_source_expr(type_name, &column.field, &column.r#type);
        writeln!(out, "            {public_field}: {source_expr},").unwrap();
    }
    if type_name == "QuoteTick" {
        out.push_str("            Midpoint: t.Midpoint,\n");
    }
    if def.contract_id {
        out.push_str("            Expiration: t.Expiration,\n");
        out.push_str("            Strike: t.Strike,\n");
        out.push_str("            Right: RightStr(t.Right),\n");
    }
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("    return result\n");
    out.push_str("}\n");
    out
}

fn tick_ffi_offsets(type_name: &str, def: &TickTypeDef) -> Vec<(String, usize)> {
    let mut offsets = Vec::new();
    let mut size = 0usize;
    for (field_name, field_type) in tick_ffi_fields(type_name, def) {
        let (field_size, field_align) = tick_ffi_field_layout(field_type);
        size = align_to(size, field_align);
        offsets.push((field_name.to_string(), size));
        size += field_size;
    }
    offsets
}

fn tick_ffi_size(type_name: &str, def: &TickTypeDef) -> usize {
    let mut size = 0usize;
    let mut struct_align = def.align.unwrap_or(1) as usize;
    for (_, field_type) in tick_ffi_fields(type_name, def) {
        let (field_size, field_align) = tick_ffi_field_layout(field_type);
        struct_align = struct_align.max(field_align);
        size = align_to(size, field_align) + field_size;
    }
    align_to(size, struct_align)
}

fn tick_ffi_fields<'a>(type_name: &'a str, def: &'a TickTypeDef) -> Vec<(&'a str, &'a str)> {
    let mut fields = def
        .columns
        .iter()
        .map(|column| (column.field.as_str(), column.r#type.as_str()))
        .collect::<Vec<_>>();
    if def.contract_id {
        fields.push(("expiration", "i32"));
        fields.push(("strike", "price"));
        fields.push(("right", "i32"));
    }
    if type_name == "QuoteTick" {
        fields.push(("midpoint", "price"));
    }
    fields
}

fn tick_ffi_field_layout(kind: &str) -> (usize, usize) {
    match kind {
        "i32" | "eod_num" | "eod_date" => (4, 4),
        "i64" | "eod_num64" | "f64" | "price" | "eod_price" => (8, 8),
        "String" => (
            std::mem::size_of::<*const ()>(),
            std::mem::align_of::<*const ()>(),
        ),
        other => panic!("unsupported Go tick FFI field type: {other}"),
    }
}

