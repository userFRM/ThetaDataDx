fn go_source_expr(type_name: &str, field: &str, kind: &str) -> String {
    let ffi_field = go_ffi_field_name(field);
    match (type_name, field, kind) {
        ("OptionContract", "symbol", "String") => "symbol".into(),
        ("OptionContract", "right", "i32") => "RightStr(t.Right)".into(),
        (_, "right", "i32") => "RightStr(t.Right)".into(),
        (_, "is_open", "i32") => "t.IsOpen != 0".into(),
        ("OptionContract", _, "i32") => format!("int(t.{ffi_field})"),
        (_, _, "i32") if field == "expiration" => format!("t.{ffi_field}"),
        (_, _, "i32") | (_, _, "eod_num") | (_, _, "eod_date") => format!("int(t.{ffi_field})"),
        (_, _, "i64")
        | (_, _, "eod_num64")
        | (_, _, "f64")
        | (_, _, "price")
        | (_, _, "eod_price") => format!("t.{ffi_field}"),
        (_, _, "String") => format!("t.{ffi_field}"),
        _ => panic!("unsupported Go source expression for {type_name}.{field}:{kind}"),
    }
}

