//! C++ SDK emitter.
//!
//! Generates the EndpointRequestOptions header, per-endpoint method
//! declarations/definitions on `MddsClient`, and the cgo-facing
//! `_with_options` extern decl header.

use std::fmt::Write as _;

use super::super::helpers::{endpoint_streams_repr_c_ticks, is_streaming_endpoint};
use super::super::model::{GeneratedEndpoint, GeneratedParam};
use super::super::sdk_helpers::{
    builder_copy_expr, builder_value_type_name, cpp_converter_expr, cpp_method_arg_decl,
    cpp_value_type, ffi_array_type, method_params, sdk_method_arg_name,
};

/// Renders the C++ `EndpointRequestOptions` header: the optional builder
/// fields, their `with_*` setters, and the internal FFI bridge struct.
pub(super) fn render_cpp_options(params: &[GeneratedParam]) -> String {
    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT — regenerated by build.rs from endpoint_surface.toml\n",
    );
    out.push_str(include_str!(
        "templates/cpp/endpoint_options_header.cpp.tmpl"
    ));
    for param in params {
        let ty = match param.param_type.as_str() {
            "Int" => "std::optional<int32_t>",
            "Float" => "std::optional<double>",
            "Bool" => "std::optional<bool>",
            _ => "std::optional<std::string>",
        };
        super::doc::cpp_doc_lines(&mut out, "    ", &param.description);
        writeln!(out, "    {} {};", ty, param.name).unwrap();
    }
    out.push_str("    /// Per-call deadline in milliseconds.\n");
    out.push_str("    std::optional<uint64_t> timeout_ms;\n");
    out.push('\n');
    for param in params {
        let arg_ty = builder_value_type_name(param);
        writeln!(
            out,
            "    EndpointRequestOptions& with_{}({} value) {{",
            param.name, arg_ty
        )
        .unwrap();
        writeln!(out, "        {};", builder_copy_expr(param, "value")).unwrap();
        out.push_str("        return *this;\n");
        out.push_str("    }\n");
    }
    out.push_str(include_str!("templates/cpp/with_timeout_ms.cpp.tmpl"));
    // A negative duration is rejected with `InvalidParameterError` rather
    // than coerced: the unsigned `timeout_ms` field cannot represent it,
    // and an `static_cast<uint64_t>` of a negative count would wrap to a
    // multi-century deadline — the opposite of the caller's intent. This
    // matches the reject-don't-coerce contract the other bindings hold for
    // the same out-of-domain deadline.
    out.push_str(include_str!("templates/cpp/with_deadline.cpp.tmpl"));
    out.push_str("};\n");
    out.push_str("\nnamespace detail {\n\n");
    out.push_str("struct FfiEndpointRequestOptions {\n");
    out.push_str("    TdxEndpointRequestOptions raw{};\n");
    for param in params {
        if param.param_type != "Int" && param.param_type != "Float" && param.param_type != "Bool" {
            writeln!(out, "    std::string {}_storage;", param.name).unwrap();
        }
    }
    out.push('\n');
    out.push_str(
        "    explicit FfiEndpointRequestOptions(const EndpointRequestOptions& options) {\n",
    );
    for param in params {
        let template = match param.param_type.as_str() {
            "Int" | "Float" => include_str!("templates/cpp/ffi_numeric_case.cpp.tmpl"),
            "Bool" => include_str!("templates/cpp/ffi_bool_case.cpp.tmpl"),
            _ => include_str!("templates/cpp/ffi_string_case.cpp.tmpl"),
        };
        out.push_str(&template.replace("__PARAM__", &param.name));
    }
    out.push_str("        if (options.timeout_ms) {\n");
    out.push_str("            raw.timeout_ms = *options.timeout_ms;\n");
    out.push_str("            raw.has_timeout_ms = 1;\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("};\n\n");
    out.push_str("} // namespace detail\n");
    out
}

/// Renders the `MddsClient` member-function declarations for every
/// non-streaming endpoint, including the singular-symbol overloads.
pub(super) fn render_cpp_historical_decls(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str(
        "    // @generated DO NOT EDIT — regenerated by build.rs from endpoint_surface.toml\n\n",
    );
    for endpoint in endpoints
        .iter()
        .filter(|endpoint| !is_streaming_endpoint(endpoint))
    {
        // Use `///` line-comments rather than a `/** ... */` block so a
        // description containing `*/` cannot prematurely close the
        // doc-comment.
        super::doc::cpp_doc_lines(&mut out, "    ", &endpoint.description);
        let has_symbols = method_params(endpoint)
            .iter()
            .any(|param| param.param_type == "Symbols");
        if has_symbols {
            let mut params = method_params(endpoint)
                .iter()
                .map(|param| {
                    if param.param_type == "Symbols" {
                        "const std::string& symbol".to_string()
                    } else {
                        cpp_method_arg_decl(param)
                    }
                })
                .collect::<Vec<_>>();
            params.push("const EndpointRequestOptions& options = {}".into());
            writeln!(
                out,
                "    std::vector<{}> {}({}) const;\n",
                cpp_value_type(&endpoint.return_type),
                endpoint.name,
                params.join(", ")
            )
            .unwrap();
        }
        let mut params = method_params(endpoint)
            .iter()
            .map(|param| cpp_method_arg_decl(param))
            .collect::<Vec<_>>();
        // Every endpoint accepts EndpointRequestOptions now: even
        // endpoints with no other builder params can carry a timeout_ms.
        params.push("const EndpointRequestOptions& options = {}".into());
        writeln!(
            out,
            "    std::vector<{}> {}({}) const;\n",
            cpp_value_type(&endpoint.return_type),
            endpoint.name,
            params.join(", ")
        )
        .unwrap();
    }
    out
}

/// One trailing argument list shared by the `<endpoint>_stream` declaration
/// and definition: the endpoint's positional params, the typed chunk handler,
/// and the trailing `EndpointRequestOptions`. `default_options` controls the
/// `= {}` default arg, present on the in-class declaration and absent on the
/// out-of-line definition (a redefaulted parameter is ill-formed in C++).
fn cpp_stream_signature(endpoint: &GeneratedEndpoint, default_options: bool) -> String {
    let tick = cpp_value_type(&endpoint.return_type);
    let mut params = method_params(endpoint)
        .iter()
        .map(|param| cpp_method_arg_decl(param))
        .collect::<Vec<_>>();
    params.push(format!("std::function<void(Span<const {tick}>)> handler"));
    let options = if default_options {
        "const EndpointRequestOptions& options = {}"
    } else {
        "const EndpointRequestOptions& options"
    };
    params.push(options.into());
    params.join(", ")
}

/// Emit the `<endpoint>_stream(...)` server-stream method *declarations* on the
/// C++ `ThetaDataDxClient`, included in the class body via
/// `sdks/cpp/include/historical_stream.hpp.inc`.
///
/// Declarations only — the definitions live out-of-line in
/// `historical_stream.cpp.inc` (mirroring the buffered `historical.cpp.inc`).
/// Keeping the C-ABI `tdx_<endpoint>_stream` call sites out of the class body
/// is also what stops the cross-binding parity collector — which scans class
/// bodies for `name(` shapes — from mistaking a C-ABI call for a C++ method.
/// The one inline member is the shared `extern "C"` trampoline
/// `stream_chunk_shim`: tick-agnostic (the erased handler carries the per-
/// endpoint reinterpretation) and `tdx_`-free, so it is safe inline. It
/// swallows exceptions — a throw must never unwind across the C ABI into Rust,
/// the same contract the FPSS `callback_shim` holds.
pub(super) fn render_cpp_stream_decls(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str(
        "    // @generated DO NOT EDIT — regenerated by build.rs from endpoint_surface.toml\n\n",
    );

    out.push_str(include_str!("templates/cpp/stream_chunk_shim.cpp.tmpl"));
    out.push('\n');

    for endpoint in endpoints
        .iter()
        .filter(|endpoint| endpoint_streams_repr_c_ticks(endpoint))
    {
        let tick = cpp_value_type(&endpoint.return_type);

        super::doc::cpp_doc_lines(&mut out, "    ", &endpoint.description);
        out.push_str("    ///\n");
        writeln!(
            out,
            "    /// Stream `{}` rows into `handler` without materialising the full",
            endpoint.name
        )
        .unwrap();
        out.push_str("    /// response. `handler` is invoked once per server chunk with a\n");
        writeln!(
            out,
            "    /// `Span<const {tick}>` view over a contiguous run of rows; the chunk is"
        )
        .unwrap();
        out.push_str("    /// freed before the next is fetched, so peak memory tracks a single\n");
        out.push_str("    /// chunk rather than the whole result. Carries the same optional\n");
        out.push_str("    /// builder parameters and `timeout_ms` as the buffered method via\n");
        out.push_str("    /// `options`. Throws on a wire or decode error once the stream\n");
        out.push_str("    /// drains; the `Span` is valid only for the duration of each call.\n");
        writeln!(
            out,
            "    void {}_stream({}) const;\n",
            endpoint.name,
            cpp_stream_signature(endpoint, true),
        )
        .unwrap();
    }
    out
}

/// Emit the out-of-line `ThetaDataDxClient::<endpoint>_stream(...)`
/// definitions, included in `thetadx.cpp` via
/// `sdks/cpp/src/historical_stream.cpp.inc`.
///
/// Each borrows the historical `TdxMddsClient*` from the unified handle
/// (`tdx_unified_historical`), builds the same `EndpointRequestOptions` the
/// buffered methods take, erases the typed `handler` to a
/// `std::function<void(const void*, size_t)>` that reinterprets each chunk
/// pointer as the endpoint's tick type, and drives the C-ABI
/// `tdx_<endpoint>_stream` entry point through the shared `stream_chunk_shim`
/// trampoline. The handler and the erased wrapper live on the stack for the
/// synchronous drain — the C ABI blocks until the stream completes and only
/// fires the callback during that call, so no heap lifetime juggling is
/// needed (unlike the long-lived FPSS push callback).
pub(super) fn render_cpp_stream_defs(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT — regenerated by build.rs from endpoint_surface.toml\n\n",
    );

    for endpoint in endpoints
        .iter()
        .filter(|endpoint| endpoint_streams_repr_c_ticks(endpoint))
    {
        let method_params = method_params(endpoint);
        let tick = cpp_value_type(&endpoint.return_type);

        writeln!(
            out,
            "void ThetaDataDxClient::{}_stream({}) const {{",
            endpoint.name,
            cpp_stream_signature(endpoint, false),
        )
        .unwrap();
        out.push_str("    const TdxMddsClient* hist = tdx_unified_historical(handle_.get());\n");
        out.push_str("    if (hist == nullptr) {\n");
        out.push_str("        detail::throw_last_ffi_error();\n");
        out.push_str("    }\n");
        out.push_str("    detail::FfiEndpointRequestOptions ffi_options(options);\n");
        // Clear any stale FFI error so a -1 return is attributed to this call.
        out.push_str("    tdx_clear_error();\n");
        // Erase the typed handler: reinterpret each chunk pointer as the
        // endpoint's tick type and build the span. The borrow of `handler` is
        // sound because both live to the end of this synchronous call.
        writeln!(
            out,
            "    std::function<void(const void*, std::size_t)> erased =\n        [&handler](const void* rows, std::size_t len) {{\n            handler(Span<const {tick}>(static_cast<const {tick}*>(rows), len));\n        }};"
        )
        .unwrap();
        write!(out, "    int32_t rc = tdx_{}_stream(hist", endpoint.name).unwrap();
        for param in &method_params {
            write!(out, ", {}.c_str()", sdk_method_arg_name(param)).unwrap();
        }
        out.push_str(", &ThetaDataDxClient::stream_chunk_shim, &erased, &ffi_options.raw);\n");
        out.push_str("    if (rc != 0) {\n");
        out.push_str("        detail::throw_last_ffi_error();\n");
        out.push_str("    }\n");
        out.push_str("}\n\n");
    }
    out
}

/// Renders the `MddsClient` member-function definitions for every
/// non-streaming endpoint, bridging each into its `_with_options` FFI call.
pub(super) fn render_cpp_historical_defs(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT — regenerated by build.rs from endpoint_surface.toml\n\n",
    );
    for endpoint in endpoints
        .iter()
        .filter(|endpoint| !is_streaming_endpoint(endpoint))
    {
        out.push_str(&render_cpp_endpoint_def(endpoint));
        out.push('\n');
    }
    out
}

fn render_cpp_endpoint_def(endpoint: &GeneratedEndpoint) -> String {
    let method_params = method_params(endpoint);
    let has_symbols = method_params
        .iter()
        .any(|param| param.param_type == "Symbols");
    let mut signature_parts = method_params
        .iter()
        .map(|param| cpp_method_arg_decl(param))
        .collect::<Vec<_>>();
    // Every endpoint accepts EndpointRequestOptions so callers can
    // pass at least the cross-cutting deadline.
    signature_parts.push("const EndpointRequestOptions& options".into());

    let mut out = String::new();
    if has_symbols {
        let mut singular_parts = method_params
            .iter()
            .map(|param| {
                if param.param_type == "Symbols" {
                    "const std::string& symbol".to_string()
                } else {
                    cpp_method_arg_decl(param)
                }
            })
            .collect::<Vec<_>>();
        singular_parts.push("const EndpointRequestOptions& options".into());
        writeln!(
            out,
            "std::vector<{}> MddsClient::{}({}) const {{",
            cpp_value_type(&endpoint.return_type),
            endpoint.name,
            singular_parts.join(", ")
        )
        .unwrap();
        let forwarded = method_params
            .iter()
            .map(|param| {
                if param.param_type == "Symbols" {
                    "std::vector<std::string>{symbol}".to_string()
                } else {
                    sdk_method_arg_name(param)
                }
            })
            .collect::<Vec<_>>()
            .join(", ");
        writeln!(out, "    return {}({}, options);", endpoint.name, forwarded).unwrap();
        out.push_str("}\n\n");
    }
    writeln!(
        out,
        "std::vector<{}> MddsClient::{}({}) const {{",
        cpp_value_type(&endpoint.return_type),
        endpoint.name,
        signature_parts.join(", ")
    )
    .unwrap();

    if has_symbols {
        out.push_str("    auto symbol_ptrs = detail::string_ptrs(symbols);\n");
    }
    out.push_str("    detail::FfiEndpointRequestOptions ffi_options(options);\n");
    // Clear any stale FFI error before the call so check_string_array /
    // check_tick_array can disambiguate "success-empty" from
    // "failure-with-empty-sentinel" (e.g. timeout). See
    // ffi/src/lib.rs::tdx_clear_error.
    out.push_str("    tdx_clear_error();\n");

    if endpoint.return_type == "StringList" {
        write!(
            out,
            "    return detail::check_string_array(tdx_{}_with_options",
            endpoint.name
        )
        .unwrap();
        out.push_str("(handle_.get()");
        for param in &method_params {
            if param.param_type == "Symbols" {
                out.push_str(", symbol_ptrs.data(), symbol_ptrs.size()");
            } else {
                write!(out, ", {}.c_str()", sdk_method_arg_name(param)).unwrap();
            }
        }
        out.push_str(", &ffi_options.raw));\n");
        out.push_str("}\n");
        return out;
    }

    if endpoint.return_type == "OptionContracts" {
        write!(
            out,
            "    TdxOptionContractArray arr = tdx_{}_with_options",
            endpoint.name
        )
        .unwrap();
        out.push_str("(handle_.get()");
        for param in &method_params {
            if param.param_type == "Symbols" {
                out.push_str(", symbol_ptrs.data(), symbol_ptrs.size()");
            } else {
                write!(out, ", {}.c_str()", sdk_method_arg_name(param)).unwrap();
            }
        }
        out.push_str(", &ffi_options.raw);\n");
        // Disambiguate empty-success vs failure-empty (e.g. timeout). See
        // ffi/src/lib.rs::tdx_clear_error and the matching call before the FFI.
        out.push_str(include_str!(
            "templates/cpp/option_contracts_convert.cpp.tmpl"
        ));
        return out;
    }

    write!(out, "    auto arr = tdx_{}_with_options", endpoint.name).unwrap();
    out.push_str("(handle_.get()");
    for param in &method_params {
        if param.param_type == "Symbols" {
            out.push_str(", symbol_ptrs.data(), symbol_ptrs.size()");
        } else {
            write!(out, ", {}.c_str()", sdk_method_arg_name(param)).unwrap();
        }
    }
    out.push_str(", &ffi_options.raw);\n");
    writeln!(out, "    {}", cpp_converter_expr(&endpoint.return_type)).unwrap();
    out.push_str("}\n");
    out
}

/// Renders the cgo-facing `extern` declarations for every endpoint's
/// `tdx_<endpoint>_with_options` C entry point.
pub(super) fn render_c_endpoint_with_options_decls(endpoints: &[GeneratedEndpoint]) -> String {
    let mut out = String::new();
    out.push_str(
        "/* @generated DO NOT EDIT — regenerated by build.rs from endpoint_surface.toml */\n",
    );
    // All non-streaming endpoints now have a `_with_options` form.
    for endpoint in endpoints
        .iter()
        .filter(|endpoint| !is_streaming_endpoint(endpoint))
    {
        let ffi_name = format!("tdx_{}_with_options", endpoint.name);
        let return_type = ffi_array_type(&endpoint.return_type);
        let params = method_params(endpoint);
        write!(
            out,
            "extern {} {}(const TdxMddsClient* client",
            return_type, ffi_name
        )
        .unwrap();
        for param in &params {
            if param.param_type == "Symbols" {
                write!(out, ", const char* const* symbols, size_t symbols_len").unwrap();
            } else {
                write!(out, ", const char* {}", param.name).unwrap();
            }
        }
        out.push_str(", const TdxEndpointRequestOptions* options);\n");
    }
    out
}
