//! REST endpoint builder emitter.
//!
//! Generates `crates/thetadatadx/src/rest/_generated/rest_endpoints.rs` вЂ”
//! the per-endpoint builder structs + their `impl` blocks + the matching
//! `RestClient` constructor methods. Driven by `endpoint_surface.toml`:
//! every endpoint whose `transport = "rest"` or `transport = "both"` is
//! emitted with a parallel REST builder.
//!
//! Per-endpoint variation reduces to three knobs:
//!
//! 1. The REST path (`endpoint._rest_path` вЂ” read off the resolved TOML
//!    surface).
//! 2. The decoder function (derived from `endpoint.return_type` via the
//!    [`rest_tick_mapping`] table, which is the single SSOT for the
//!    `<XxxTicks>` в†’ `(<XxxTick>, decode_<xxx>_csv)` pair).
//! 3. Whether the endpoint accepts an `interval` query parameter вЂ” keyed
//!    on the presence of a TOML param named `interval`.
//!
//! Every other shape (the required `symbol` / `expiration` / `start_date`
//! / `end_date` query params, the `format=csv` literal, the strike /
//! right / end_date optional setters, the `fetch_csv` + decode call
//! sequence) is identical across endpoints and templated once here.
//!
//! The generated file is byte-identical (modulo `cargo fmt` whitespace
//! normalization) to the hand-written builders previously living in
//! `crates/thetadatadx/src/rest/client.rs`. Adding a fifth REST endpoint
//! becomes one row in `endpoint_surface.toml` (set `transport = "both"`
//! on an endpoint whose `returns` maps via [`rest_tick_mapping`]); no
//! code change required.

use std::fmt::Write as _;

use super::super::model::{GeneratedEndpoint, Transport};

/// Map a `return_type` (e.g. `"QuoteTicks"`) to the (tick struct name,
/// CSV decoder function) pair used by the REST builder for that
/// endpoint. The mapping is the SSOT for which tick families ship a
/// REST decoder today вЂ” return `None` for any `return_type` that has no
/// hand-written decoder counterpart, and the renderer panics at codegen
/// time (a missing TOML row would otherwise silently emit nothing).
fn rest_tick_mapping(return_type: &str) -> Option<(&'static str, &'static str)> {
    match return_type {
        "QuoteTicks" => Some(("QuoteTick", "decode_quote_csv")),
        "TradeQuoteTicks" => Some(("TradeQuoteTick", "decode_trade_quote_csv")),
        "IvTicks" => Some(("IvTick", "decode_iv_csv")),
        "GreeksFirstOrderTicks" => Some(("GreeksFirstOrderTick", "decode_greeks_first_order_csv")),
        _ => None,
    }
}

/// PascalCase builder type name for the endpoint
/// (`option_history_quote` в†’ `OptionHistoryQuoteRestBuilder`).
///
/// Mirrors the hand-written builder type names verbatim, with one
/// special-case alias to match the established API surface: the
/// implied-volatility endpoint's builder is exposed as
/// `OptionHistoryGreeksIvRestBuilder` (matching the codebase's existing
/// `IvTick` / `decode_iv_csv` shorthand), not the over-long
/// `OptionHistoryGreeksImpliedVolatilityRestBuilder`.
fn rest_builder_type_name(endpoint_name: &str) -> String {
    // Special-case to preserve the existing public API. Backwards-
    // compatible alias, not a rename: the hand-written builders used
    // `Iv` shorthand for the `implied_volatility` endpoint.
    if endpoint_name == "option_history_greeks_implied_volatility" {
        return "OptionHistoryGreeksIvRestBuilder".to_string();
    }
    let mut out = String::new();
    for segment in endpoint_name.split('_') {
        let mut chars = segment.chars();
        if let Some(first) = chars.next() {
            out.push(first.to_ascii_uppercase());
            out.push_str(chars.as_str());
        }
    }
    out.push_str("RestBuilder");
    out
}

/// `true` when the endpoint accepts an `interval` query parameter.
/// Keyed strictly on TOML вЂ” adding an endpoint whose template carries
/// `interval` automatically emits the `.interval()` setter.
fn endpoint_has_interval(endpoint: &GeneratedEndpoint) -> bool {
    endpoint.params.iter().any(|param| param.name == "interval")
}

/// Render `crates/thetadatadx/src/rest/_generated/rest_endpoints.rs`.
///
/// Emits the file unconditionally (every checked-in generated file is
/// regenerated on every `generate_sdk_surfaces` run); the body filters
/// to endpoints with `Transport::Rest` or `Transport::Both`.
pub(super) fn render_rest_endpoints(endpoints: &[GeneratedEndpoint]) -> String {
    let rest_endpoints: Vec<&GeneratedEndpoint> = endpoints
        .iter()
        .filter(|endpoint| matches!(endpoint._transport, Transport::Rest | Transport::Both))
        .collect();

    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT вЂ” regenerated by `cargo run --bin generate_sdk_surfaces`\n",
    );
    out.push_str("// from `endpoint_surface.toml`. Builder structs + their `impl`\n");
    out.push_str("// blocks + the matching `RestClient` constructor methods are emitted\n");
    out.push_str("// from a single per-endpoint template; the hand-written hot-path\n");
    out.push_str("// counterparts in `crates/thetadatadx/src/rest/client.rs` were\n");
    out.push_str("// replaced by this file when issue #580 landed.\n\n");
    // Import lines match `cargo fmt`'s output verbatim so the generator
    // and the checked-in formatted file round-trip without drift.
    out.push_str(
        "use tdbe::types::tick::{GreeksFirstOrderTick, IvTick, QuoteTick, TradeQuoteTick};\n\n",
    );
    out.push_str("use super::super::client::{\n");
    out.push_str("    decode_greeks_first_order_csv, decode_iv_csv, decode_quote_csv, decode_trade_quote_csv,\n");
    out.push_str("    fetch_csv, RestClient,\n");
    out.push_str("};\n");
    out.push_str("use super::super::error::RestError;\n\n");

    // в”Ђв”Ђ RestClient constructor methods в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
    out.push_str("impl RestClient {\n");
    for (idx, endpoint) in rest_endpoints.iter().enumerate() {
        if idx > 0 {
            out.push('\n');
        }
        out.push_str(&render_rest_client_method(endpoint));
    }
    out.push_str("}\n\n");

    // в”Ђв”Ђ Per-endpoint builder structs + impls в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
    for (idx, endpoint) in rest_endpoints.iter().enumerate() {
        if idx > 0 {
            out.push('\n');
        }
        out.push_str(&render_rest_builder(endpoint));
    }

    out
}

/// Render the `RestClient::<endpoint>(...)` constructor method.
fn render_rest_client_method(endpoint: &GeneratedEndpoint) -> String {
    let builder_ty = rest_builder_type_name(&endpoint.name);
    let has_interval = endpoint_has_interval(endpoint);
    let rest_path = &endpoint._rest_path;

    let mut out = String::new();
    writeln!(
        out,
        "    /// Build a builder for `{}` (REST path `{}`).",
        endpoint.name, rest_path,
    )
    .unwrap();
    writeln!(out, "    ///").unwrap();
    writeln!(
        out,
        "    /// Mirrors the gRPC builder's setter surface; decodes to",
    )
    .unwrap();
    let (tick_ty, _decoder) = rest_tick_mapping(&endpoint.return_type).unwrap_or_else(|| {
        panic!(
            "endpoint '{}' has return_type '{}' with no entry in `rest_tick_mapping`; \
             add it to `build_support_bin/endpoints/sdk_render/rest_builder.rs` before \
             flipping the endpoint to a REST transport.",
            endpoint.name, endpoint.return_type,
        )
    });
    writeln!(out, "    /// `Vec<{tick_ty}>` on `.execute().await`.",).unwrap();
    writeln!(out, "    #[must_use]").unwrap();
    writeln!(out, "    pub fn {}(", endpoint.name,).unwrap();
    writeln!(out, "        &self,").unwrap();
    writeln!(out, "        symbol: &str,").unwrap();
    writeln!(out, "        expiration: &str,").unwrap();
    writeln!(out, "        date: &str,").unwrap();
    writeln!(out, "    ) -> {builder_ty}<'_> {{").unwrap();
    writeln!(out, "        {builder_ty} {{").unwrap();
    writeln!(out, "            client: self,").unwrap();
    writeln!(out, "            symbol: symbol.to_string(),").unwrap();
    writeln!(out, "            expiration: expiration.to_string(),").unwrap();
    writeln!(out, "            start_date: date.to_string(),").unwrap();
    writeln!(out, "            end_date: date.to_string(),").unwrap();
    writeln!(out, "            strike: None,").unwrap();
    writeln!(out, "            right: None,").unwrap();
    if has_interval {
        writeln!(out, "            interval: None,").unwrap();
    }
    writeln!(out, "        }}").unwrap();
    writeln!(out, "    }}").unwrap();
    out
}

/// Render the per-endpoint builder struct + its `impl` block.
fn render_rest_builder(endpoint: &GeneratedEndpoint) -> String {
    let builder_ty = rest_builder_type_name(&endpoint.name);
    let has_interval = endpoint_has_interval(endpoint);
    let rest_path = &endpoint._rest_path;
    let (tick_ty, decoder) =
        rest_tick_mapping(&endpoint.return_type).expect("rest_tick_mapping verified above");

    let mut out = String::new();
    // Struct decl
    writeln!(out, "/// Builder for [`RestClient::{}`].", endpoint.name,).unwrap();
    writeln!(out, "#[derive(Debug)]").unwrap();
    writeln!(out, "pub struct {builder_ty}<'a> {{").unwrap();
    writeln!(out, "    client: &'a RestClient,").unwrap();
    writeln!(out, "    symbol: String,").unwrap();
    writeln!(out, "    expiration: String,").unwrap();
    writeln!(out, "    start_date: String,").unwrap();
    writeln!(out, "    end_date: String,").unwrap();
    writeln!(out, "    strike: Option<String>,").unwrap();
    writeln!(out, "    right: Option<String>,").unwrap();
    if has_interval {
        writeln!(out, "    interval: Option<String>,").unwrap();
    }
    writeln!(out, "}}").unwrap();
    out.push('\n');

    // Impl block
    writeln!(out, "impl<'a> {builder_ty}<'a> {{").unwrap();
    writeln!(
        out,
        "    /// Set the option strike price (e.g. `\"440\"` for $440, `\"17.5\"`"
    )
    .unwrap();
    writeln!(
        out,
        "    /// for $17.50). `None` is equivalent to the wildcard query."
    )
    .unwrap();
    writeln!(out, "    #[must_use]").unwrap();
    writeln!(
        out,
        "    pub fn strike(mut self, strike: impl Into<String>) -> Self {{"
    )
    .unwrap();
    writeln!(out, "        self.strike = Some(strike.into());").unwrap();
    writeln!(out, "        self").unwrap();
    writeln!(out, "    }}").unwrap();
    out.push('\n');
    writeln!(
        out,
        "    /// Set the option right (`\"call\"`, `\"put\"`, or `\"both\"`)."
    )
    .unwrap();
    writeln!(out, "    #[must_use]").unwrap();
    writeln!(
        out,
        "    pub fn right(mut self, right: impl Into<String>) -> Self {{"
    )
    .unwrap();
    writeln!(out, "        self.right = Some(right.into());").unwrap();
    writeln!(out, "        self").unwrap();
    writeln!(out, "    }}").unwrap();
    if has_interval {
        out.push('\n');
        writeln!(
            out,
            "    /// Set the sampling interval (`\"tick\"`, `\"1s\"`, `\"1m\"`, `\"5m\"`,"
        )
        .unwrap();
        writeln!(out, "    /// `\"1h\"`, ...).").unwrap();
        writeln!(out, "    #[must_use]").unwrap();
        writeln!(
            out,
            "    pub fn interval(mut self, interval: impl Into<String>) -> Self {{"
        )
        .unwrap();
        writeln!(out, "        self.interval = Some(interval.into());").unwrap();
        writeln!(out, "        self").unwrap();
        writeln!(out, "    }}").unwrap();
    }
    out.push('\n');
    writeln!(
        out,
        "    /// Override the end date (defaults to the same value as the"
    )
    .unwrap();
    writeln!(
        out,
        "    /// `date` parameter passed to `RestClient::{}`).",
        endpoint.name,
    )
    .unwrap();
    writeln!(out, "    /// Use for multi-day requests.").unwrap();
    writeln!(out, "    #[must_use]").unwrap();
    writeln!(
        out,
        "    pub fn end_date(mut self, end_date: impl Into<String>) -> Self {{"
    )
    .unwrap();
    writeln!(out, "        self.end_date = end_date.into();").unwrap();
    writeln!(out, "        self").unwrap();
    writeln!(out, "    }}").unwrap();
    out.push('\n');

    // execute()
    writeln!(
        out,
        "    /// Execute the REST request, decode the CSV body, and return the"
    )
    .unwrap();
    writeln!(out, "    /// resulting `Vec<{tick_ty}>`.").unwrap();
    writeln!(out, "    ///").unwrap();
    writeln!(out, "    /// # Errors").unwrap();
    writeln!(out, "    ///").unwrap();
    writeln!(
        out,
        "    /// Returns [`RestError`] on HTTP transport failure, non-2xx"
    )
    .unwrap();
    writeln!(out, "    /// status, or CSV decode failure.").unwrap();
    writeln!(
        out,
        "    pub async fn execute(self) -> Result<Vec<{tick_ty}>, RestError> {{"
    )
    .unwrap();
    writeln!(out, "        let body = fetch_csv(").unwrap();
    writeln!(out, "            self.client,").unwrap();
    writeln!(out, "            \"{rest_path}\",").unwrap();
    writeln!(out, "            &[").unwrap();
    writeln!(out, "                (\"symbol\", self.symbol.as_str()),").unwrap();
    writeln!(
        out,
        "                (\"expiration\", self.expiration.as_str()),"
    )
    .unwrap();
    writeln!(
        out,
        "                (\"start_date\", self.start_date.as_str()),"
    )
    .unwrap();
    writeln!(
        out,
        "                (\"end_date\", self.end_date.as_str()),"
    )
    .unwrap();
    writeln!(
        out,
        "                (\"strike\", self.strike.as_deref().unwrap_or(\"\")),"
    )
    .unwrap();
    writeln!(
        out,
        "                (\"right\", self.right.as_deref().unwrap_or(\"\")),"
    )
    .unwrap();
    if has_interval {
        writeln!(
            out,
            "                (\"interval\", self.interval.as_deref().unwrap_or(\"\")),"
        )
        .unwrap();
    }
    writeln!(out, "                (\"format\", \"csv\"),").unwrap();
    writeln!(out, "            ],").unwrap();
    writeln!(out, "        )").unwrap();
    writeln!(out, "        .await?;").unwrap();
    writeln!(out, "        {decoder}(&body)").unwrap();
    writeln!(out, "    }}").unwrap();
    writeln!(out, "}}").unwrap();

    out
}
