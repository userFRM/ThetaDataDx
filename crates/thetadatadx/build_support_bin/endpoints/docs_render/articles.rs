//! Generated Subscriptions article: the tier × endpoint capability
//! matrix, derived from the pinned upstream OpenAPI snapshot — the
//! same source the per-page tier badges use, so the two can never
//! disagree.

use std::fmt::Write as _;

use super::super::super::upstream_openapi::UpstreamOpenApi;
use super::super::model::GeneratedEndpoint;
use super::{endpoint_page_path, endpoint_tier, page};

const TIERS: [&str; 4] = ["free", "value", "standard", "professional"];
const TIER_HEADERS: [&str; 4] = ["Free", "Value", "Standard", "Pro"];

fn tier_row(tier: &str) -> String {
    let min = TIERS
        .iter()
        .position(|t| *t == tier)
        .unwrap_or_else(|| panic!("unknown tier {tier}"));
    TIERS
        .iter()
        .enumerate()
        .map(|(i, _)| if i >= min { " ✓ |" } else { "  |" })
        .collect()
}

/// Renders the Subscriptions article: per-category tables mapping each
/// endpoint to its minimum tier, plus the streaming-tier note.
pub(super) fn render_subscriptions_page(
    endpoints: &[&GeneratedEndpoint],
    upstream: &UpstreamOpenApi,
) -> String {
    let mut out = String::from(
        "---\ntitle: Subscriptions\ndescription: Minimum ThetaData subscription tier for every endpoint.\n---\n\n\
         <!-- @generated by `generate_docs_site` from scripts/ci/data/upstream_openapi.yaml. Do not edit by hand. -->\n\n\
         # Subscriptions\n\n\
         Each endpoint requires a minimum [ThetaData](https://www.thetadata.net/) subscription tier on the matching asset class. \
         The tables below map every endpoint to its tier; the same requirement appears as a badge on each reference page.\n\n\
         <TierBadge tier=\"free\" /> available on every tier\n\n\
         <TierBadge tier=\"standard\" /> requires Standard or Pro\n\n\
         For pricing, see [thetadata.net](https://www.thetadata.net/subscribe.html).\n",
    );

    let categories: [(&str, &str); 5] = [
        ("stock", "Stock"),
        ("option", "Option"),
        ("index", "Index"),
        ("calendar", "Calendar"),
        ("rate", "Interest Rate"),
    ];

    for (category, label) in categories {
        let in_category: Vec<&&GeneratedEndpoint> = endpoints
            .iter()
            .filter(|e| e.category == category)
            .collect();
        if in_category.is_empty() {
            continue;
        }
        let _ = write!(out, "\n## {label} Endpoints\n\n");
        let _ = writeln!(
            out,
            "| Endpoint | {} | {} | {} | {} |",
            TIER_HEADERS[0], TIER_HEADERS[1], TIER_HEADERS[2], TIER_HEADERS[3]
        );
        out.push_str("|---|:---:|:---:|:---:|:---:|\n");

        let flat = matches!(category, "calendar" | "rate");
        let mut last_group = String::new();
        for endpoint in &in_category {
            if !flat {
                let group = page::subcategory_label(&endpoint.subcategory);
                if group != last_group {
                    let _ = writeln!(out, "| **{group}** |  |  |  |  |");
                    last_group = group.to_string();
                }
            }
            let title = page::endpoint_title(endpoint);
            let link = endpoint_page_path(endpoint);
            let tier = endpoint_tier(endpoint, upstream);
            let _ = writeln!(out, "| [{title}](/{link}) |{}", tier_row(&tier));
        }
    }

    out.push_str(
        "\n## Streaming\n\n\
         Real-time streaming requires a **Standard** subscription or higher on the matching asset class. \
         Per-contract quote, trade, and open-interest streams are available on Standard and Pro; \
         full-stream subscriptions follow the same requirement, with contract limits depending on tier.\n\n\
         ::: tip\nFor current tier definitions, see the vendor's \
         [Subscriptions article](https://docs.thetadata.us/Articles/Getting-Started/Subscriptions.html).\n:::\n",
    );
    out
}
