//! Emit `sdks/typescript/src/_generated/fpss_event_classes.rs` — typed
//! `#[napi(object)]` structs + flat `StreamEvent` wrapper + dispatcher.

use std::fmt::Write as _;

use super::common::{snake_case, ts_rust_field_type};
use super::schema::{
    sorted_control_events, sorted_data_event_names, sorted_event_names, EventDef, Schema,
};

/// Emit the Contract napi struct. Same shape across every language —
/// `symbol` is always present (empty when not yet resolved), option fields
/// are `Option<T>` on the Rust side / `?: T` on the TS side.
///
/// Uses a raw string literal (`r#""#`) so the field indentation is
/// preserved verbatim. The previous backslash-newline continuation form
/// stripped every line's leading whitespace (Rust `\`-followed-by-space
/// elision), producing fields that were flush-left inside the generated
/// struct — not aligned with the rest of the file.
fn render_contract_napi() -> &'static str {
    r#"/// Streaming contract identifier. Surfaced on every decoded streaming data
/// event as `event.quote.contract` / `event.trade.contract` / etc.
/// `secType` is the symbolic uppercase name (`"STOCK"` / `"OPTION"` /
/// `"INDEX"` / `"RATE"`); `right` is `"C"` / `"P"` / `null`;
/// `strike` is the option strike in dollars — the same unit historical
/// rows carry under the same name, so streaming contracts join against
/// historical data directly. `expiration` is a `YYYYMMDD` integer.
#[must_use]
#[napi(object)]
#[derive(Clone)]
pub struct Contract {
    pub symbol: String,
    pub sec_type: String,
    pub expiration: Option<i32>,
    pub right: Option<String>,
    pub strike: Option<f64>,
    pub strike_thousandths: Option<i32>,
}

"#
}

/// Renders the TypeScript SDK source: a `#[napi(object)]` struct per event variant, the flat `StreamEvent` wrapper, and the `buffered_event_to_typed` dispatcher.
pub(super) fn render_ts_fpss_event_classes(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT — regenerated by build.rs from fpss_event_schema.toml\n",
    );
    out.push_str("// Typed #[napi(object)] structs for every `StreamData` and `StreamControl`\n");
    out.push_str("// variant + a flat `StreamEvent` wrapper and the `buffered_event_to_typed`\n");
    out.push_str("// dispatcher. Consumers discriminate on `event.kind` and pull the matching\n");
    out.push_str("// optional payload field; control variants surface their typed payloads on\n");
    out.push_str("// `event.loginSuccess` / `event.disconnected` / etc. so TypeScript users\n");
    out.push_str("// dispatch via the same `switch (event.kind)` they already use for ticks.\n");
    out.push_str("//\n");
    out.push_str("// `BigInt` is pulled in via `tick_classes.rs`'s `use` line — both\n");
    out.push_str("// files `include!` into the same TS-SDK module scope (see\n");
    out.push_str("// `sdks/typescript/src/lib.rs`), so importing it here would collide.\n\n");

    let data_names = sorted_data_event_names(schema);
    let control_events = sorted_control_events(schema);
    let names = sorted_event_names(schema);

    // Contract struct — every data variant's `contract` field is of this type.
    out.push_str(render_contract_napi());

    // One `#[napi(object)]` struct per data variant.
    for event_name in &data_names {
        let def = &schema.events[*event_name];
        out.push_str(&render_ts_event_class_struct(event_name, def));
        out.push('\n');
    }

    // One `#[napi(object)]` struct per control variant. Unit-style
    // variants (`MarketOpen`, `Reconnected`, ...) still get an
    // (empty) struct so the discriminated-union shape is uniform on
    // the TS side — every kind has a typed payload field on
    // `StreamEvent`.
    for (event_name, def) in &control_events {
        out.push_str(&render_ts_event_class_struct(event_name, def));
        out.push('\n');
    }

    // Removed the `FpssRawDataPayload` shim along with the
    // `StreamEvent::RawData` variant — decode-fallback frames are
    // filtered before reaching the SDK boundary, so the typescript
    // surface no longer needs a payload struct for them.

    // Build the TS literal union for the `kind` field. Emitted as a
    // `ts_type` override on `pub kind: &'static str` — see below. Using a
    // `ts_type` string avoids the napi-rs `string_enum` code path, which
    // lowered to `export declare const enum StreamEventKind { ... }` in
    // `index.d.ts`. `const enum`s are rejected by every TS toolchain with
    // `isolatedModules` turned on (Vite, esbuild, ts-jest, Next.js,
    // SWC-based loaders...), so downstream consumers could not even
    // import the discriminator. The `ts_return_type` override on
    // `nextEvent()` already narrows `event.kind` to a string-literal
    // union, so the enum was redundant anyway — we pin the same
    // narrowing directly on the struct field.
    let kind_ts_union = names
        .iter()
        .map(|n| format!("'{}'", snake_case(n)))
        .collect::<Vec<_>>()
        .join(" | ");

    // Flat wrapper struct with `kind` tag + optional payloads.
    out.push_str("/// A single streaming event surfaced to JS/TS.\n");
    out.push_str("///\n");
    out.push_str("/// `kind` is the discriminator — switch on it and read the matching\n");
    out.push_str("/// payload field. The shape is stable and every payload is typed, so\n");
    out.push_str("/// consumers never fall back to untyped `any`.\n");
    out.push_str(include_str!(
        "templates/typescript/fpss_event_attrs.rs.tmpl"
    ));
    writeln!(out, "    #[napi(ts_type = \"{kind_ts_union}\")]").unwrap();
    out.push_str("    pub kind: &'static str,\n");
    // One optional typed payload per data variant.
    for event_name in &data_names {
        let field = snake_case(event_name);
        writeln!(out, "    pub {field}: Option<{event_name}>,").unwrap();
    }
    // One optional typed payload per control variant. Field names
    // are the snake_case schema name (`login_success`,
    // `contract_assigned`, ...) which match the `kind` tag the
    // dispatcher writes for that variant.
    for (event_name, _) in &control_events {
        let field = snake_case(event_name);
        writeln!(out, "    pub {field}: Option<{event_name}>,").unwrap();
    }
    out.push_str("}\n\n");

    // Dispatcher. Every arm must assign `out.kind` exactly once. The
    // initial value is a harmless control variant (`unknown_control`)
    // every match arm overwrites — schema-declared so it cannot drift
    // from the `_ => BufferedEvent::UnknownControl` fallback in
    // `fpss_event_to_buffered`. Zero heap allocs on the hot per-event
    // path: each `kind` assignment is a `&'static str` literal.
    out.push_str("pub(crate) fn buffered_event_to_typed(event: BufferedEvent) -> StreamEvent {\n");
    out.push_str("    let mut out = StreamEvent {\n");
    out.push_str("        kind: \"unknown_control\",\n");
    for event_name in &data_names {
        let field = snake_case(event_name);
        writeln!(out, "        {field}: None,").unwrap();
    }
    for (event_name, _) in &control_events {
        let field = snake_case(event_name);
        writeln!(out, "        {field}: None,").unwrap();
    }
    out.push_str("    };\n");
    out.push_str("    match event {\n");
    for event_name in &data_names {
        let def = &schema.events[*event_name];
        out.push_str(&render_ts_match_arm(event_name, def));
    }
    for (event_name, def) in &control_events {
        out.push_str(&render_ts_match_arm(event_name, def));
    }
    out.push_str("    }\n");
    out.push_str("    out\n");
    out.push_str("}\n");

    out
}

/// Render one match arm for the dispatcher. Handles unit-style variants
/// (no field block) and fielded variants uniformly.
fn render_ts_match_arm(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    let field = snake_case(event_name);
    let kind_tag = snake_case(event_name);
    if def.columns.is_empty() {
        writeln!(out, "        BufferedEvent::{event_name} => {{").unwrap();
        writeln!(out, "            out.kind = \"{kind_tag}\";").unwrap();
        writeln!(out, "            out.{field} = Some({event_name} {{}});").unwrap();
        out.push_str("        }\n");
        return out;
    }
    writeln!(out, "        BufferedEvent::{event_name} {{").unwrap();
    for column in &def.columns {
        writeln!(out, "            {},", column.name).unwrap();
    }
    out.push_str("        } => {\n");
    writeln!(out, "            out.kind = \"{kind_tag}\";").unwrap();
    writeln!(out, "            out.{field} = Some({event_name} {{").unwrap();
    for column in &def.columns {
        // u64/i64 wire columns cross to JS as `bigint` to preserve
        // full precision. Nanosecond timestamps today already exceed
        // `Number.MAX_SAFE_INTEGER` by three orders of magnitude, so
        // `number` is not an option.
        match column.r#type.as_str() {
            "u64" | "i64" => writeln!(
                out,
                "                {name}: BigInt::from({name}),",
                name = column.name
            )
            .unwrap(),
            // `u8` widens to `u32` because napi-rs has no `u8` encoder.
            "u8" => writeln!(
                out,
                "                {name}: {name} as u32,",
                name = column.name
            )
            .unwrap(),
            // Contract is constructed explicitly — `symbol` converts to
            // owned String at the SDK boundary; the option fields transfer by value.
            "Contract" => writeln!(
                out,
                "                {name}: Contract {{\n                    symbol: {name}.symbol.to_string(),\n                    sec_type: {name}.sec_type.as_str().to_string(),\n                    expiration: {name}.expiration,\n                    right: {name}.right().map(|r| r.as_char().to_string()),\n                    strike: {name}.strike_dollars(),\n                    strike_thousandths: {name}.strike_thousandths,\n                }},",
                name = column.name
            )
            .unwrap(),
            _ => writeln!(out, "                {name},", name = column.name).unwrap(),
        }
    }
    // Populate the synthetic `reason_name` field on RemoveReason-bearing
    // control variants so the TS surface mirrors the Python pyclass
    // accessor.
    if matches!(
        event_name,
        "Disconnected" | "Reconnecting" | "ReconnectsExhausted"
    ) {
        out.push_str("                reason_name: thetadatadx::RemoveReason::from_code(reason as i16).as_str().to_string(),\n");
    }
    out.push_str("            });\n        }\n");
    out
}

fn render_ts_event_class_struct(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    let doc_text = if def.doc.is_empty() {
        format!("Streaming {event_name} event.")
    } else {
        def.doc.clone()
    };
    for line in doc_text.lines() {
        writeln!(out, "/// {line}").unwrap();
    }
    // `#[must_use]` — events surfaced from the FPSS stream; silently dropping
    // decoded events on the Rust side is a bug even though napi-rs is the
    // primary consumer.
    out.push_str("#[must_use]\n");
    out.push_str("#[napi(object)]\n");
    out.push_str("#[derive(Clone)]\n");
    if def.columns.is_empty() {
        // Unit-style control variants (`MarketOpen`, `Reconnected`, ...)
        // still get a named struct so the TS discriminated-union shape
        // is uniform — `event.marketOpen` is `MarketOpen | undefined`,
        // not `boolean | undefined`. napi-rs lowers the empty struct
        // to a TS `{}` interface.
        writeln!(out, "pub struct {event_name} {{}}").unwrap();
        return out;
    }
    writeln!(out, "pub struct {event_name} {{").unwrap();
    for column in &def.columns {
        let ty = ts_rust_field_type(&column.r#type, event_name, &column.name);
        writeln!(out, "    pub {}: {},", column.name, ty).unwrap();
    }
    // Symbolic name field for control variants whose `reason: i32`
    // column is the wire encoding of `thetadatadx::RemoveReason`.
    // TS users branch on the variant name (`event.disconnected.reasonName
    // === "TooManyRequests"`) instead of looking up the integer.
    if matches!(
        event_name,
        "Disconnected" | "Reconnecting" | "ReconnectsExhausted"
    ) {
        out.push_str("    /// Resolved disconnect-reason name (e.g. `\"TooManyRequests\"`,\n");
        out.push_str("    /// `\"InvalidCredentials\"`, `\"Unspecified\"` for unknown codes).\n");
        out.push_str("    /// Derived from the wire-level `reason` integer.\n");
        out.push_str("    pub reason_name: String,\n");
    }
    out.push_str("}\n");
    out
}
