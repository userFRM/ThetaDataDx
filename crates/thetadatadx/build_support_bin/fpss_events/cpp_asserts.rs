//! C++ layout-assert emitter for FPSS C mirror structs.
//!
//! Emits per-control-variant `static_assert(offsetof / sizeof)` coverage
//! for the typed C structs (`ThetaDataDxStreamLoginSuccess`, `ThetaDataDxStreamDisconnected`,
//! ...) alongside the data-variant + `ThetaDataDxStreamEvent` checks. A schema
//! entry's columns expand into one offset assert per physical field —
//! `Vec<u8>` columns split into `(<name>, <name>_len)` pairs.

use std::fmt::Write as _;

use super::layout::{
    c_struct_offsets, c_struct_size, contract_field_offsets, contract_size, fpss_event_offsets,
    fpss_event_size,
};
use super::schema::{sorted_control_events, sorted_data_events, EventDef, Schema};

/// Emit direct size + per-field offset asserts for the shared
/// `ThetaDataDxContract`. Every data event embeds this struct by value, so an
/// internal field permutation that preserved the 40-byte total (swapping the
/// `has_*` flags, say) would slip past the embedding events' offset asserts
/// yet silently misread `contract.right` / `.strike` / `.expiration` on every
/// tick. Pinning the contract's own size and each field offset closes that
/// gap the same way the tick mirrors are pinned. Numbers come from the
/// generator's layout walk, not hand-typed constants.
fn render_contract_asserts(out: &mut String) {
    writeln!(
        out,
        "static_assert(sizeof(ThetaDataDxContract) == {},\n              \"ThetaDataDxContract total size drifted\");",
        contract_size()
    )
    .unwrap();
    for (field_name, offset) in contract_field_offsets() {
        writeln!(
            out,
            "static_assert(offsetof(ThetaDataDxContract, {field_name}) == {offset},\n              \"ThetaDataDxContract::{field_name} offset drifted\");"
        )
        .unwrap();
    }
    out.push('\n');
}

fn render_event_asserts(out: &mut String, event_name: &str, def: &EventDef) {
    let alias = format!("ThetaDataDxStream{event_name}");
    if def.columns.is_empty() {
        // Empty control variants have a single `_padding` byte. Emit a
        // size-only assert; offsetof on `_padding == 0` is implicit.
        writeln!(
            out,
            "static_assert(offsetof({alias}, _padding) == 0,\n              \"{alias}::_padding offset drifted\");"
        )
        .unwrap();
        writeln!(
            out,
            "static_assert(sizeof({alias}) == 1,\n              \"{alias} total size drifted\");"
        )
        .unwrap();
    } else {
        for (field_name, offset) in c_struct_offsets(&def.columns) {
            writeln!(
                out,
                "static_assert(offsetof({alias}, {field_name}) == {offset},\n              \"{alias}::{field_name} offset drifted\");"
            )
            .unwrap();
        }
        writeln!(
            out,
            "static_assert(sizeof({alias}) == {},\n              \"{alias} total size drifted\");",
            c_struct_size(&def.columns)
        )
        .unwrap();
    }
    out.push('\n');
}

/// Renders the C++ `static_assert` block covering field offsets and sizes for every FPSS C mirror struct and the tagged `ThetaDataDxStreamEvent` wrapper.
pub(super) fn render_cpp_fpss_layout_asserts(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str(
        "/* @generated DO NOT EDIT — regenerated by build.rs from fpss_event_schema.toml */\n\n",
    );

    // Shared `ThetaDataDxContract` — pinned directly so an internal field
    // permutation can't hide behind the embedding events' offset asserts.
    render_contract_asserts(&mut out);

    // Per-variant struct asserts — data first, then control, mirroring
    // the Rust struct emission order in `ffi_rust.rs`.
    for (event_name, def) in sorted_data_events(schema) {
        render_event_asserts(&mut out, event_name, def);
    }
    for (event_name, def) in sorted_control_events(schema) {
        render_event_asserts(&mut out, event_name, def);
    }

    // Tagged-event wrapper.
    for (field_name, offset) in fpss_event_offsets(schema) {
        writeln!(
            out,
            "static_assert(offsetof(ThetaDataDxStreamEvent, {field_name}) == {offset},\n              \"ThetaDataDxStreamEvent::{field_name} offset drifted\");"
        )
        .unwrap();
    }
    writeln!(
        out,
        "static_assert(sizeof(ThetaDataDxStreamEvent) == {},\n              \"ThetaDataDxStreamEvent total size drifted\");",
        fpss_event_size(schema)
    )
    .unwrap();

    out
}
