//! Emit `sdks/python/src/_generated/fpss_event_classes.rs` â€” typed `#[pyclass]`
//! structs per Data variant + the `fpss_event_to_typed` dispatcher that
//! builds one directly from a borrowed `&fpss::StreamEvent`.
//!
//! The dispatcher converts the borrowed event in a single pass: every
//! owned String / byte buffer the pyclass needs is materialised exactly
//! once, and the contract is stored inline (an `Arc<str>` refcount bump,
//! not a heap copy) so the nested `ContractRef` Python object is built
//! only when a consumer reads `event.contract`.

use std::fmt::Write as _;

use super::common::{is_contract, python_rust_field_type, snake_case};
use super::schema::{
    sorted_control_events, sorted_data_events, sorted_event_names, ColumnDef, EventDef, Schema,
};

/// Emit the `ContractRef` pyclass + helper constructor. Every data event
/// exposes `event.contract` as a getter that materialises one of these
/// from the inline contract on access.
///
/// Named `ContractRef` (not `Contract`) on the Python side because the
/// fluent builder in `fluent.rs` already registers as `Contract` â€” see
/// the corresponding TypeScript split in
/// `sdks/typescript/src/fluent.rs` where the two are also held apart.
/// Sharing the bare name would have pyo3's `m.add_class` last-write-wins
/// silently shadow whichever helper registers first, hiding factory
/// methods like `.stock()` / `.option()` from end users.
fn render_contract_pyclass() -> &'static str {
    "/// FPSS contract identifier. Surfaced on every decoded FPSS data\n\
/// event as `event.contract`. Reads `symbol`, `sec_type` (symbolic\n\
/// string, e.g. `\"STOCK\"` / `\"OPTION\"`), `expiration` (`YYYYMMDD`),\n\
/// `right` (`\"C\"` / `\"P\"` / `None`), and `strike` (option strike in\n\
/// dollars) directly. `strike` carries dollars on every surface â€”\n\
/// user code reads the same notation it writes when calling\n\
/// `Contract.option(symbol, expiration=..., strike=\"5400\", right=\"C\")`,\n\
/// and values join directly against historical-row `strike` columns.\n\
///\n\
/// Distinct from the fluent `Contract` builder (in `fluent.rs`): this\n\
/// type is the read-only event payload; the fluent builder is what\n\
/// users instantiate via `Contract.stock(\"AAPL\")` / `Contract.option(...)`.\n\
#[must_use]\n\
#[pyclass(module = \"thetadatadx\", frozen, skip_from_py_object)]\n\
#[derive(Clone)]\n\
pub(crate) struct ContractRef {\n\
    #[pyo3(get)] pub symbol: String,\n\
    #[pyo3(get)] pub sec_type: String,\n\
    #[pyo3(get)] pub expiration: Option<i32>,\n\
    #[pyo3(get)] pub right: Option<String>,\n\
    #[pyo3(get)] pub strike: Option<f64>,\n\
    #[pyo3(get)] pub strike_thousandths: Option<i32>,\n\
}\n\
#[pymethods]\n\
impl ContractRef {\n\
    fn __repr__(&self) -> String {\n\
        format!(\n\
            \"ContractRef(symbol={:?}, sec_type={:?}, expiration={:?}, right={:?}, strike={:?}, strike_thousandths={:?})\",\n\
            self.symbol, self.sec_type, self.expiration, self.right, self.strike, self.strike_thousandths\n\
        )\n\
    }\n\
}\n\
impl ContractRef {\n\
    /// Build from the core `thetadatadx::fpss::protocol::Contract` value\n\
    /// carried by each data event. `sec_type` is the symbolic uppercase\n\
    /// name (`\"STOCK\"` / `\"OPTION\"` / `\"INDEX\"` / `\"RATE\"` /\n\
    /// `\"UNKNOWN\"`). `strike` is dollars; `strike_thousandths` is the\n\
    /// exact wire integer (a `$550.00` strike is `550000`).\n\
    pub(crate) fn from_core(c: &fpss::protocol::Contract) -> Self {\n\
        Self {\n\
            symbol: c.symbol.to_string(),\n\
            sec_type: c.sec_type.as_str().to_string(),\n\
            expiration: c.expiration,\n\
            right: c.right().map(|r| r.as_char().to_string()),\n\
            strike: c.strike_dollars(),\n\
            strike_thousandths: c.strike_thousandths,\n\
        }\n\
    }\n\
}\n\n"
}

/// Renders the Python SDK source: a typed `#[pyclass]` per event variant, the `fpss_event_to_typed` dispatcher, and the module registration helper.
pub(super) fn render_python_fpss_event_classes(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT â€” regenerated by build.rs from fpss_event_schema.toml\n",
    );
    out.push_str("// Typed #[pyclass] structs for every `StreamData` / `StreamControl`\n");
    out.push_str("// variant + `fpss_event_to_typed`, which builds one directly from a\n");
    out.push_str("// borrowed `&fpss::StreamEvent`. The single source of truth is\n");
    out.push_str("// `crates/thetadatadx/fpss_event_schema.toml`.\n\n");

    let names = sorted_event_names(schema);

    // Contract pyclass â€” materialised on demand by each data variant's
    // `contract` getter.
    out.push_str(render_contract_pyclass());

    // Structs.
    for event_name in &names {
        let def = &schema.events[*event_name];
        out.push_str(&render_python_event_class_struct(event_name, def));
        out.push('\n');
    }

    // Dispatcher: &StreamEvent -> typed pyclass for EVERY variant, in one
    // pass. No `BufferedEvent` intermediate â€” owned strings / byte
    // buffers are materialised exactly once, directly into the pyclass.
    out.push_str(&render_fpss_event_to_typed(schema));

    // Module registration helper.
    out.push_str(
        "pub(crate) fn register_fpss_event_classes(m: &Bound<'_, PyModule>) -> PyResult<()> {\n",
    );
    out.push_str("    m.add_class::<ContractRef>()?;\n");
    for event_name in &names {
        writeln!(out, "    m.add_class::<{}>()?;", event_name).unwrap();
    }
    out.push_str("    Ok(())\n");
    out.push_str("}\n");

    out
}

fn render_python_event_class_struct(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    let doc_text = if def.doc.is_empty() {
        format!("FPSS {event_name} event.")
    } else {
        def.doc.clone()
    };
    for line in doc_text.lines() {
        writeln!(out, "/// {line}").unwrap();
    }
    // `#[must_use]` â€” events surfaced from the FPSS stream; silently dropping
    // decoded events is a bug. `skip_from_py_object` opts out of the
    // deprecated auto-derived `FromPyObject` impl for pyclasses; these
    // event structs flow FROM Rust to Python only.
    //
    // No `#[derive(Clone)]`: events flow one-way from the Rust callback
    // to the Python consumer and are never cloned on the Rust side.
    out.push_str("#[must_use]\n");
    out.push_str("#[pyclass(module = \"thetadatadx\", frozen, skip_from_py_object)]\n");
    let has_contract = def.columns.iter().any(|c| is_contract(&c.r#type));
    if def.columns.is_empty() {
        // Unit-style control variant (`MarketOpen`, `Reconnected`, ...).
        // Empty struct is still a fully-fledged pyclass â€” `match
        // event: case MarketOpen():` works because pyo3 exposes
        // `MarketOpen()` as a callable Python type with the same
        // class-pattern semantics as a fielded class.
        writeln!(out, "pub(crate) struct {event_name};").unwrap();
    } else {
        writeln!(out, "pub(crate) struct {event_name} {{").unwrap();
        for column in &def.columns {
            if is_contract(&column.r#type) {
                // Contract is stored inline by value â€” an `Arc<str>`
                // symbol refcount bump plus POD fields, no heap copy.
                // It is NOT a `#[pyo3(get)]` field (pyo3 cannot expose a
                // non-pyclass struct by value); `event.contract` is the
                // getter below, which builds a `ContractRef` Python
                // object only when a consumer actually reads it.
                writeln!(out, "    pub {}: fpss::protocol::Contract,", column.name).unwrap();
            } else {
                let ty = python_rust_field_type(&column.r#type, event_name, &column.name);
                writeln!(out, "    #[pyo3(get)] pub {}: {},", column.name, ty).unwrap();
            }
        }
        out.push_str("}\n");
    }
    writeln!(out, "#[pymethods]").unwrap();
    writeln!(out, "impl {event_name} {{").unwrap();
    // `__repr__` surfaces live per-instance field values (mirroring the
    // tick-class renderer in `ticks/python_classes.rs::render_python_tick_class_struct`)
    // so pdb, Jupyter, pytest short-repr, and print()-based debugging
    // show actionable diagnostic data instead of an opaque
    // `"Ohlcvc(...)"` placeholder. Cap at six columns to stay one-line
    // readable; `Vec<u8>` payload columns (e.g. `UnknownFrame.payload`)
    // and `received_at_ns` are skipped because the first is unbounded
    // and the second is a timestamp that dwarfs the other fields
    // visually.
    let repr_fields = fpss_event_repr_fields(&def.columns, 6);
    if repr_fields.is_empty() {
        // Defensive fallback â€” every schema event has columns, but keep
        // the fallback so a future zero-column variant still compiles.
        writeln!(
            out,
            "    fn __repr__(&self) -> String {{ \"{event_name}()\".to_string() }}"
        )
        .unwrap();
    } else {
        let mut fmt_string = String::new();
        for (idx, field) in repr_fields.iter().enumerate() {
            if idx > 0 {
                fmt_string.push_str(", ");
            }
            fmt_string.push_str(field.name);
            fmt_string.push('=');
            // `{:?}` quotes strings and surfaces `None` / `Some(..)` on
            // Option fields (`Simple.detail`, `Simple.id`); `{}` for
            // numerics keeps the output tight.
            fmt_string.push_str(if field.use_debug { "{:?}" } else { "{}" });
        }
        let args = repr_fields
            .iter()
            .map(|f| format!("self.{}", f.name))
            .collect::<Vec<_>>()
            .join(", ");
        writeln!(out, "    fn __repr__(&self) -> String {{").unwrap();
        writeln!(
            out,
            "        format!(\"{event_name}({fmt_string})\", {args})"
        )
        .unwrap();
        out.push_str("    }\n");
    }
    // Contract getter â€” materialise the nested `ContractRef` Python
    // object on demand from the inline contract. Consumers that never
    // read `event.contract` (type-only routing, price/size books) pay
    // nothing for it on the per-event delivery path.
    if has_contract {
        let contract_field = def
            .columns
            .iter()
            .find(|c| is_contract(&c.r#type))
            .map(|c| c.name.as_str())
            .expect("has_contract implies a contract column");
        out.push('\n');
        out.push_str("    /// FPSS contract identity (`symbol`, `sec_type`,\n");
        out.push_str("    /// `expiration`, `right`, `strike`). Built on access from the\n");
        out.push_str("    /// event's inline contract.\n");
        out.push_str("    #[getter]\n");
        writeln!(
            out,
            "    fn contract(&self, py: Python<'_>) -> PyResult<Py<ContractRef>> {{"
        )
        .unwrap();
        writeln!(
            out,
            "        Py::new(py, ContractRef::from_core(&self.{contract_field}))"
        )
        .unwrap();
        out.push_str("    }\n");
    }
    out.push('\n');
    // Use the same lowercase/snake_case wire tag the existing dict-based
    // `next_event` emits ("quote", "trade", "open_interest", "ohlcvc") so
    // `next_event_typed` is a true drop-in â€” consumer `event.kind` checks
    // don't need to branch on which API they called.
    let kind_tag = snake_case(event_name);
    writeln!(out, "    #[getter]").unwrap();
    writeln!(
        out,
        "    fn kind(&self) -> &'static str {{ \"{kind_tag}\" }}"
    )
    .unwrap();
    // Symbolic name accessor for control variants whose `reason: i32`
    // column is the wire encoding of `thetadatadx::RemoveReason`.
    // Lets Python users branch on the variant name (`event.reason_name
    // == "TooManyRequests"`) instead of looking up the integer.
    if matches!(
        event_name,
        "Disconnected" | "Reconnecting" | "ReconnectsExhausted"
    ) {
        out.push_str("\n    /// Resolved disconnect-reason name (e.g. `\"TooManyRequests\"`,\n");
        out.push_str("    /// `\"InvalidCredentials\"`, `\"Unspecified\"` for unknown codes).\n");
        out.push_str("    /// Derived from the wire-level `reason` integer.\n");
        out.push_str("    #[getter]\n");
        out.push_str("    fn reason_name(&self) -> &'static str {\n");
        out.push_str("        thetadatadx::RemoveReason::from_code(self.reason as i16).as_str()\n");
        out.push_str("    }\n");
    }
    out.push_str("}\n");
    out
}

struct FpssReprField<'a> {
    name: &'a str,
    use_debug: bool,
}

/// Pick the first handful of FPSS event columns for `__repr__`
/// rendering. Mirrors the tick-class heuristic
/// (`ticks/python_classes.rs::repr_fields_for_tick`) but skips diagnostic noise:
///
/// * `Vec<u8>` payload columns (unbounded byte blobs â€” would balloon
///   the repr past a single screen and tank any REPL that auto-repr's
///   incoming events).
/// * `received_at_ns` â€” a nanosecond epoch timestamp that dwarfs every
///   other field visually and carries no per-event routing signal for
///   debugger consumption. Consumers that need it still read the
///   attribute directly.
fn fpss_event_repr_fields<'a>(
    columns: &'a [ColumnDef],
    max_fields: usize,
) -> Vec<FpssReprField<'a>> {
    let mut fields: Vec<FpssReprField<'a>> = Vec::new();
    for column in columns {
        if fields.len() >= max_fields {
            break;
        }
        // Skip noise fields (see doc comment above). `Vec<u8>` and
        // `received_at_ns` are skipped per module docs; `Contract`
        // because its own `__repr__` is nested and printing the full
        // struct inside the event's one-line repr quickly exceeds the
        // 6-field width budget.
        if column.r#type == "Vec<u8>"
            || column.name == "received_at_ns"
            || column.r#type == "Contract"
        {
            continue;
        }
        // `Option<..>` and `String` types render better with `{:?}`
        // (quoted strings, explicit `None` / `Some(..)`).
        let use_debug = matches!(
            column.r#type.as_str(),
            "String" | "Option<String>" | "Option<i32>"
        );
        fields.push(FpssReprField {
            name: &column.name,
            use_debug,
        });
    }
    fields
}

/// Emit `fpss_event_to_typed`, the single-pass converter from a borrowed
/// `&fpss::StreamEvent` to the typed pyclass. Mirrors the FFI converter
/// (`ffi_rust::render_ffi_fpss_event_converter`): match the source enum
/// directly and construct the destination in place, no intermediate.
fn render_fpss_event_to_typed(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str("/// Convert a borrowed FPSS event into its typed `#[pyclass]` in a\n");
    out.push_str("/// single pass. Owned strings / byte buffers are materialised\n");
    out.push_str("/// exactly once; the contract is stored inline so the nested\n");
    out.push_str("/// `ContractRef` Python object is built only on `event.contract`\n");
    out.push_str("/// access.\n");
    out.push_str("pub(crate) fn fpss_event_to_typed(\n");
    out.push_str("    py: Python<'_>,\n");
    out.push_str("    event: &fpss::StreamEvent,\n");
    out.push_str(") -> PyResult<Py<PyAny>> {\n");
    out.push_str("    match event {\n");

    out.push_str("        fpss::StreamEvent::Data(data) => match data {\n");
    for (event_name, def) in sorted_data_events(schema) {
        out.push_str(&render_data_arm(event_name, def));
    }
    // `StreamData` is `#[non_exhaustive]`; route unknown data variants
    // through `UnknownControl` (typed, payload-less) so downstream
    // consumers observe them without panicking.
    out.push_str("            _ => Py::new(py, UnknownControl).map(|p| p.into_any()),\n");
    out.push_str("        },\n");

    out.push_str("        fpss::StreamEvent::Control(ctrl) => match ctrl {\n");
    for (event_name, def) in sorted_control_events(schema) {
        if event_name == "UnknownControl" {
            // Emitted as the trailing wildcard arm below.
            continue;
        }
        out.push_str(&render_control_arm(event_name, def));
    }
    out.push_str("            _ => Py::new(py, UnknownControl).map(|p| p.into_any()),\n");
    out.push_str("        },\n");

    // `StreamEvent` itself is `#[non_exhaustive]`.
    out.push_str("        _ => Py::new(py, UnknownControl).map(|p| p.into_any()),\n");

    out.push_str("    }\n");
    out.push_str("}\n\n");
    out
}

/// Emit one `StreamEvent::Data(StreamData::<Variant> { .. }) => ...` arm that
/// builds the typed pyclass directly. The contract is moved inline by an
/// `Arc<str>` refcount bump (`(**contract).clone()`); owned strings /
/// byte buffers clone exactly once; `Copy` primitives deref.
fn render_data_arm(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    writeln!(out, "            fpss::StreamData::{event_name} {{").unwrap();
    for column in &def.columns {
        writeln!(out, "                {},", column.name).unwrap();
    }
    // `..` rest pattern: the core enum's struct-variants may carry
    // auxiliary fields (e.g. an internal decode cursor) the binding does
    // not surface.
    out.push_str("                ..\n");
    out.push_str("            } => Py::new(\n                py,\n");
    writeln!(out, "                {event_name} {{").unwrap();
    for column in &def.columns {
        let rhs = field_rhs(&column.r#type, &column.name);
        writeln!(out, "                    {}: {rhs},", column.name).unwrap();
    }
    out.push_str("                },\n            )\n            .map(|p| p.into_any()),\n");
    out
}

/// Emit one `StreamEvent::Control(StreamControl::<Variant> { .. }) => ...`
/// arm. The per-variant Rust pattern + field-construction mapping keeps
/// the translation from the core `StreamControl` enum's field shapes to the
/// schema's flat columns in one auditable table â€” mirroring the same
/// table in `buffered.rs` / `ffi_rust.rs`.
fn render_control_arm(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    let rust_variant = control_rust_variant(event_name);
    if def.columns.is_empty() {
        writeln!(
            out,
            "            fpss::StreamControl::{rust_variant} => Py::new(py, {event_name}).map(|p| p.into_any()),",
        )
        .unwrap();
        return out;
    }
    let has_contract = def.columns.iter().any(|c| is_contract(&c.r#type));
    let (rust_pattern, field_assigns) = control_variant_mapping(event_name);
    writeln!(
        out,
        "            fpss::StreamControl::{rust_variant} {{ {rust_pattern} }} => Py::new(",
    )
    .unwrap();
    out.push_str("                py,\n");
    writeln!(out, "                {event_name} {{").unwrap();
    for assign in field_assigns {
        writeln!(out, "                    {assign},").unwrap();
    }
    out.push_str("                },\n            )\n            .map(|p| p.into_any()),\n");
    // `has_contract` only affects how the assignment table is written;
    // it is encoded in `control_variant_mapping` already.
    let _ = has_contract;
    out
}

/// RHS expression for a data-variant column, given its schema type and
/// the pattern-bound local of the same name.
fn field_rhs(column_type: &str, name: &str) -> String {
    match column_type {
        // Owned non-`Copy` types clone through the deref of the pattern
        // binding (these materialise exactly once, here).
        "String" | "Vec<u8>" | "Option<String>" => format!("{name}.clone()"),
        // Contract arrives as `&Arc<fpss::protocol::Contract>`; store it
        // inline by value â€” an `Arc<str>` symbol refcount bump, no heap
        // copy of the symbol bytes.
        "Contract" => format!("(**{name}).clone()"),
        // `Copy` primitives (including `Option<i32>`) â€” deref the binding.
        _ => format!("*{name}"),
    }
}

/// Core `StreamControl` variant backing a schema control event. The two
/// names coincide for every variant except `ParseError`, whose core
/// enum variant keeps the historical `Error` spelling â€” the public
/// event class is named `ParseError` so no binding ships a class that
/// collides with the language's own error types.
fn control_rust_variant(event_name: &str) -> &str {
    match event_name {
        "ParseError" => "Error",
        other => other,
    }
}

/// Per-variant Rust-pattern + pyclass field-assignment mapping. Keeps the
/// translation from the core `StreamControl` enum's field shapes to the
/// schema's flat scalar columns in one auditable table.
///
/// Returned tuple: (Rust-side `match` pattern body inside `{ ... }`,
/// list of `field: rhs` assignments for the pyclass constructor).
fn control_variant_mapping(event_name: &str) -> (&'static str, Vec<String>) {
    match event_name {
        "LoginSuccess" => (
            "permissions",
            vec!["permissions: permissions.clone()".to_string()],
        ),
        "ContractAssigned" => (
            "id, contract",
            vec![
                "id: *id".to_string(),
                // Store the contract inline; the getter materialises
                // `ContractRef` on access.
                "contract: (**contract).clone()".to_string(),
            ],
        ),
        "ReqResponse" => (
            "req_id, result",
            vec![
                "req_id: *req_id".to_string(),
                "result: i32::from(*result as u8)".to_string(),
            ],
        ),
        "ServerError" => ("message", vec!["message: message.clone()".to_string()]),
        "Disconnected" => (
            "reason",
            vec!["reason: i32::from(*reason as i16)".to_string()],
        ),
        "Reconnecting" => (
            "reason, attempt, delay_ms",
            vec![
                // `RemoveReason` is `#[repr(i16)]`, so the discriminant
                // widens losslessly and totally into the wire `i32` â€” no
                // sentinel needed. `attempt: u32` can exceed `i32::MAX`, so
                // it saturates instead so the diagnostic value stays
                // non-negative in a (implausible but allowed) long-lived
                // reconnect loop.
                "reason: i32::from(*reason as i16)".to_string(),
                "attempt: i32::try_from(*attempt).unwrap_or(i32::MAX)".to_string(),
                "delay_ms: *delay_ms".to_string(),
            ],
        ),
        "ReconnectsExhausted" => (
            "reason, attempts",
            vec![
                "reason: i32::from(*reason as i16)".to_string(),
                // Same saturating shape as `Reconnecting.attempt`.
                "attempts: i32::try_from(*attempts).unwrap_or(i32::MAX)".to_string(),
            ],
        ),
        "ParseError" => ("message", vec!["message: message.clone()".to_string()]),
        "UnknownFrame" => (
            "code, payload",
            vec![
                "code: *code".to_string(),
                "payload: payload.clone()".to_string(),
            ],
        ),
        "Ping" => ("payload", vec!["payload: payload.clone()".to_string()]),
        other => panic!(
            "control variant '{other}' has no Rustâ†’pyclass mapping; \
             add it to control_variant_mapping in build_support/fpss_events/python.rs"
        ),
    }
}
