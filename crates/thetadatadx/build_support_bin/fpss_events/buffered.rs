//! `BufferedEvent` enum + `fpss_event_to_buffered` converter emitted
//! into the TypeScript SDK crate.
//!
//! The intermediate event shape between the FPSS dispatcher callback and
//! the napi typed dispatcher. The Python SDK converts the borrowed
//! `&StreamEvent` directly to its pyclass (see `python.rs`), so it carries
//! no buffered copy.

use std::fmt::Write as _;

use super::common::{is_option, rust_field_type};
use super::schema::{sorted_control_events, sorted_event_names, EventDef, Schema};

/// Emit the `BufferedEvent` enum + the `fpss_event_to_buffered` converter
/// in a form the TypeScript (`napi-rs`) SDK can `include!`.
///
/// Variants + field names come from `fpss_event_schema.toml` directly —
/// data variants mirror `StreamData::*`, control variants mirror
/// `StreamControl::*` one-for-one (no flat `Simple { event_type, detail,
/// id }` envelope). `StreamControl` itself is `#[non_exhaustive]`, so
/// the converter routes any unknown future variant through
/// `BufferedEvent::UnknownControl` (also schema-declared, so the
/// routing arm is generator-emitted, not hand-rolled). Decode-fallback
/// frames never reach this dispatcher — the public `StreamEvent` only
/// carries `Data` / `Control`.
pub(super) fn render_buffered_event_file(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str(
        "// @generated DO NOT EDIT — regenerated by build.rs from fpss_event_schema.toml\n",
    );
    out.push_str("// Intermediate flat event type that crosses the Rust mpsc channel\n");
    out.push_str("// between the FPSS callback and the napi typed dispatcher. Do not\n");
    out.push_str("// hand-edit; change `fpss_event_schema.toml` and regenerate.\n\n");

    // ── enum BufferedEvent ──
    out.push_str("#[derive(Clone, Debug)]\n");
    out.push_str("pub(crate) enum BufferedEvent {\n");
    let names = sorted_event_names(schema);
    for event_name in &names {
        let def = &schema.events[*event_name];
        if !def.doc.is_empty() {
            for line in def.doc.lines() {
                writeln!(out, "    /// {line}").unwrap();
            }
        }
        if def.columns.is_empty() {
            // Unit-style variant — no field block.
            writeln!(out, "    {event_name},").unwrap();
        } else {
            writeln!(out, "    {event_name} {{").unwrap();
            for column in &def.columns {
                let ty = rust_field_type(&column.r#type, event_name, &column.name);
                writeln!(out, "        {}: {},", column.name, ty).unwrap();
            }
            out.push_str("    },\n");
        }
    }
    out.push_str("}\n\n");

    // ── converter ──
    out.push_str(
        "pub(crate) fn fpss_event_to_buffered(event: &fpss::StreamEvent) -> BufferedEvent {\n",
    );
    out.push_str("    match event {\n");
    out.push_str("        fpss::StreamEvent::Data(data) => match data {\n");
    for event_name in &names {
        let def = &schema.events[*event_name];
        if def.kind != "data" {
            continue;
        }
        // Data variants mirror `StreamData` one-for-one by schema name.
        out.push_str(&render_data_match_arm(event_name, def));
    }
    // `StreamData` is `#[non_exhaustive]`; route unknown data variants
    // through `UnknownControl` so downstream consumers observe them
    // without panicking. (Strictly speaking these are unknown DATA
    // variants — but the typed `UnknownControl` carries no payload, so
    // it absorbs the case losslessly without inventing a separate
    // `UnknownData` schema entry.)
    out.push_str("            _ => BufferedEvent::UnknownControl,\n");
    out.push_str("        },\n");

    // Control variants → typed BufferedEvent arms.
    out.push_str(&render_control_match_arms(schema));

    // `StreamEvent` itself is `#[non_exhaustive]`; route through
    // `UnknownControl` (typed, payload-less). Decode-fallback frames
    // are filtered before reaching this dispatcher, so the public
    // `StreamEvent` only ever carries `Data` / `Control`.
    out.push_str("        _ => BufferedEvent::UnknownControl,\n");

    out.push_str("    }\n");
    out.push_str("}\n");
    out
}

fn render_data_match_arm(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    writeln!(out, "            fpss::StreamData::{event_name} {{").unwrap();
    for column in &def.columns {
        writeln!(out, "                {},", column.name).unwrap();
    }
    // `StreamData::*` variants are `#[non_exhaustive]` at the top-level
    // enum but each struct-variant has named fields. The `..` rest
    // pattern lets the core crate add auxiliary fields (e.g. an internal
    // decode cursor) without breaking the SDK conversion.
    out.push_str("                ..\n            } => BufferedEvent::");
    writeln!(out, "{event_name} {{").unwrap();
    for column in &def.columns {
        let rhs = match column.r#type.as_str() {
            "String" | "Vec<u8>" => format!("{field}.clone()", field = column.name),
            // Contract arrives as `&Arc<fpss::protocol::Contract>`;
            // the `BufferedEvent` field stores the contract by value
            // so the per-language dispatcher can construct a fresh
            // language-native object without holding the Arc.
            "Contract" => format!("(**{field}).clone()", field = column.name),
            t if is_option(t) => format!("{field}.clone()", field = column.name),
            _ => format!("*{field}", field = column.name),
        };
        writeln!(out, "                {}: {rhs},", column.name).unwrap();
    }
    out.push_str("            },\n");
    out
}

/// Emit one `fpss::StreamControl::*` match arm per schema control variant.
/// Each arm constructs the matching typed `BufferedEvent::*` value from
/// the Rust enum's bound fields. Variant-specific shape adjustments
/// (e.g. `RemoveReason` → `i32` discriminant cast, `Reconnecting.attempt`
/// `u32` → `i32` saturate, `StreamResponseType` → `i32`) live here so
/// the schema-driven side stays declarative.
///
/// Stable order: the `_ => BufferedEvent::UnknownControl` wildcard arm
/// is emitted last to absorb any future `StreamControl::*` variant the
/// core crate adds (`StreamControl` is `#[non_exhaustive]`).
fn render_control_match_arms(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str("        fpss::StreamEvent::Control(ctrl) => match ctrl {\n");
    for (event_name, def) in sorted_control_events(schema) {
        if event_name == "UnknownControl" {
            // `UnknownControl` is the schema-declared landing pad for
            // future `StreamControl::*` variants; emitted as the trailing
            // wildcard arm below, not as a named-variant match.
            continue;
        }
        out.push_str(&render_control_match_arm(event_name, def));
    }
    out.push_str("            _ => BufferedEvent::UnknownControl,\n");
    out.push_str("        },\n");
    out
}

/// Render a single `fpss::StreamControl::<Variant> { ... } => BufferedEvent::<Variant> { ... }`
/// arm. The bound-field list comes from a hand-coded mapping per
/// variant because the Rust enum's field shapes don't always line up
/// with the schema columns one-for-one (e.g. `Disconnected.reason` is
/// `RemoveReason` upstream / `i32` in the schema).
fn render_control_match_arm(event_name: &str, def: &EventDef) -> String {
    let mut out = String::new();
    let rust_variant = control_rust_variant(event_name);
    let (rust_pattern, field_assigns) = control_variant_mapping(event_name, def);
    if def.columns.is_empty() {
        writeln!(
            out,
            "            fpss::StreamControl::{rust_variant} => BufferedEvent::{event_name},",
        )
        .unwrap();
    } else {
        writeln!(
            out,
            "            fpss::StreamControl::{rust_variant} {{ {rust_pattern} }} => BufferedEvent::{event_name} {{",
        )
        .unwrap();
        for assign in field_assigns {
            writeln!(out, "                {assign},").unwrap();
        }
        out.push_str("            },\n");
    }
    out
}

/// Core `StreamControl` variant backing a schema control event. The two
/// names coincide for every variant except `ParseError`, whose core
/// enum variant keeps the historical `Error` spelling — the public
/// event class is named `ParseError` so no binding ships a class that
/// collides with the language's own error types.
fn control_rust_variant(event_name: &str) -> &str {
    match event_name {
        "ParseError" => "Error",
        other => other,
    }
}

/// Per-variant Rust-pattern + field-assignment mapping. Keeps the
/// translation from the core `StreamControl` enum's field shapes to the
/// schema's flat scalar columns in one auditable table.
///
/// Returned tuple: (Rust-side `match` pattern body inside `{ ... }`,
/// list of `field: rhs` assignments for the BufferedEvent constructor).
fn control_variant_mapping(event_name: &str, _def: &EventDef) -> (&'static str, Vec<String>) {
    match event_name {
        "LoginSuccess" => (
            "permissions",
            vec!["permissions: permissions.clone()".to_string()],
        ),
        "ContractAssigned" => (
            "id, contract",
            vec![
                "id: *id".to_string(),
                "contract: (**contract).clone()".to_string(),
            ],
        ),
        "ReqResponse" => (
            "req_id, result",
            vec![
                "req_id: *req_id".to_string(),
                "result: i32::from(*result as u8)".to_string(),
            ],
        ),
        "ServerError" => ("message", vec!["message: message.clone()".to_string()]),
        "Disconnected" => (
            "reason",
            vec!["reason: i32::from(*reason as i16)".to_string()],
        ),
        "Reconnecting" => (
            "reason, attempt, delay_ms",
            vec![
                // `RemoveReason` is `#[repr(i16)]`, so the discriminant
                // widens losslessly and totally into the wire `i32` — no
                // sentinel needed. `attempt: u32` can exceed `i32::MAX`, so
                // it saturates instead so the diagnostic value stays
                // non-negative in a (implausible but allowed) long-lived
                // reconnect loop.
                "reason: i32::from(*reason as i16)".to_string(),
                "attempt: i32::try_from(*attempt).unwrap_or(i32::MAX)".to_string(),
                "delay_ms: *delay_ms".to_string(),
            ],
        ),
        "ReconnectsExhausted" => (
            "reason, attempts",
            vec![
                "reason: i32::from(*reason as i16)".to_string(),
                // Same saturating shape as `Reconnecting.attempt`.
                "attempts: i32::try_from(*attempts).unwrap_or(i32::MAX)".to_string(),
            ],
        ),
        "ParseError" => ("message", vec!["message: message.clone()".to_string()]),
        "UnknownFrame" => (
            "code, payload",
            vec![
                "code: *code".to_string(),
                "payload: payload.clone()".to_string(),
            ],
        ),
        "Ping" => ("payload", vec!["payload: payload.clone()".to_string()]),
        // Unit variants: pattern body is empty, no fields. Returned
        // here for completeness, but the caller checks
        // `def.columns.is_empty()` and emits `=>` shorthand instead.
        "MarketOpen" | "MarketClose" | "Reconnected" | "Connected" | "ReconnectedServer"
        | "Restart" | "UnknownControl" => ("", vec![]),
        other => panic!(
            "control variant '{other}' has no Rust→BufferedEvent mapping; \
             add it to control_variant_mapping in build_support/fpss_events/buffered.rs"
        ),
    }
}
