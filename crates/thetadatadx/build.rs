use std::collections::HashMap;
use std::fmt::Write as _;
use std::path::Path;

use serde::Deserialize;

fn main() -> Result<(), Box<dyn std::error::Error>> {
    tonic_prost_build::configure()
        .build_server(false)
        .compile_protos(&["proto/external.proto"], &["proto"])?;

    generate_endpoint_registry()?;
    generate_mcp_bridge()?;
    generate_tick_types()?;
    Ok(())
}

// ─────────────────────────────────────────────────────────────────────────────
//  Code-generate tick structs + parsers from endpoint_schema.toml
// ─────────────────────────────────────────────────────────────────────────────

#[derive(Debug, Deserialize)]
struct Schema {
    types: HashMap<String, TickTypeDef>,
}

#[derive(Debug, Deserialize)]
struct TickTypeDef {
    #[serde(rename = "doc")]
    _doc: String,
    #[serde(rename = "copy")]
    _copy: bool,
    #[serde(default, rename = "align")]
    _align: Option<u32>,
    parser: String,
    #[serde(default)]
    required: Vec<String>,
    #[serde(default)]
    eod_style: bool,
    #[serde(default)]
    contract_id: bool,
    columns: Vec<ColumnDef>,
}

#[derive(Debug, Deserialize)]
struct ColumnDef {
    name: String,
    field: String,
    r#type: String,
}

fn generate_tick_types() -> Result<(), Box<dyn std::error::Error>> {
    let schema_path = "endpoint_schema.toml";
    let schema_str = std::fs::read_to_string(schema_path)?;
    let schema: Schema = toml::from_str(&schema_str)?;

    let out_dir = std::env::var("OUT_DIR")?;

    // Collect type names and sort for deterministic output.
    let mut type_names: Vec<&String> = schema.types.keys().collect();
    type_names.sort();

    // ── Generate decode_generated.rs (parser functions) ──────────────────────
    // Struct definitions now live in the `tdbe` crate (crates/tdbe/src/types/tick.rs).
    // Only parser functions are generated here.
    let mut parsers = String::new();
    parsers.push_str(
        "// Auto-generated by build.rs from endpoint_schema.toml \u{2014} do not edit manually.\n\n",
    );
    // Note: tick types are imported by decode.rs via `use tdbe::types::tick::*;`
    // No additional imports needed in the generated code.

    for type_name in &type_names {
        let def = &schema.types[*type_name];
        generate_parser(&mut parsers, type_name, def);
    }

    let parsers_dest = Path::new(&out_dir).join("decode_generated.rs");
    std::fs::write(&parsers_dest, &parsers)?;

    // Rerun if schema changes
    println!("cargo:rerun-if-changed={schema_path}");

    Ok(())
}

/// Generate a single parser function.
///
/// Price columns are decoded to `f64` during extraction using the wire
/// `price_type` (internally fetched, never exposed on the public struct).
// Reason: code generator -- the match dispatch over column types cannot be meaningfully split.
#[allow(clippy::too_many_lines)]
fn generate_parser(out: &mut String, type_name: &str, def: &TickTypeDef) {
    let fn_name = &def.parser;

    writeln!(
        out,
        "pub fn {fn_name}(table: &crate::proto::DataTable) -> Vec<{type_name}> {{"
    )
    .unwrap();

    // If eod_style, emit the local eod_num and eod_price helpers inline.
    if def.eod_style {
        out.push_str("    // EOD rows may have Price-typed cells or plain Number cells.\n");
        out.push_str("    fn eod_num(row: &crate::proto::DataValueList, idx: usize) -> i32 {\n");
        out.push_str("        row.values\n");
        out.push_str("            .get(idx)\n");
        out.push_str("            .and_then(|dv| dv.data_type.as_ref())\n");
        out.push_str("            .and_then(|dt| match dt {\n");
        out.push_str(
            "                crate::proto::data_value::DataType::Number(n) => Some(*n as i32),\n",
        );
        out.push_str(
            "                crate::proto::data_value::DataType::Price(p) => Some(p.value),\n",
        );
        out.push_str(
            "                crate::proto::data_value::DataType::Timestamp(ts) => Some(crate::decode::timestamp_to_date(ts.epoch_ms)),\n",
        );
        out.push_str("                _ => None,\n");
        out.push_str("            })\n");
        out.push_str("            .unwrap_or(0)\n");
        out.push_str("    }\n\n");

        out.push_str("    // EOD price field: decode to f64 from Price or Number cell.\n");
        out.push_str("    fn eod_price(row: &crate::proto::DataValueList, idx: usize) -> f64 {\n");
        out.push_str("        row.values\n");
        out.push_str("            .get(idx)\n");
        out.push_str("            .and_then(|dv| dv.data_type.as_ref())\n");
        out.push_str("            .map(|dt| match dt {\n");
        out.push_str("                crate::proto::data_value::DataType::Price(p) => {\n");
        out.push_str(
            "                    tdbe::types::price::Price::new(p.value, p.r#type).to_f64()\n",
        );
        out.push_str("                }\n");
        out.push_str(
            "                crate::proto::data_value::DataType::Number(n) => *n as f64,\n",
        );
        out.push_str("                _ => 0.0,\n");
        out.push_str("            })\n");
        out.push_str("            .unwrap_or(0.0)\n");
        out.push_str("    }\n\n");
    }

    out.push_str("    let h: Vec<&str> = table.headers.iter().map(|s| s.as_str()).collect();\n");

    // For eod_style, use the shared find_header() from decode.rs.
    if def.eod_style {
        out.push_str("    let find = |name: &str| find_header(&h, name);\n\n");
    }

    // Determine which columns need helpers.
    let needs_opt_number = def
        .columns
        .iter()
        .any(|c| c.r#type == "i32" && !def.required.contains(&c.name) && c.field != "date");
    let needs_opt_float = def
        .columns
        .iter()
        .any(|c| c.r#type == "f64" && !def.required.contains(&c.name));
    let needs_opt_i64 = def
        .columns
        .iter()
        .any(|c| c.r#type == "i64" && !def.required.contains(&c.name));

    // Emit opt_number / opt_float / opt_i64 helpers (non-eod only).
    if !def.eod_style {
        if needs_opt_number {
            out.push_str("    fn opt_number(row: &crate::proto::DataValueList, idx: Option<usize>) -> i32 {\n");
            out.push_str("        match idx {\n");
            out.push_str("            Some(i) => row_number(row, i),\n");
            out.push_str("            None => 0,\n");
            out.push_str("        }\n");
            out.push_str("    }\n\n");
        }
        if needs_opt_float {
            out.push_str("    fn opt_float(row: &crate::proto::DataValueList, idx: Option<usize>) -> f64 {\n");
            out.push_str("        match idx {\n");
            out.push_str("            Some(i) => row_float(row, i),\n");
            out.push_str("            None => 0.0,\n");
            out.push_str("        }\n");
            out.push_str("    }\n\n");
        }
        if needs_opt_i64 {
            out.push_str(
                "    fn opt_i64(row: &crate::proto::DataValueList, idx: Option<usize>) -> i64 {\n",
            );
            out.push_str("        match idx {\n");
            out.push_str("            Some(i) => row_number_i64(row, i),\n");
            out.push_str("            None => 0,\n");
            out.push_str("        }\n");
            out.push_str("    }\n\n");
        }
    }

    // Required header guards (non-eod only).
    if !def.eod_style {
        for req in &def.required {
            let var = idx_var_name(req);
            writeln!(out,
                "    let Some({var}) = find_header(&h, \"{req}\") else {{\n        return vec![];\n    }};"
            ).unwrap();
        }
        if !def.required.is_empty() {
            out.push('\n');
        }
    }

    // Declare index variables for all columns.
    for col in &def.columns {
        let var = idx_var_name(&col.name);
        if def.eod_style {
            writeln!(out, "    let {var} = find(\"{}\");", col.name).unwrap();
        } else if def.required.contains(&col.name) {
            // Already declared above as required.
        } else {
            writeln!(out, "    let {var} = find_header(&h, \"{}\");", col.name).unwrap();
        }
    }

    // Contract identification columns (wildcard queries inject expiration/strike/right).
    if def.contract_id {
        out.push_str("    let _cid_exp_idx = h.iter().position(|c| *c == \"expiration\");\n");
        out.push_str("    let _cid_strike_idx = h.iter().position(|c| *c == \"strike\");\n");
        out.push_str("    let _cid_right_idx = h.iter().position(|c| *c == \"right\");\n");
        out.push_str("    let _cid_strike_is_typed = _cid_strike_idx.is_some() && h.contains(&\"strike\");\n");
    }

    // Check if this is QuoteTick (needs midpoint computation).
    let is_quote_tick = type_name == "QuoteTick";

    out.push('\n');

    // Emit the row mapping.
    out.push_str("    table\n");
    out.push_str("        .data_table\n");
    out.push_str("        .iter()\n");
    out.push_str("        .map(|row| {\n");

    // Struct literal.
    if is_quote_tick {
        writeln!(out, "            let _tick = {type_name} {{").unwrap();
    } else {
        writeln!(out, "            {type_name} {{").unwrap();
    }

    for col in &def.columns {
        let var = idx_var_name(&col.name);
        let is_required = def.required.contains(&col.name);

        match col.r#type.as_str() {
            "i32" => {
                if def.eod_style {
                    writeln!(
                        out,
                        "                {}: {var}.map(|i| eod_num(row, i)).unwrap_or(0),",
                        col.field
                    )
                    .unwrap();
                } else if is_required {
                    let accessor = if col.field == "date" {
                        "row_date"
                    } else {
                        "row_number"
                    };
                    writeln!(
                        out,
                        "                {}: {accessor}(row, {var}),",
                        col.field
                    )
                    .unwrap();
                } else if col.field == "date" {
                    writeln!(
                        out,
                        "                {}: {var}.map(|i| row_date(row, i)).unwrap_or(0),",
                        col.field
                    )
                    .unwrap();
                } else {
                    writeln!(
                        out,
                        "                {}: opt_number(row, {var}),",
                        col.field
                    )
                    .unwrap();
                }
            }
            "i64" => {
                if is_required {
                    writeln!(
                        out,
                        "                {}: row_number_i64(row, {var}),",
                        col.field
                    )
                    .unwrap();
                } else {
                    writeln!(out, "                {}: opt_i64(row, {var}),", col.field).unwrap();
                }
            }
            "f64" => {
                if is_required {
                    writeln!(out, "                {}: row_float(row, {var}),", col.field).unwrap();
                } else {
                    writeln!(out, "                {}: opt_float(row, {var}),", col.field).unwrap();
                }
            }
            "String" => {
                if is_required {
                    writeln!(out, "                {}: row_text(row, {var}),", col.field).unwrap();
                } else {
                    writeln!(
                        out,
                        "                {}: {var}.map(|i| row_text(row, i)).unwrap_or_default(),",
                        col.field
                    )
                    .unwrap();
                }
            }
            "price" => {
                // Decode price to f64 at parse time.
                let field = &col.field;
                if is_required {
                    writeln!(out, "                {field}: row_price_f64(row, {var}),").unwrap();
                } else {
                    writeln!(out, "                {field}: match {var} {{").unwrap();
                    out.push_str("                    Some(i) => row_price_f64(row, i),\n");
                    out.push_str("                    None => 0.0,\n");
                    out.push_str("                },\n");
                }
            }
            "eod_num" => {
                writeln!(
                    out,
                    "                {}: {var}.map(|i| eod_num(row, i)).unwrap_or(0),",
                    col.field
                )
                .unwrap();
            }
            "eod_price" => {
                // EOD price field: decode to f64 from Price or Number cell.
                writeln!(
                    out,
                    "                {}: {var}.map(|i| eod_price(row, i)).unwrap_or(0.0),",
                    col.field
                )
                .unwrap();
            }
            other => panic!("unknown column type '{other}' in parser for {type_name}"),
        }
    }

    // Contract identification fields (injected when contract_id = true).
    if def.contract_id {
        out.push_str(
            "                expiration: _cid_exp_idx.map(|i| { let n = row_number(row, i); if n != 0 { n } else { let s = row_text(row, i); parse_iso_date(&s) } }).unwrap_or(0),\n",
        );
        // strike: decode to f64
        out.push_str("                strike: match _cid_strike_idx {\n");
        out.push_str(
            "                    Some(i) if _cid_strike_is_typed => row_price_f64(row, i),\n",
        );
        out.push_str("                    Some(i) => row_number(row, i) as f64,\n");
        out.push_str("                    None => 0.0,\n");
        out.push_str("                },\n");
        out.push_str("                right: _cid_right_idx.map(|i| {\n");
        out.push_str("                    let s = row_text(row, i);\n");
        out.push_str(
            "                    if s.is_empty() { 0i32 } else { s.as_bytes()[0] as i32 }\n",
        );
        out.push_str("                }).unwrap_or(0),\n");
    }

    if is_quote_tick {
        // QuoteTick gets midpoint computed from bid + ask.
        out.push_str("                midpoint: 0.0, // placeholder, set below\n");
        out.push_str("            };\n");
        out.push_str("            let mut _tick = _tick;\n");
        out.push_str("            _tick.midpoint = (_tick.bid + _tick.ask) / 2.0;\n");
        out.push_str("            _tick\n");
    } else {
        out.push_str("            }\n");
    }

    out.push_str("        })\n");
    out.push_str("        .collect()\n");
    out.push_str("}\n\n");
}

/// Convert a column name to a consistent index variable name.
fn idx_var_name(name: &str) -> String {
    format!("{name}_idx")
}

// ─────────────────────────────────────────────────────────────────────────────
//  Code-generate the ENDPOINTS static array from external.proto
// ─────────────────────────────────────────────────────────────────────────────

/// A parsed proto field.
#[derive(Debug, Clone)]
struct ProtoField {
    name: String,
    proto_type: String, // "string", "int32", "double", "bool", or "ContractSpec"
    is_optional: bool,
    is_repeated: bool,
}

/// A parsed RPC entry.
#[derive(Debug)]
struct Rpc {
    rpc_name: String,     // e.g. "GetStockHistoryEod"
    request_type: String, // e.g. "StockHistoryEodRequest"
}

#[derive(Debug, Clone)]
struct GeneratedParam {
    name: String,
    description: String,
    param_type: String,
    required: bool,
}

#[derive(Debug, Clone)]
struct GeneratedEndpoint {
    name: String,
    description: String,
    category: String,
    subcategory: String,
    params: Vec<GeneratedParam>,
    return_type: String,
}

#[derive(Debug, Clone)]
struct ParsedEndpoints {
    endpoints: Vec<GeneratedEndpoint>,
}

fn load_parsed_endpoints() -> Result<ParsedEndpoints, Box<dyn std::error::Error>> {
    let proto = std::fs::read_to_string("proto/external.proto")?;

    // ── Parse RPCs ──────────────────────────────────────────────────────────
    let rpc_re = regex::Regex::new(r"rpc\s+(Get\w+)\s*\((\w+)\)\s*returns")?;
    let rpcs: Vec<Rpc> = rpc_re
        .captures_iter(&proto)
        .map(|c| Rpc {
            rpc_name: c[1].to_string(),
            request_type: c[2].to_string(),
        })
        .collect();

    // ── Parse query messages ────────────────────────────────────────────────
    // Everything lives in one package, so ContractSpec is referenced
    // unqualified instead of `endpoints.ContractSpec`.
    let msg_re = regex::Regex::new(r"message\s+(\w+RequestQuery)\s*\{([^}]*)}")?;
    let field_re = regex::Regex::new(
        r"(optional\s+|repeated\s+)?(string|int32|double|bool|ContractSpec)\s+(\w+)\s*=\s*\d+",
    )?;

    let mut query_messages: HashMap<String, Vec<ProtoField>> = HashMap::new();
    for cap in msg_re.captures_iter(&proto) {
        let msg_name = cap[1].to_string();
        let body = &cap[2];
        let fields: Vec<ProtoField> = field_re
            .captures_iter(body)
            .map(|f| ProtoField {
                name: f[3].to_string(),
                proto_type: f[2].to_string(),
                is_optional: f.get(1).is_some_and(|m| m.as_str().starts_with("optional")),
                is_repeated: f.get(1).is_some_and(|m| m.as_str().starts_with("repeated")),
            })
            .collect();
        query_messages.insert(msg_name, fields);
    }

    let mut endpoints = Vec::new();

    for rpc in &rpcs {
        // Derive snake_case method name: GetStockHistoryEod → stock_history_eod
        let method = rpc_to_method(&rpc.rpc_name);

        // Find the query message: StockHistoryEodRequest → StockHistoryEodRequestQuery
        let query_msg_name = format!("{}Query", rpc.request_type);
        let fields = if let Some(f) = query_messages.get(&query_msg_name) {
            f.clone()
        } else {
            eprintln!(
                "warning: no query message '{}' found, skipping {}",
                query_msg_name, rpc.rpc_name
            );
            continue;
        };

        // Expand fields (contract_spec → symbol, expiration, strike, right)
        let params = expand_fields(&fields);

        let category = derive_category(&method);
        let subcategory = derive_subcategory(&method, &category);
        let return_type = derive_return_type(&method);
        let description = derive_description(&method);
        let mut params = params
            .into_iter()
            .map(|(name, description, param_type, required)| GeneratedParam {
                name,
                description,
                param_type,
                required,
            })
            .collect::<Vec<_>>();
        normalize_method_params(&method, &mut params);

        endpoints.push(GeneratedEndpoint {
            name: method,
            description,
            category,
            subcategory,
            params,
            return_type,
        });
    }

    // ── Manual extra: stock_history_ohlc_range ─────────────────────────────
    // Second SDK-level method on top of the same GetStockHistoryOhlc RPC.
    // The proto supports both shapes via the optional `date` vs
    // `start_date`/`end_date` fields; the SDK exposes them as two distinct
    // methods for nicer ergonomics. The registry parser only picks up one
    // method per RPC, so the range variant is appended manually here.
    endpoints.push(GeneratedEndpoint {
        name: "stock_history_ohlc_range".into(),
        description: "Fetch intraday OHLC bars across a date range.".into(),
        category: "stock".into(),
        subcategory: "history".into(),
        params: vec![
            GeneratedParam {
                name: "symbol".into(),
                description: "Ticker symbol (e.g. AAPL)".into(),
                param_type: "Symbol".into(),
                required: true,
            },
            GeneratedParam {
                name: "start_date".into(),
                description: "Start date YYYYMMDD".into(),
                param_type: "Date".into(),
                required: true,
            },
            GeneratedParam {
                name: "end_date".into(),
                description: "End date YYYYMMDD".into(),
                param_type: "Date".into(),
                required: true,
            },
            GeneratedParam {
                name: "interval".into(),
                description: "Accepts milliseconds (60000) or shorthand (1m). Presets: 100ms, 500ms, 1s, 5s, 10s, 15s, 30s, 1m, 5m, 10m, 15m, 30m, 1h.".into(),
                param_type: "Interval".into(),
                required: true,
            },
            GeneratedParam {
                name: "start_time".into(),
                description: "Start time filter (hh:mm:ss.SSS or ms)".into(),
                param_type: "Str".into(),
                required: false,
            },
            GeneratedParam {
                name: "end_time".into(),
                description: "End time filter (hh:mm:ss.SSS or ms)".into(),
                param_type: "Str".into(),
                required: false,
            },
            GeneratedParam {
                name: "venue".into(),
                description: "Venue/exchange filter".into(),
                param_type: "Str".into(),
                required: false,
            },
        ],
        return_type: "OhlcTicks".into(),
    });

    Ok(ParsedEndpoints { endpoints })
}

fn generate_endpoint_registry() -> Result<(), Box<dyn std::error::Error>> {
    let parsed = load_parsed_endpoints()?;

    // ── Generate Rust code ──────────────────────────────────────────────────
    let mut code = String::new();
    code.push_str("// Auto-generated by build.rs from external.proto — do not edit manually.\n\n");
    code.push_str("pub static ENDPOINTS: &[EndpointMeta] = &[\n");

    for endpoint in &parsed.endpoints {
        code.push_str("    EndpointMeta {\n");
        writeln!(code, "        name: \"{}\",", endpoint.name).unwrap();
        writeln!(code, "        description: \"{}\",", endpoint.description).unwrap();
        writeln!(code, "        category: \"{}\",", endpoint.category).unwrap();
        writeln!(code, "        subcategory: \"{}\",", endpoint.subcategory).unwrap();

        if endpoint.params.is_empty() {
            code.push_str("        params: &[],\n");
        } else {
            code.push_str("        params: &[\n");
            for param in &endpoint.params {
                code.push_str("            ParamMeta {\n");
                writeln!(code, "                name: \"{}\",", param.name).unwrap();
                writeln!(
                    code,
                    "                description: \"{}\",",
                    param.description
                )
                .unwrap();
                writeln!(
                    code,
                    "                param_type: ParamType::{},",
                    param.param_type
                )
                .unwrap();
                writeln!(code, "                required: {},", param.required).unwrap();
                code.push_str("            },\n");
            }
            code.push_str("        ],\n");
        }

        writeln!(
            code,
            "        returns: ReturnType::{},",
            endpoint.return_type
        )
        .unwrap();
        code.push_str("    },\n");
    }

    code.push_str("];\n");

    let out_dir = std::env::var("OUT_DIR")?;
    let dest = Path::new(&out_dir).join("registry_generated.rs");
    std::fs::write(&dest, &code)?;

    // Rerun if proto changes
    println!("cargo:rerun-if-changed=proto/external.proto");

    Ok(())
}

fn generate_mcp_bridge() -> Result<(), Box<dyn std::error::Error>> {
    let parsed = load_parsed_endpoints()?;

    let mut code = String::new();
    code.push_str("// Auto-generated by build.rs from external.proto — do not edit manually.\n\n");
    code.push_str(
        "/// Dispatch a validated MCP endpoint call into the generated SDK adapter set.\n",
    );
    code.push_str("///\n");
    code.push_str(
        "/// The build script emits one match arm per endpoint from the shared endpoint\n",
    );
    code.push_str(
        "/// metadata so the MCP server and registry stay aligned with the SDK surface.\n",
    );
    code.push_str("pub async fn invoke_generated_endpoint(\n");
    code.push_str("    client: &crate::ThetaDataDx,\n");
    code.push_str("    name: &str,\n");
    code.push_str("    args: &McpArgs,\n");
    code.push_str(") -> Result<McpOutput, McpError> {\n");
    code.push_str("    match name {\n");

    for endpoint in &parsed.endpoints {
        generate_mcp_dispatch_arm(&mut code, endpoint);
    }

    code.push_str("        _ => Err(McpError::UnknownEndpoint(name.to_string())),\n");
    code.push_str("    }\n");
    code.push_str("}\n");

    let out_dir = std::env::var("OUT_DIR")?;
    let dest = Path::new(&out_dir).join("mcp_generated.rs");
    std::fs::write(&dest, &code)?;

    println!("cargo:rerun-if-changed=proto/external.proto");

    Ok(())
}

fn generate_mcp_dispatch_arm(out: &mut String, endpoint: &GeneratedEndpoint) {
    writeln!(out, "        \"{}\" => {{", endpoint.name).unwrap();

    if is_simple_list_endpoint(&endpoint.name) {
        for param in &endpoint.params {
            emit_required_arg(out, endpoint, param);
        }
        let args = endpoint
            .params
            .iter()
            .map(call_arg_name)
            .collect::<Vec<_>>()
            .join(", ");
        writeln!(
            out,
            "            let values = client.{}({args}).await?;",
            endpoint.name
        )
        .unwrap();
        writeln!(
            out,
            "            Ok(McpOutput::{}(values))",
            endpoint.return_type
        )
        .unwrap();
        out.push_str("        }\n");
        return;
    }

    let method_call_params = endpoint
        .params
        .iter()
        .filter(|param| is_method_call_param(param))
        .collect::<Vec<_>>();
    let builder_params = endpoint
        .params
        .iter()
        .filter(|param| !is_method_call_param(param))
        .collect::<Vec<_>>();

    for param in &method_call_params {
        emit_required_arg(out, endpoint, param);
    }

    let call_args = method_call_params
        .into_iter()
        .map(call_arg_name)
        .collect::<Vec<_>>()
        .join(", ");

    if builder_params.is_empty() {
        writeln!(
            out,
            "            let result = client.{}({call_args}).await?;",
            endpoint.name
        )
        .unwrap();
    } else {
        writeln!(
            out,
            "            let mut builder = client.{}({call_args});",
            endpoint.name
        )
        .unwrap();

        for param in builder_params {
            let getter = optional_getter_name(&param.param_type);
            writeln!(
                out,
                "            if let Some(value) = args.{getter}(\"{}\")? {{",
                param.name
            )
            .unwrap();
            writeln!(
                out,
                "                builder = builder.{}(value);",
                param.name
            )
            .unwrap();
            out.push_str("            }\n");
        }

        out.push_str("            let result = builder.await?;\n");
    }
    writeln!(
        out,
        "            Ok(McpOutput::{}(result))",
        endpoint.return_type
    )
    .unwrap();
    out.push_str("        }\n");
}

fn emit_required_arg(out: &mut String, endpoint: &GeneratedEndpoint, param: &GeneratedParam) {
    if endpoint.name == "stock_history_ohlc" && param.name == "date" && !param.required {
        out.push_str("            let date = args.optional_date(\"date\")?.unwrap_or(\"\");\n");
        return;
    }

    if param.param_type == "Symbols" {
        writeln!(
            out,
            "            let symbol_values = args.required_symbols(\"{}\")?;",
            param.name
        )
        .unwrap();
        out.push_str(
            "            let symbol_refs: Vec<&str> = symbol_values.iter().map(String::as_str).collect();\n",
        );
        return;
    }

    let getter = required_getter_name(&param.param_type);
    writeln!(
        out,
        "            let {} = args.{getter}(\"{}\")?;",
        param.name, param.name
    )
    .unwrap();
}

fn call_arg_name(param: &GeneratedParam) -> String {
    if param.param_type == "Symbols" {
        "&symbol_refs".into()
    } else {
        param.name.clone()
    }
}

fn is_method_call_param(param: &GeneratedParam) -> bool {
    // The proto models many intraday history shapes as a union of
    // `date`-based and `start_date`/`end_date`-based queries, so `date`
    // often appears optional at the wire level even when the SDK method
    // exposes it as a positional constructor argument.
    param.required || param.name == "date"
}

fn normalize_method_params(method: &str, params: &mut [GeneratedParam]) {
    let supports_symbol_lists =
        method.starts_with("stock_snapshot_") || method.starts_with("index_snapshot_");

    if !supports_symbol_lists {
        for param in params.iter_mut() {
            if param.name == "symbol" && param.param_type == "Symbols" {
                param.param_type = "Symbol".into();
                param.description = "Ticker symbol (e.g. AAPL)".into();
            }
        }
    }
}

fn required_getter_name(param_type: &str) -> &'static str {
    match param_type {
        "Symbol" => "required_symbol",
        "Symbols" => "required_symbols",
        "Date" | "Expiration" => "required_date",
        "Interval" => "required_interval",
        "Right" => "required_right",
        "Int" => "required_int32",
        "Float" => "required_float64",
        "Bool" => "required_bool",
        "Year" => "required_year",
        _ => "required_str",
    }
}

fn optional_getter_name(param_type: &str) -> &'static str {
    match param_type {
        "Date" | "Expiration" => "optional_date",
        "Int" => "optional_int32",
        "Float" => "optional_float64",
        "Bool" => "optional_bool",
        _ => "optional_str",
    }
}

fn is_simple_list_endpoint(method: &str) -> bool {
    method.ends_with("_list_symbols")
        || method.ends_with("_list_dates")
        || method.ends_with("_list_expirations")
        || method.ends_with("_list_strikes")
}

/// Convert `GetStockHistoryEod` → `stock_history_eod`.
fn rpc_to_method(rpc_name: &str) -> String {
    // Strip leading "Get"
    let name = rpc_name.strip_prefix("Get").unwrap_or(rpc_name); // build script: panic is intentional
                                                                 // PascalCase → snake_case
    let mut result = String::new();
    for (i, ch) in name.chars().enumerate() {
        if ch.is_uppercase() {
            if i > 0 {
                result.push('_');
            }
            result.push(ch.to_lowercase().next().unwrap()); // build script: panic is intentional
        } else {
            result.push(ch);
        }
    }
    result
}

/// Expand proto fields, replacing `contract_spec` with (symbol, expiration, strike, right).
///
/// Many option query messages carry both a `ContractSpec` (contract identity,
/// expanded here to 4 fields) AND an explicit top-level `expiration` field
/// (the query range expiration — e.g. "include all contracts expiring by..."),
/// which would otherwise collide with the contract's own expiration. Any
/// post-expansion duplicate parameter name is dropped in favor of the first
/// occurrence (ContractSpec wins, since it is structurally the contract
/// identity the user really cares about).
fn expand_fields(fields: &[ProtoField]) -> Vec<(String, String, String, bool)> {
    let mut params: Vec<(String, String, String, bool)> = Vec::new();
    let mut seen: std::collections::HashSet<String> = std::collections::HashSet::new();

    let push = |params: &mut Vec<(String, String, String, bool)>,
                seen: &mut std::collections::HashSet<String>,
                entry: (String, String, String, bool)| {
        if seen.insert(entry.0.clone()) {
            params.push(entry);
        }
    };

    for f in fields {
        if f.proto_type == "ContractSpec" {
            // Expand to the 4 contract spec fields (symbol, expiration, strike, right).
            push(
                &mut params,
                &mut seen,
                (
                    "symbol".into(),
                    "Underlying symbol (e.g. AAPL)".into(),
                    "Symbol".into(),
                    true,
                ),
            );
            push(
                &mut params,
                &mut seen,
                (
                    "expiration".into(),
                    "Expiration date YYYYMMDD".into(),
                    "Expiration".into(),
                    true,
                ),
            );
            push(
                &mut params,
                &mut seen,
                (
                    "strike".into(),
                    "Strike price (raw integer)".into(),
                    "Strike".into(),
                    true,
                ),
            );
            push(
                &mut params,
                &mut seen,
                (
                    "right".into(),
                    "C for call, P for put".into(),
                    "Right".into(),
                    true,
                ),
            );
        } else {
            let (param_type, desc) = map_field(&f.name, &f.proto_type, f.is_repeated);
            let required = !f.is_optional;
            push(
                &mut params,
                &mut seen,
                (f.name.clone(), desc, param_type, required),
            );
        }
    }
    params
}

/// Map a proto field (name + type + repeated) to (`ParamType` variant name, description).
fn map_field(name: &str, proto_type: &str, is_repeated: bool) -> (String, String) {
    // Repeated string symbol → Symbols
    if is_repeated && name == "symbol" {
        return (
            "Symbols".into(),
            "Comma-separated ticker symbols (e.g. AAPL,MSFT)".into(),
        );
    }

    match (proto_type, name) {
        ("string", "symbol") => ("Symbol".into(), "Ticker symbol (e.g. AAPL)".into()),
        ("string", "start_date") => ("Date".into(), "Start date YYYYMMDD".into()),
        ("string", "end_date") => ("Date".into(), "End date YYYYMMDD".into()),
        ("string", "date") => ("Date".into(), "Date YYYYMMDD".into()),
        ("string", "interval") => (
            "Interval".into(),
            "Accepts milliseconds (60000) or shorthand (1m). Presets: 100ms, 500ms, 1s, 5s, 10s, 15s, 30s, 1m, 5m, 10m, 15m, 30m, 1h.".into(),
        ),
        ("string", "right") => ("Right".into(), "C for call, P for put".into()),
        ("string", "strike") => ("Strike".into(), "Strike price (raw integer)".into()),
        ("string", "expiration") => ("Expiration".into(), "Expiration date YYYYMMDD".into()),
        ("string", "request_type") => (
            "RequestType".into(),
            "Request type: EOD, TRADE, QUOTE, OHLC, etc.".into(),
        ),
        ("string", "year") => ("Year".into(), "4-digit year (e.g. 2024)".into()),
        ("string", "time_of_day") => (
            "Str".into(),
            "Milliseconds from midnight ET (e.g. 34200000 = 9:30 AM)".into(),
        ),
        ("string", "venue") => ("Str".into(), "Venue/exchange filter".into()),
        ("string", "min_time") => ("Str".into(), "Minimum time filter".into()),
        ("string", "start_time") => ("Str".into(), "Start time filter".into()),
        ("string", "end_time") => ("Str".into(), "End time filter".into()),
        ("string", "rate_type") => ("Str".into(), "Rate type".into()),
        ("string", "version") => ("Str".into(), "Greeks model version".into()),
        ("double", _) => ("Float".into(), humanize_name(name).clone()),
        ("int32", "max_dte") => ("Int".into(), "Maximum days to expiration".into()),
        ("int32", "strike_range") => ("Int".into(), "Strike range filter".into()),
        ("int32", _) => ("Int".into(), humanize_name(name).clone()),
        ("bool", "exclusive") => ("Bool".into(), "Exclusive time boundary".into()),
        ("bool", "use_market_value") => ("Bool".into(), "Use market value for Greeks".into()),
        ("bool", "underlyer_use_nbbo") => ("Bool".into(), "Use NBBO for underlyer price".into()),
        ("bool", _) => ("Bool".into(), humanize_name(name).clone()),
        _ => ("Str".into(), humanize_name(name).clone()),
    }
}

fn humanize_name(name: &str) -> String {
    name.replace('_', " ")
        .split_whitespace()
        .enumerate()
        .map(|(i, w)| {
            if i == 0 {
                let mut c = w.chars();
                match c.next() {
                    Some(first) => first.to_uppercase().to_string() + c.as_str(),
                    None => String::new(),
                }
            } else {
                w.to_string()
            }
        })
        .collect::<Vec<_>>()
        .join(" ")
}

fn derive_category(method: &str) -> String {
    if method.starts_with("stock_") {
        "stock".into()
    } else if method.starts_with("option_") {
        "option".into()
    } else if method.starts_with("index_") {
        "index".into()
    } else if method.starts_with("calendar_") {
        "calendar".into()
    } else if method.starts_with("interest_rate_") {
        "rate".into()
    } else {
        "other".into()
    }
}

fn derive_subcategory(method: &str, category: &str) -> String {
    // Strip the category prefix to get the rest
    let rest = match category {
        // build script: panic is intentional (unwrap_or provides safe fallback)
        "stock" => method.strip_prefix("stock_").unwrap_or(method),
        "option" => method.strip_prefix("option_").unwrap_or(method),
        "index" => method.strip_prefix("index_").unwrap_or(method),
        "calendar" => method.strip_prefix("calendar_").unwrap_or(method),
        "rate" => method.strip_prefix("interest_rate_").unwrap_or(method),
        _ => method,
    };

    if rest.starts_with("list_") {
        "list".into()
    } else if rest.starts_with("snapshot_greeks_") || rest.starts_with("snapshot_greeks") {
        "snapshot_greeks".into()
    } else if rest.starts_with("snapshot_") {
        "snapshot".into()
    } else if rest.starts_with("history_trade_greeks_") {
        "history_trade_greeks".into()
    } else if rest.starts_with("history_greeks_") {
        "history_greeks".into()
    } else if rest.starts_with("history_") {
        "history".into()
    } else if rest.starts_with("at_time_") {
        "at_time".into()
    } else if rest == "open_today" {
        "status".into()
    } else if rest == "on_date" || rest == "year" {
        "query".into()
    } else {
        "other".into()
    }
}

fn derive_return_type(method: &str) -> String {
    if is_simple_list_endpoint(method) {
        return "StringList".into();
    }

    if method == "option_list_contracts" {
        return "OptionContracts".into();
    }

    if method.starts_with("calendar_") {
        return "CalendarDays".into();
    }

    if method.starts_with("interest_rate_") {
        return "InterestRateTicks".into();
    }

    if method.contains("_trade_quote") {
        return "TradeQuoteTicks".into();
    }

    if method.contains("_open_interest") {
        return "OpenInterestTicks".into();
    }

    if method.contains("_market_value") {
        return "MarketValueTicks".into();
    }

    if method.contains("greeks_implied_volatility") {
        return "IvTicks".into();
    }

    if method.contains("_greeks_") {
        return "GreeksTicks".into();
    }

    if method == "index_snapshot_price"
        || method == "index_history_price"
        || method == "index_at_time_price"
    {
        return "PriceTicks".into();
    }

    if method.ends_with("_history_eod") {
        return "EodTicks".into();
    }

    if method.contains("_ohlc") {
        return "OhlcTicks".into();
    }

    if method.contains("_trade") || method.ends_with("_trade") {
        return "TradeTicks".into();
    }

    if method.contains("_quote") || method.ends_with("_quote") {
        return "QuoteTicks".into();
    }

    panic!("unhandled return type mapping for endpoint {method}");
}

