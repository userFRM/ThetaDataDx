//! C++ layout-assert emitter for the C mirror tick structs.

use std::fmt::Write as _;

use heck::ToSnakeCase;

use super::idents::c_field_ident;
use super::layout::{tick_ffi_offsets, tick_ffi_size_and_align};
use super::schema::Schema;
use super::sorted_type_names;

/// Renders `thetadatadx-cpp/include/tick_layout_asserts.hpp.inc` — `static_assert` size, alignment, and per-field offset checks pinning each C++ tick mirror to the Rust struct layout.
pub(super) fn render_cpp_tick_layout_asserts(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str(
        "/* @generated DO NOT EDIT — regenerated by build.rs from tick_schema.toml */\n\n",
    );
    // Drive every static_assert off the schema. `OptionContract` is
    // intentionally skipped: its FFI mirror carries a `const char* root`
    // pointer, so the C++ wrapper exposes a `std::string`-backed
    // hand-written struct (see `thetadatadx-cpp/include/thetadatadx.hpp::OptionContract`)
    // rather than the FFI-layout `ThetaDataDxOptionContract`. Adding any other
    // tick type to `tick_schema.toml` flows through here automatically.
    for type_name in sorted_type_names(schema) {
        if type_name == "OptionContract" {
            continue;
        }
        let def = &schema.types[type_name];
        let (size, align) = tick_ffi_size_and_align(type_name, def);
        // C++ wrapper exposes the schema type name verbatim under
        // `thetadatadx::cxx`; the C mirror is `ThetaDataDx<TypeName>`. The wrapper
        // alias is `using <TypeName> = ThetaDataDx<TypeName>;` so `sizeof(alias)`
        // and `offsetof(alias, field)` resolve through the C struct.
        let alias = type_name;
        let c_name = format!("ThetaDataDx{type_name}");
        writeln!(
            out,
            "static_assert(sizeof({alias}) == {size} && alignof({alias}) == {align},"
        )
        .unwrap();
        writeln!(out, "              \"{c_name} layout drifted from Rust\");").unwrap();
        // Per-field offset asserts. Same field set/order as the Rust
        // offset table (column tail + contract_id triple +
        // QuoteTick.midpoint). Field-offset drift can sneak past total-
        // size asserts when two same-size fields swap order.
        for (field, offset) in tick_ffi_offsets(type_name, def) {
            let field = c_field_ident(&field);
            writeln!(out, "static_assert(offsetof({alias}, {field}) == {offset},").unwrap();
            writeln!(
                out,
                "              \"{c_name}.{field} offset drifted from Rust\");"
            )
            .unwrap();
        }
    }
    out
}

/// Emit `thetadatadx-cpp/include/tick_arrow_ipc.hpp.inc` — the C++ free functions
/// that serialise a `std::vector<Tick>` history result to Arrow IPC bytes,
/// mirroring the `FlatFileRowList::to_arrow_ipc()` terminal. Each wraps the
/// matching `thetadatadx_<collection>_to_arrow_ipc` C ABI function; the vector's
/// element type is the layout-pinned C tick struct, so `data()` / `size()`
/// pass straight through with no copy. This gives the C++ history results
/// the columnar exit Python already exposes via `<TickName>List.to_arrow()`.
pub(super) fn render_cpp_tick_arrow_ipc(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str(
        "/* @generated DO NOT EDIT — regenerated by build.rs from tick_schema.toml */\n\n",
    );
    // RAII owner of a `ThetaDataDxColumnPresence` (the decode's wire-column set).
    // Move-only: it owns the heap names the C ABI leaked and frees them in the
    // destructor with `thetadatadx_column_presence_free`, so a caller never
    // hand-manages the carrier. Produced by the per-tick `_present_columns`
    // wrappers, consumed by the `_to_arrow_ipc_projected` wrappers.
    out.push_str("/// Owns the wire-column presence set a decode produced (which columns a\n");
    out.push_str("/// response actually carried). Move-only RAII over the C ABI carrier;\n");
    out.push_str("/// frees the names on destruction. Build one with a `<tick>_present_columns`\n");
    out.push_str(
        "/// free function and pass it to the matching `<tick>_to_arrow_ipc_projected`.\n",
    );
    out.push_str("class ColumnPresence {\n");
    out.push_str("public:\n");
    out.push_str(
        "    explicit ColumnPresence(ThetaDataDxColumnPresence raw) noexcept : raw_(raw) {}\n",
    );
    out.push_str("    ColumnPresence(const ColumnPresence&) = delete;\n");
    out.push_str("    ColumnPresence& operator=(const ColumnPresence&) = delete;\n");
    out.push_str("    ColumnPresence(ColumnPresence&& other) noexcept : raw_(other.raw_) {\n");
    out.push_str("        other.raw_.names = nullptr;\n");
    out.push_str("        other.raw_.len = 0;\n");
    out.push_str("    }\n");
    out.push_str("    ColumnPresence& operator=(ColumnPresence&& other) noexcept {\n");
    out.push_str("        if (this != &other) {\n");
    out.push_str("            thetadatadx_column_presence_free(raw_);\n");
    out.push_str("            raw_ = other.raw_;\n");
    out.push_str("            other.raw_.names = nullptr;\n");
    out.push_str("            other.raw_.len = 0;\n");
    out.push_str("        }\n");
    out.push_str("        return *this;\n");
    out.push_str("    }\n");
    out.push_str("    ~ColumnPresence() { thetadatadx_column_presence_free(raw_); }\n");
    out.push_str("    /// Number of present columns.\n");
    out.push_str("    size_t size() const noexcept { return raw_.len; }\n");
    out.push_str("    /// Present schema-column names, in schema order.\n");
    out.push_str("    std::vector<std::string> names() const {\n");
    out.push_str("        std::vector<std::string> out;\n");
    out.push_str("        out.reserve(raw_.len);\n");
    out.push_str(
        "        for (size_t i = 0; i < raw_.len; ++i) { out.emplace_back(raw_.names[i]); }\n",
    );
    out.push_str("        return out;\n");
    out.push_str("    }\n");
    out.push_str("    /// The borrowed C carrier, for the projected serialiser. Not owned by\n");
    out.push_str("    /// the callee — this object retains ownership and frees it.\n");
    out.push_str("    const ThetaDataDxColumnPresence& raw() const noexcept { return raw_; }\n");
    out.push_str("private:\n");
    out.push_str("    ThetaDataDxColumnPresence raw_;\n");
    out.push_str("};\n\n");
    // One shared template carries the serialise body; each per-type terminal
    // below is a thin forwarder that binds the row type and its C ABI
    // function. Tests call the per-type names with `{}` (a non-deduced
    // context), so those must stay concrete `inline` functions, not
    // templates.
    out.push_str("namespace detail {\n");
    out.push_str("/// Serialise a typed history-row vector as Arrow IPC stream bytes by\n");
    out.push_str("/// forwarding to its C ABI serialiser. `data == nullptr` is the FFI\n");
    out.push_str("/// error sentinel; an empty input still yields a valid schema-only\n");
    out.push_str("/// stream (non-null), so null means a real failure to surface.\n");
    out.push_str("template <typename Tick, typename CFn>\n");
    out.push_str(
        "inline std::vector<uint8_t> tick_to_arrow_ipc(const std::vector<Tick>& rows, CFn c_fn) {\n",
    );
    out.push_str("    ThetaDataDxArrowBytes raw = c_fn(rows.data(), rows.size());\n");
    out.push_str("    if (raw.data == nullptr) {\n");
    out.push_str("        throw_last_ffi_error();\n");
    out.push_str("    }\n");
    out.push_str("    std::vector<uint8_t> out(raw.data, raw.data + raw.len);\n");
    out.push_str("    thetadatadx_arrow_bytes_free(raw);\n");
    out.push_str("    return out;\n");
    out.push_str("}\n");
    out.push_str(
        "/// Serialise a typed history-row vector as a PROJECTED Arrow IPC stream —\n\
         /// only the columns named in `presence` — by forwarding to its `_projected`\n\
         /// C ABI serialiser. `symbol` is the response's constant root, broadcast as\n\
         /// the leading column; `nullptr` omits it (an empty string stays a valid\n\
         /// value). Same error / empty-stream contract as `tick_to_arrow_ipc`.\n",
    );
    out.push_str("template <typename Tick, typename CFn>\n");
    out.push_str(
        "inline std::vector<uint8_t> tick_to_arrow_ipc_projected(const std::vector<Tick>& rows, const ColumnPresence& presence, const char* symbol, CFn c_fn) {\n",
    );
    out.push_str(
        "    ThetaDataDxArrowBytes raw = c_fn(rows.data(), rows.size(), presence.raw(), symbol);\n",
    );
    out.push_str("    if (raw.data == nullptr) {\n");
    out.push_str("        throw_last_ffi_error();\n");
    out.push_str("    }\n");
    out.push_str("    std::vector<uint8_t> out(raw.data, raw.data + raw.len);\n");
    out.push_str("    thetadatadx_arrow_bytes_free(raw);\n");
    out.push_str("    return out;\n");
    out.push_str("}\n");
    out.push_str("} // namespace detail\n\n");

    for type_name in sorted_type_names(schema) {
        // OptionContract carries a heap string and is not a columnar tick
        // row; it has no Arrow IPC terminal (matching the FFI surface).
        if type_name == "OptionContract" {
            continue;
        }
        let def = &schema.types[type_name];
        let collection = &def.render.collection;
        let snake = collection.to_snake_case();
        let c_fn = format!("thetadatadx_{snake}_to_arrow_ipc");
        writeln!(
            out,
            "/// Serialise a `{type_name}` history result as Arrow IPC stream bytes,"
        )
        .unwrap();
        out.push_str(
            "/// the same columnar exit the Python `<TickName>List.to_arrow()` terminal\n",
        );
        out.push_str(
            "/// gives. Throws `thetadatadx::Error` on serialisation failure. The returned\n",
        );
        out.push_str("/// vector owns its memory; the FFI buffer is freed before return.\n");
        writeln!(
            out,
            "inline std::vector<uint8_t> {snake}_to_arrow_ipc(const std::vector<{type_name}>& rows) {{"
        )
        .unwrap();
        writeln!(out, "    return detail::tick_to_arrow_ipc(rows, {c_fn});").unwrap();
        out.push_str("}\n\n");

        // Decode-fed pair: build the wire-column presence from a response's
        // header names, then serialise a PROJECTED Arrow stream carrying only
        // those columns — the `<TickName>List.to_arrow()` behaviour on a
        // decode result, as opposed to the all-present terminal above.
        writeln!(
            out,
            "/// Build the wire-column presence set for a `{type_name}` response from its"
        )
        .unwrap();
        out.push_str(
            "/// header names (the response's `DataTable` headers), via the same decode\n\
             /// logic the buffered `.await` path uses. Feed the result to\n\
             /// `{snake}_to_arrow_ipc_projected` for a terminal-exact columnar export.\n"
                .replace("{snake}", &snake)
                .as_str(),
        );
        writeln!(
            out,
            "inline ColumnPresence {snake}_present_columns(const std::vector<std::string>& headers) {{"
        )
        .unwrap();
        out.push_str("    std::vector<const char*> ptrs;\n");
        out.push_str("    ptrs.reserve(headers.size());\n");
        out.push_str("    for (const auto& h : headers) { ptrs.push_back(h.c_str()); }\n");
        // An empty presence is a valid result (a response with no columns),
        // so the C side reports failure through the errno-style last-error
        // slot rather than the return value. Clear it, then throw if set.
        out.push_str("    thetadatadx_clear_error();\n");
        writeln!(
            out,
            "    ColumnPresence presence(thetadatadx_{snake}_present_columns(ptrs.data(), ptrs.size()));"
        )
        .unwrap();
        out.push_str("    if (thetadatadx_last_error() != nullptr) {\n");
        out.push_str("        detail::throw_last_ffi_error();\n");
        out.push_str("    }\n");
        out.push_str("    return presence;\n");
        out.push_str("}\n\n");

        writeln!(
            out,
            "/// Serialise a `{type_name}` history result as a PROJECTED Arrow IPC stream —"
        )
        .unwrap();
        out.push_str(
            "/// only the columns `presence` names (from the matching\n\
             /// `_present_columns`), the terminal-exact output Python's\n\
             /// `<TickName>List.to_arrow()` gives on a decode result. `symbol` is the\n\
             /// response's constant root, broadcast as the leading column\n\
             /// (option/index carry it, stock does not; `nullptr` omits it). Throws\n\
             /// `thetadatadx::Error` on failure; the returned vector owns its memory.\n",
        );
        writeln!(
            out,
            "inline std::vector<uint8_t> {snake}_to_arrow_ipc_projected(const std::vector<{type_name}>& rows, const ColumnPresence& presence, const char* symbol = nullptr) {{"
        )
        .unwrap();
        writeln!(
            out,
            "    return detail::tick_to_arrow_ipc_projected(rows, presence, symbol, thetadatadx_{snake}_to_arrow_ipc_projected);"
        )
        .unwrap();
        out.push_str("}\n\n");
    }
    out
}

/// Emit `thetadatadx-cpp/include/tick_flag_accessors.hpp.inc` — the C++ free
/// functions that decode each tick type's boolean flag-word accessors
/// (e.g. `thetadatadx::is_cancelled(const TradeTick&)`). C++ tick rows are C
/// `#[repr(C)]` struct aliases with no member methods, so the decoded
/// booleans are free functions in the `thetadatadx::` namespace, mirroring the
/// existing `thetadatadx::strike(const ThetaDataDxContract&)` accessor. The predicate
/// matches the Rust core and the Python / TypeScript projections from
/// the same schema rows.
pub(super) fn render_cpp_tick_flag_accessors(schema: &Schema) -> String {
    let mut out = String::new();
    out.push_str(
        "/* @generated DO NOT EDIT — regenerated by build.rs from tick_schema.toml */\n\n",
    );
    for type_name in sorted_type_names(schema) {
        let def = &schema.types[type_name];
        if def.flag_accessors.is_empty() {
            continue;
        }
        for flag in &def.flag_accessors {
            writeln!(out, "/// {}", flag.doc).unwrap();
            writeln!(
                out,
                "inline bool {}(const {type_name}& c) noexcept {{",
                flag.name
            )
            .unwrap();
            writeln!(out, "    return {};", flag.cpp_predicate("c")).unwrap();
            out.push_str("}\n\n");
        }
    }
    out
}
