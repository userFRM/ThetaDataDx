//! Emit `decode_generated.rs` (two-phase bulk-extraction tick parsers).
//!
//! Struct definitions live in the internal data-format module
//! (`thetadatadx-rs/src/tdbe/types/tick.rs`); only parser functions
//! are generated here. Parsers return
//! `Result<Vec<Tick>, DecodeError>` and decode in two phases:
//!
//! 1. Resolve the table's column layout once against the schema —
//!    `find_header` lookups plus the required-header guards.
//! 2. Bulk-extract each resolved column through
//!    `crate::decode::column::extract_column`, monomorphized over the
//!    column's schema type, filling the pre-seeded tick vector
//!    column-by-column in `BLOCK_ROWS`-row blocks (see
//!    `decode/column.rs` for the cache rationale).
//!
//! Any cell failing its column's accept-set fails the whole table with
//! a typed `DecodeError`, matching upstream wire-protocol semantics;
//! the bulk path names the schema column and row in the diagnostic.

use std::fmt::Write as _;
use std::path::Path;

use super::schema::{load_schema, ColumnDef, TickTypeDef};

/// Loads the tick schema and writes `decode_generated.rs` with one two-phase
/// parser function per tick type, in deterministic name order.
pub(super) fn generate() -> Result<(), Box<dyn std::error::Error>> {
    let schema = load_schema()?;
    let schema_path = "tick_schema.toml";

    let out_dir = std::env::var("OUT_DIR")?;

    // Collect type names and sort for deterministic output.
    let mut type_names: Vec<&String> = schema.types.keys().collect();
    type_names.sort();

    // ── Generate decode_generated.rs (parser functions) ──────────────────────
    let mut parsers = String::new();
    parsers
        .push_str("// @generated DO NOT EDIT — regenerated by build.rs from tick_schema.toml\n\n");
    // Note: tick types are imported by cell.rs via
    // `use crate::tdbe::types::tick::{...}`, and the generated module pulls
    // them in through `use super::*;`. No additional imports needed in the
    // generated code.

    for type_name in &type_names {
        let def = &schema.types[*type_name];
        generate_parser(&mut parsers, type_name, def);
        generate_present_columns(&mut parsers, type_name, def);
        generate_chain_sort_key(&mut parsers, type_name, def);
    }

    let parsers_dest = Path::new(&out_dir).join("decode_generated.rs");
    std::fs::write(&parsers_dest, &parsers)?;

    // Rerun if schema changes
    println!("cargo:rerun-if-changed={schema_path}");

    Ok(())
}

/// The single-cell decoder (`decode::cell`) backing a schema column
/// type, and the seed literal its absent-column default fills with.
///
/// Schema column types map onto four decode families: plain numerics
/// (`row_number` / `row_number_i64` with the `date`-field variant
/// `row_date`), price-or-number floats (`row_price_f64` — also the
/// `price`, `eod_price`, and contract-`strike` decode), text
/// (`row_text`), and the EOD wildcard-report shapes that additionally
/// accept `Price` cells (`row_eod_*`).
fn column_decoder(def: &TickTypeDef, col: &ColumnDef) -> (&'static str, &'static str) {
    match col.r#type.as_str() {
        "i32" => {
            if def.eod_style {
                ("row_eod_number", "0")
            } else if col.field == "date" {
                ("row_date", "0")
            } else {
                ("row_number", "0")
            }
        }
        "i64" => ("row_number_i64", "0"),
        "f64" | "price" => ("row_price_f64", "0.0"),
        "String" => ("row_text", "String::new()"),
        // Logical char: `'\0'` is the absent fill (no contract right).
        "right" => ("row_contract_right", "'\\0'"),
        // Conservative absent fill: a (never observed) type-less
        // calendar row reads as a closed day rather than an open one.
        "calendar_status" => (
            "row_calendar_status",
            "crate::tdbe::CalendarStatus::FullClose",
        ),
        "eod_num" => ("row_eod_number", "0"),
        "eod_num64" => ("row_eod_number_i64", "0"),
        "eod_date" => ("row_eod_date", "0"),
        "eod_price" => ("row_price_f64", "0.0"),
        other => panic!("unknown column type '{other}' in tick schema"),
    }
}

/// Generate a single two-phase parser function.
///
/// Price columns are decoded to `f64` during extraction using the wire
/// `price_type` (internally fetched, never exposed on the public struct).
fn generate_parser(out: &mut String, type_name: &str, def: &TickTypeDef) {
    let fn_name = &def.parser;

    writeln!(
        out,
        "pub fn {fn_name}(table: &crate::proto::DataTable) -> Result<Vec<{type_name}>, DecodeError> {{"
    )
    .unwrap();

    out.push_str("    let h: Vec<&str> = table.headers.iter().map(|s| s.as_str()).collect();\n");

    // Phase 1: resolve the column layout once.
    //
    // Required header guards. A missing required header on a non-empty
    // response is a schema drift: silently returning `Ok(vec![])` masked real
    // data loss for ~1M-row `TradeQuoteTick` responses (P11). We raise
    // `DecodeError::MissingRequiredHeader` whenever the server returns rows
    // without the declared header — eod-style parsers included, so a fully
    // header-drifted EOD response errors instead of yielding zero-seeded ticks.
    // Empty responses ("no trades today") still return `Ok(vec![])` because a
    // vendor holiday / symbol with no activity is legitimate.
    for req in &def.required {
        let var = format!("{req}_idx");
        writeln!(
            out,
            "    let {var} = match find_header(&h, \"{req}\") {{\n\
             \x20       Some(i) => i,\n\
             \x20       None => {{\n\
             \x20           if table.data_table.is_empty() {{\n\
             \x20               return Ok(vec![]);\n\
             \x20           }}\n\
             \x20           return Err(DecodeError::MissingRequiredHeader {{\n\
             \x20               header: \"{req}\",\n\
             \x20               rows: table.data_table.len(),\n\
             \x20               available: h.join(\",\"),\n\
             \x20           }});\n\
             \x20       }}\n\
             \x20   }};"
        )
        .unwrap();
    }
    if !def.required.is_empty() {
        out.push('\n');
    }

    // Optional column lookups (alias-aware).
    for col in &def.columns {
        if def.required.contains(&col.name) {
            // Already resolved above as required.
            continue;
        }
        let var = format!("{}_idx", col.name);
        writeln!(out, "    let {var} = find_header(&h, \"{}\");", col.name).unwrap();
    }

    // Contract identification columns (wildcard queries inject
    // expiration/strike/right under their exact wire names).
    if def.contract_id {
        out.push_str("    let _cid_exp_idx = h.iter().position(|c| *c == \"expiration\");\n");
        out.push_str("    let _cid_strike_idx = h.iter().position(|c| *c == \"strike\");\n");
        out.push_str("    let _cid_right_idx = h.iter().position(|c| *c == \"right\");\n");
    }

    out.push('\n');

    // Phase 2: seed the output, then bulk-extract column by column in
    // cache-resident row blocks. Columns absent from the wire response
    // keep the seed value — the same zero-fill the per-cell decode
    // applied row by row.
    out.push_str("    let rows = table.data_table.as_slice();\n");
    out.push_str("    let mut ticks = vec![\n");
    writeln!(out, "        {type_name} {{").unwrap();
    for col in &def.columns {
        let (_, seed) = column_decoder(def, col);
        writeln!(out, "            {}: {seed},", col.field).unwrap();
    }
    if def.contract_id {
        out.push_str("            expiration: 0,\n");
        out.push_str("            strike: 0.0,\n");
        out.push_str("            right: '\\0',\n");
    }
    out.push_str("        };\n");
    out.push_str("        rows.len()\n");
    out.push_str("    ];\n");

    out.push_str("    let mut row_base = 0_usize;\n");
    out.push_str("    for (rows, ticks) in rows\n");
    out.push_str("        .chunks(crate::decode::column::BLOCK_ROWS)\n");
    out.push_str("        .zip(ticks.chunks_mut(crate::decode::column::BLOCK_ROWS))\n");
    out.push_str("    {\n");

    for col in &def.columns {
        let var = format!("{}_idx", col.name);
        let (decoder, fill) = column_decoder(def, col);
        let name = &col.name;
        let field = &col.field;
        let call = format!(
            "crate::decode::column::extract_column(rows, ticks, row_base, {{idx}}, \"{name}\", {fill}, {decoder}, |t, v| t.{field} = v)?;"
        );
        if def.required.contains(&col.name) {
            writeln!(out, "        {}", call.replace("{idx}", &var)).unwrap();
        } else {
            writeln!(out, "        if let Some(idx) = {var} {{").unwrap();
            writeln!(out, "            {}", call.replace("{idx}", "idx")).unwrap();
            out.push_str("        }\n");
        }
    }

    // Contract identification fields (injected when contract_id = true). The
    // `expiration` column legitimately arrives as Number (YYYYMMDD) or Text
    // (ISO "2026-04-13") depending on upstream version — dispatch on the
    // cell's own type rather than coalescing silently. `strike` decodes
    // through the same Price|Number accept-set as every price column;
    // `right` decodes both wire encodings (Number ASCII code, Text
    // "CALL"/"C"/"PUT"/"P") to the logical character — strict dispatch.
    if def.contract_id {
        out.push_str("        if let Some(idx) = _cid_exp_idx {\n");
        out.push_str("            crate::decode::column::extract_column(rows, ticks, row_base, idx, \"expiration\", 0, row_contract_expiration, |t, v| t.expiration = v)?;\n");
        out.push_str("        }\n");
        out.push_str("        if let Some(idx) = _cid_strike_idx {\n");
        out.push_str("            crate::decode::column::extract_column(rows, ticks, row_base, idx, \"strike\", 0.0, row_price_f64, |t, v| t.strike = v)?;\n");
        out.push_str("        }\n");
        out.push_str("        if let Some(idx) = _cid_right_idx {\n");
        out.push_str("            crate::decode::column::extract_column(rows, ticks, row_base, idx, \"right\", '\\0', row_contract_right, |t, v| t.right = v)?;\n");
        out.push_str("        }\n");
    }

    out.push_str("        row_base += rows.len();\n");
    out.push_str("    }\n");
    out.push_str("    Ok(ticks)\n");
    out.push_str("}\n\n");
}

/// Emit `impl WireColumns for <Tick>` next to the parser. It resolves the
/// same schema columns through the same alias-aware lookup the parser uses,
/// via the shared `crate::columns::present_columns_from` helper.
///
/// The contract this produces is the response's *physical wire-column set*,
/// deduplicated by first-claim — NOT the set of struct fields the parser
/// filled. One physical header can feed more than one struct field (an alias
/// resolves several schema names to the same wire spelling), and the parser
/// fills every such field from that one column. `present_columns_from` counts
/// each physical header once (first-claim, exact matches before aliases) but
/// exempts the derived `date` field that is not its own physical column:
/// the `YYYYMMDD` split of a `Timestamp` header, present whenever it
/// resolves. The EOD wire sends `created`: the exact match
/// (`created` -> `created_ms_of_day`) claims the header, and `date` rides the
/// same column via the exemption instead of being dropped.
///
/// The set names the public schema *field* (e.g. `condition_flags`,
/// `expiration`) — the name the Arrow / Polars builders key on — not the
/// wire spelling the alias table resolves. `CalendarDay` is the exception:
/// the hand-written v3 parser renames the wire columns (`type` -> `status`,
/// `open`/`close` -> the time fields), so its generated impl delegates to the
/// matching calendar-specific presence table instead of the generic
/// first-claim helper.
fn generate_present_columns(out: &mut String, type_name: &str, def: &TickTypeDef) {
    writeln!(out, "impl crate::columns::WireColumns for {type_name} {{").unwrap();
    out.push_str("    fn present_columns(headers: &[&str]) -> crate::columns::ColumnPresence {\n");
    if type_name == "CalendarDay" {
        out.push_str("        crate::columns::present_calendar_day_columns(headers)\n");
    } else {
        out.push_str("        const COLS: &[(&str, &str)] = &[\n");
        for col in &def.columns {
            writeln!(
                out,
                "            (\"{name}\", \"{field}\"),",
                name = col.name,
                field = col.field,
            )
            .unwrap();
        }
        out.push_str("        ];\n");
        writeln!(
            out,
            "        crate::columns::present_columns_from(headers, COLS, {})",
            def.contract_id,
        )
        .unwrap();
    }
    out.push_str("    }\n\n");

    // all_columns(): the full-schema column set (schema columns + the
    // contract-id trio), matching
    // the column order the full `to_arrow` / `to_polars` builders emit.
    out.push_str("    fn all_columns() -> crate::columns::ColumnPresence {\n");
    out.push_str("        crate::columns::ColumnPresence::from_names([\n");
    for col in &def.columns {
        writeln!(out, "            \"{}\",", col.field).unwrap();
    }
    if def.contract_id {
        out.push_str("            \"expiration\",\n");
        out.push_str("            \"strike\",\n");
        out.push_str("            \"right\",\n");
    }
    out.push_str("        ])\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");
}

/// Emit `impl ChainSortKey for <Tick>` next to the parser. Tick types
/// carrying the injected contract-identity trio (`contract_id = true`)
/// yield the ranked `(expiration, strike, right)` key the bulk-fetch
/// buffered merge orders chain rows by; every other type yields `None`,
/// so its sharded pulls concatenate in band order. Generated from the
/// same schema flag that injects the trio, so the key and the fields it
/// reads can never drift apart.
fn generate_chain_sort_key(out: &mut String, type_name: &str, def: &TickTypeDef) {
    writeln!(
        out,
        "impl crate::mdds::shard::ChainSortKey for {type_name} {{"
    )
    .unwrap();
    out.push_str("    fn chain_sort_key(&self) -> Option<crate::mdds::shard::ChainKey> {\n");
    if def.contract_id {
        out.push_str(
            "        Some(crate::mdds::shard::ChainKey::new(self.expiration, self.strike, self.right))\n",
        );
    } else {
        out.push_str("        None\n");
    }
    out.push_str("    }\n}\n\n");
}
