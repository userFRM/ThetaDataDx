// `await using session = await tdx.streaming(callback)` (TC39 explicit
// resource management) wrapper for the ThetaDataDxClient FPSS streaming
// lifecycle.
//
// On `[Symbol.asyncDispose]`, `stopStreaming()` is called and then the
// async drain barrier `awaitDrain(5000)` is awaited so the consumer
// thread is guaranteed to have finished firing the registered callback
// before the JS callback closure can be released. This mirrors the
// C++ RAII destructor in `sdks/cpp/src/thetadx.cpp`.
//
// SSOT: every public method on `ThetaDataDxClient` (subscribe_*,
// unsubscribe_*, activeSubscriptions, droppedEventCount, reconnect,
// ...) is reachable on the session via a `Proxy` `get` trap. There is
// NO hand-listed mirror of subscription methods here -- adding a new
// method to the napi-rs `ThetaDataDxClient` makes it callable through the
// session automatically, with zero drift surface.
//
// Loaded as the package's CommonJS entry point (`main` in
// `package.json`) so consumers get the wrapper when they
// `require('thetadatadx')`. The native loader in `index.js` is
// auto-generated by napi-rs and re-exported untouched.

'use strict';

const native = require('./index.js');

// Drain timeout applied on `[Symbol.asyncDispose]`. Matches the C++
// destructor's 5 s budget in `sdks/cpp/src/thetadx.cpp` and the FFI
// free-path budget in `ffi/src/streaming.rs::FREE_DRAIN_TIMEOUT`.
// Cross-binding parity matters more than tunability here -- a slow
// JS callback that needs more than 5 s to drain is already a contract
// violation worth surfacing.
const EXIT_DRAIN_TIMEOUT_MS = 5000;

// Wrapper-defined member names. Lookups for these resolve on the
// session itself; everything else proxies to the underlying
// `ThetaDataDxClient`. Symbol.asyncDispose is included so `await using`
// finds the wrapper's dispose, not anything on the native binding.
const WRAPPER_OWN = new Set(['_tdx', 'constructor']);

class StreamingSession {
  /**
   * Construct a session bound to a `ThetaDataDxClient` instance. Returns a
   * Proxy that forwards every unknown attribute access to the
   * underlying instance, so adding a `subscribe_X` method to the napi
   * binding makes it callable through the session with no drift.
   *
   * @param {InstanceType<typeof native.ThetaDataDxClient>} tdx
   * @returns {StreamingSession}
   */
  constructor(tdx) {
    this._tdx = tdx;
    return new Proxy(this, {
      get(target, prop, receiver) {
        // Wrapper-defined members (Symbol.asyncDispose, _tdx,
        // constructor) take precedence; everything else forwards to
        // the underlying ThetaDataDxClient instance.
        if (WRAPPER_OWN.has(prop) || prop === Symbol.asyncDispose) {
          return Reflect.get(target, prop, receiver);
        }
        if (prop in target && typeof target[prop] !== 'undefined') {
          // Methods explicitly defined on this class (none today
          // beyond the dispose symbol; future hand-written shortcuts
          // would land here).
          const own = Reflect.get(target, prop, receiver);
          if (typeof own === 'function') {
            return own.bind(target);
          }
          return own;
        }
        const tdx = target._tdx;
        const value = tdx[prop];
        // Bind methods to the underlying instance so `this` resolves
        // correctly when the caller invokes them.
        return typeof value === 'function' ? value.bind(tdx) : value;
      },
      has(target, prop) {
        if (WRAPPER_OWN.has(prop) || prop === Symbol.asyncDispose) return true;
        return prop in target._tdx;
      },
    });
  }

  /**
   * TC39 explicit resource management: invoked by `await using session
   * = ...` on scope exit. Stops the streaming connection and awaits
   * the drain barrier so the consumer thread is guaranteed to have
   * finished firing the registered callback before the JS closure can
   * be released. Mirrors the C++ RAII destructor.
   *
   * Drain timeout failures fire `console.warn` rather than throwing;
   * the streaming pipeline is already torn down (`stopStreaming` ran),
   * and the drain barrier is best-effort observability. Throwing here
   * would mask any error from the `using` block body.
   *
   * @returns {Promise<void>}
   */
  async [Symbol.asyncDispose]() {
    this._tdx.stopStreaming();
    const drained = await this._tdx.awaitDrain(EXIT_DRAIN_TIMEOUT_MS);
    if (!drained) {
      console.warn(
        `ThetaDataDxClient streaming drain timed out after ${EXIT_DRAIN_TIMEOUT_MS}ms; ` +
          'consumer callback may still be firing. The JS callback closure ' +
          'will remain referenced until the consumer exits.',
      );
    }
  }
}

// Monkey-patch `streaming(callback)` onto the napi-generated
// `ThetaDataDxClient` class. Returning a `Promise<StreamingSession>` matches
// the spec example (`await using session = await tdx.streaming(cb)`)
// and leaves room for an async startup path in the future without an
// API break.
if (
  native.ThetaDataDxClient &&
  typeof native.ThetaDataDxClient.prototype.streaming !== 'function'
) {
  native.ThetaDataDxClient.prototype.streaming = async function streaming(callback) {
    this.startStreaming(callback);
    return new StreamingSession(this);
  };
}

// Pull-iter delivery. Patch `[Symbol.asyncIterator]` onto the
// napi-bound `EventIterator` class so user code drains it with the
// idiomatic `for await (const event of iter)` shape. The napi-rs
// macros generate the class binding but cannot synthesize a
// well-known-symbol method; the JS shim adds it once at module load,
// ahead of any user instance.
if (
  native.EventIterator &&
  typeof native.EventIterator.prototype[Symbol.asyncIterator] !== 'function'
) {
  native.EventIterator.prototype[Symbol.asyncIterator] = function asyncIterator() {
    const iter = this;
    return {
      async next() {
        const evt = await iter.next();
        if (evt === null || evt === undefined) {
          return { value: undefined, done: true };
        }
        return { value: evt, done: false };
      },
      async return() {
        // `for await` short-circuit (early `break`) calls return().
        // Close the iterator so the worker stops blocking on the
        // queue wait; the underlying streaming session is left
        // running so the user can keep calling subscribe / etc.
        try {
          iter.close();
        } catch (e) {
          // ignore — close is best-effort
        }
        return { value: undefined, done: true };
      },
    };
  };
}

// Re-export the entire native surface plus the StreamingSession class.
// `Object.assign` rather than spread so non-enumerable properties on
// the napi binding survive.
//
// `Contract` aliases `ContractRef` — napi-rs exposes the fluent
// contract type under the `ContractRef` symbol because `Contract`
// already names the FPSS event-payload data class. The public-facing
// surface documented in the quickstart is `Contract.stock("AAPL")` /
// `Contract.option(...)`, so the alias makes the JS export agree with
// the docs without a second pyclass.
module.exports = Object.assign({}, native, {
  StreamingSession,
  Contract: native.ContractRef,
});
